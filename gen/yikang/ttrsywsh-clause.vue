    <h2 class="h2"><em>保险责任</em></h2>
    <p class="p"><em>第五条</em> 在保险期间内，被保险人因遭受意外伤害事故导致身故、伤残的，保险人依照下列约定给付保险金，且给付各项保险金之和不超过意外伤害保险金额：</p>
