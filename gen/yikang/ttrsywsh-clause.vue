      0083-2013）（以下简称《伤残评定标准》）所列伤残的，<em>保险人按《伤残评定标准》所列给付比例乘以保险单载明的意外伤害保险金额给付意外伤害伤残保险金。如果自意外伤害发生之日起第180日内治疗仍未结束的，则按第180日被保险人的身体情况进行伤残评定，并据此给付意外伤害伤残保险金。</em>
