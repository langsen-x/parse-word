    <p class="p"><em>当被保险人在保险期间内因意外伤害导致伤残时，若在保险开始日期之前被保险人的同一部位已有伤残或者在保险期间内被保险人因不同次意外伤害导致同一部位多次伤残的（不含因责任免除事项所致伤残，下同）</em>，保险人将按照被保险人上述各类伤残中最重的伤残等级对应《伤残评定标准》所列的给付比例计算伤残保险金，<em>但上述已有伤残、多次伤残将视同已按约定给付过伤残保险金，在给付本次意外伤害伤残保险金时应予以扣除。</em>
