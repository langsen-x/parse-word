    <p class="p"><em>第十五条</em>
      保险人自收到给付保险金的请求和有关证明、资料之日起六十日内，对其给付保险金的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定给付的数额后，应当支付相应的差额。 </p>
    <h2 class="h2"><em>投保人、被保险人义务</em></h2>
    <p class="p"><em>第十六条</em> 除另有约定外，投保人应当在订立本保险合同时一次性交清保险费。 </p>
    <p class="p"><em>第十七条 </em>订立本保险合同，保险人就被保险人的有关情况提出询问的，投保人应当如实告知。</p>
    <p class="p"><em>投保人故意或者因重大过失未履行前款约定的义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除本合同。</em></p>
    <p class="p">前款约定的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。自本保险合同成立之日起超过二年的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</p>
    <p class="p"><em>投保人故意不履行如实告知义务的，保险人对于本保险合同解除前发生的保险事故，不承担给付保险金责任，并不退还保险费。</em></p>
    <p class="p"><em>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于本保险合同解除前发生的保险事故，不承担给付保险金责任，但应当退还保险费。</em></p>
    <p class="p">保险人在本保险合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</p>
    <p class="p"><em>第十八条</em> 被保险人变更职业或工种时，投保人或被保险人应在10日内以书面形式通知保险人。</p>
    <p class="p">（一）被保险人所变更的职业或工种，在依照保险人的职业分类表进行划分时：</p>
    <p class="p">（1）其危险性降低的，保险人自接到通知后，自职业变更之日起，退还变更前后职业或工种对应的未满期净保费差额；</p>
    <p class="p"><em>（2</em>）其危险性增加的，保险人在接到通知后，自职业变更之日起，增收变更前后职业或工种对应的未满期保险费差额；</p>
    <p class="p"><em>（3）其在拒绝承保范围内的，保险人在接到通知后有权解除本保险合同，并按照接到通知之日的次日退还原职业或工种所对应的未满期净保费。</em></p>
    <p class="p">（二）被保险人的职业或工种发生变更，且在未依照本保险合同的约定书面通知保险人的情况下发生保险事故的：</p>
    <p class="p">（1）依照保险人的职业分类表，被保险人所变更的职业或工种的危险性降低，则保险人仍旧按其原职业或工种计算并给付保险金；</p>
    <p class="p"><em>（2）依照保险人的职业分类表，被保险人所变更的职业或工种的危险性增加，则保险人按其原保险费与新职业或工种所对应的保险费的比例计算并给付保险金；</em></p>
    <p class="p"><em>（3）依照保险人的职业分类表，被保险人所变更的职业或工种在拒绝承保范围内的，保险人不承担给付保险金的责任，并将退还原职业或工种所对应的未满期净保费。</em></p>
    <p class="p"><em>第十九条</em> 投保人住所或通讯地址变更时，应及时以书面形式通知保险人。<em>投保人未通知的，保险人按本保险合同所载的最后住所或通讯地址发送的有关通知，均视为已发送给投保人。</em></p>
    <p class="p"><em>第二十条</em> 在保险期间内，投保人因其人员变动，需增加、减少被保险人时，应以书面形式向保险人提出申请。保险人同意后出具批单，并在本保险合同中批注。</p>
    <p class="p">被保险人人数增加时，保险人在审核同意后，于收到申请之日的次日零时开始承担保险责任，并按约定增收未满期保险费。</p>
    <p class="p">被保险人人数减少时，保险人在审核同意后，于收到申请之日的次日零时起，对减少的被保险人终止保险责任，并按约定退还未满期净保费，<em>但减少的被保险人本人或其保险金申请人已领取过任何保险金的，保险人不退还未满期净保费。减少后的被保险人人数不足其在职人员75%或人数标准不满足监管规定时，保险人有权解除本保险合同，并按约定退还未满期净保费。</em>
    </p>
    <p class="p"><em>第二十一条</em> 投保人、被保险人或者保险金受益人知道保险事故发生后，应当及时通知保险人。<em>故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金责任，</em>但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。
    </p>
    <p class="p">上述约定，不包括因不可抗力而导致的迟延。</p>
    <h2 class="h2"><em>保险金的申请与给付</em></h2>
    <p class="p"><em>第二十二条 </em> 保险金申请人向保险人申请给付保险金时，应提交以下材料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。<em>保险金申请人未能提供有关材料，导致保险人无法核实该申请的真实性的，保险人对无法核实部分不承担给付保险金的责任。</em>
    </p>
    <p class="p"><em>（一）身故保险金申请</em></p>
    <p class="p">（1）索赔申请书；</p>
    <p class="p">（2）保险单原件；</p>
    <p class="p">（3）保险金申请人的身份证明；</p>
    <p class="p">（4）公安部门出具的被保险人户籍注销证明、二级以上（含二级）或保险人认可的医疗机构出具的被保险人身故证明书。若被保险人为宣告死亡，保险金申请人应提供人民法院出具的宣告死亡证明文件；</p>
    <p class="p">（5）保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；</p>
    <p class="p">（6）若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。</p>
    <p class="p"><em>（二）伤残保险金申请</em></p>
    <p class="p">（1）索赔申请书；</p>
    <p class="p">（2）保险单原件；</p>
    <p class="p">（3）被保险人身份证明；</p>
    <p class="p">（4）二级以上（含二级）或保险人认可的医疗机构或司法鉴定机构出具的伤残鉴定书；</p>
    <p class="p">（5）保险金申请人所能提供的其他与本项申请相关的材料；</p>
    <p class="p">（6）若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。</p>
    <p class="p"><em>第二十三条</em> 保险金申请人向保险人请求给付保险金的诉讼时效期间以现行有效的《中华人民共和国保险法》的规定为准，自其知道或者应当知道保险事故发生之日起计算。</p>
    <h2 class="h2"><em>争议处理和法律适用</em></h2>
    <p class="p"><em>第二十四条</em> 因履行本保险合同发生的争议，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁；保险单未载明仲裁机构或者争议发生后未达成仲裁协议的，依法向具有管辖权的人民法院起诉。
    </p>
    <p class="p"><em>第二十五条 </em> 与本保险合同有关的以及履行本保险合同产生的一切争议处理适用中华人民共和国法律（不包括港澳台地区法律）。</p>
    <h2 class="h2"><em>其他事项</em></h2>
    <p class="p"><em>第二十六条</em> 在本保险合同成立后，投保人可以书面形式通知保险人解除合同，<em>但《中华人民共和国保险法》另有规定的除外。</em></p>
    <p class="p">投保人解除本保险合同时，应提供下列证明文件和资料：</p>
    <p class="p">（一）保险合同解除申请书；</p>
    <p class="p">（二）保险单原件；</p>
    <p class="p">（三）保险费交付凭证；</p>
    <p class="p">（四）投保人/被保险人身份证明；</p>
    <p class="p">（五）保险人要求的其他有关证明和资料。</p>
    <p class="p">投保人要求解除本保险合同的，经保险人审核同意并自保险人接到保险合同解除申请书之时起，本保险合同的效力终止。保险人收到上述证明文件和资料之日起30日内退还保险单的未满期净保费，<em>但被保险人已提出有效的赔偿申请或者保险人已根据本保险合同约定给付保险金的除外</em>。
    </p>
    <p class="p"><em>第二十七条</em>
      在保险期间内，经投保人与保险人双方约定，可以采用附加批注或批单的方式变更本保险合同的有关内容。附加批注或批单是本保险合同的有效组成部分，本保险合同条款与附加批注或批单不一致之处，以附加批注或批单为准，附加批注或批单未尽之处，以本保险合同条款为准。
    </p>
    <h2 class="h2">释义</h2>
    <p class="p"><em>【</em>1】周岁<em>：以法定身份证明文件中记载的出生日期为基础计算的实足年龄。</em></p>
    <p class="p"><em>【</em>2】保险人<em>：指与投保人签订本保险合同的永诚财产保险股份有限公司各分支机构。</em></p>
    <p class="p"><em>【3】</em>意外伤害：指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</p>
    <p class="p"><em>【4】</em>伤残：因意外伤害损伤所致的人体残疾。</p>
    <p class="p"><em>【5】</em>身体结构：指身体的解剖部位，如器官、肢体及其组成部分。</p>
    <p class="p"><em>【6】</em>身体功能：指身体各系统的生理功能。</p>
    <p class="p"><em>【7】</em>无有效驾驶证:</p>
    <p class="p"><em>指被保险人存在下列情形之一者：</em></p>
    <p class="p"><em>（1）无驾驶证或驾驶证有效期已届满；</em></p>
    <p class="p"><em>（2）驾驶的机动车与驾驶证载明的准驾车型不符；</em></p>
    <p class="p"><em>（3）实习期内驾驶公共汽车、营运客车或者载有爆炸物品、易燃易爆化学物品、剧毒或者放射性等危险物品的机动车，实习期内驾驶的机动车牵引挂车；</em></p>
    <p class="p"><em>（4）持未按规定审验的驾驶证，以及在暂扣、扣留、吊销、注销驾驶证期间驾驶机动车；</em></p>
    <p class="p"><em>（5）使用各种专用机械车、特种车的人员无国家有关部门核发的有效操作证，驾驶营业性客车的驾驶人无国家有关部门核发的有效资格证书；</em></p>
    <p class="p"><em>（6）依照法律法规或公安机关交通管理部门有关规定不允许驾驶机动车的其他情况下驾车。</em></p>
    <p class="p"><em>【8</em>】<em>无有效行驶证:</em></p>
    <p class="p"><em>指下列情形之一：</em></p>
    <p class="p"><em>（1）机动车被依法注销登记的；</em></p>
    <p class="p"><em>（2）无公安机关交通管理部门核发的行驶证、号牌，或临时号牌或临时移动证的机动交通工具；</em></p>
    <p class="p"><em>（3）未在规定检验期限内进行机动车安全技术检验或检验未通过的机动交通工具。未依法按时进行或通过机动车安全技术检验。</em></p>
    <p class="p"><em>【9】</em>未满期净保费:</p>
    <p class="p"><em>未满期净保费=保险费×[1-(保险单已经过天数/保险期间天数)]
      ×（1-退保手续费率）。经过天数不足一天的按一天计算。退保手续费率将由保险人和投保人在投保时约定，并在保险单上载明，若未约定退保手续费率的，则退保手续费率默认取值为25%。</em></p>
    <p class="p"><em>【10</em>】<em>未满期保险费:</em></p>
    <p class="p"><em>未满期保险费=保险费×[1-(保险单已经过天数/保险期间天数)]。经过天数不足一天的按一天计算。</em></p>
    <p class="p"><em>【11</em>】保险金申请人<em>：指受益人或被保险人的继承人或依法享有保险金请求权的其他自然人。</em></p>
    <p class="p"><em>【12</em>】不可抗力：<em>指不能预见、不能避免并不能克服的客观情况。</em></p>
    <p class="p"><em>【13</em>】猝死：<em>一个貌似健康的人，由于患有潜在的疾病或机能障碍，发生突然的、出人意外的非暴力死亡（自然死亡）。</em></p>
    <p class="p">
      【14】感染艾滋病病毒（HIV）或患艾滋病（AIDS）：艾滋病病毒指人类免疫缺陷病毒，英文缩写为HIV。艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合征，英文缩写为AIDS。在人体血液或其它样本中检测到艾滋病病毒或其抗体呈阳性，没有出现临床症状或体征的，为感染艾滋病病毒；如果同时出现了明显临床症状或体征的，为患艾滋病。</p>
    <p class="p">
      【15】高风险运动：指比一般常规性的运动风险等级更高、更容易发生人身伤害的运动，在进行此类运动前需有充分的心理准备和行动上的准备，必须具备一般人不具备的相关知识和技能或者必须在接受专业人士提供的培训或训练之后方能掌握。被保险人进行此类运动时须具备相关防护措施或设施，以避免发生损失或减轻损失，包括但不限于潜水，滑水，滑雪，滑冰，驾驶或乘坐滑翔翼、滑翔伞，跳伞，攀岩运动，探险活动，武术比赛，摔跤比赛，柔道，空手道，跆拳道，马术，拳击，特技表演，驾驶卡丁车，赛马，赛车，各种车辆表演，蹦极。</p>
    <p class="p">【16】恐怖主义行为：指任何人或团体因政治、宗教、意识形态或类似目的，通过武力、暴力威胁表示或其他方式，意图影响政府、公众或令其恐慌所采取的任何行动或以任何方式控制、阻止或压制所引致损害的行为。</p>
    <p class="p">
      【17】职业/半职业体育运动：是一种追求竞技比赛票房价值、以商业牟利为目的的竞技体育活动。其中，职业/半职业运动员一般均有明确的运动训练目标与计划安排，且职业运动员以此为生，而半职业运动员通常拥有第二份正常工作。</p>
