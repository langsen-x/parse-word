        <h1 class="h1">团体重大疾病保险条款（2020 版）</h1>
        <h2 class="h2"><em>备案号：(众诚保险)(备-疾病保险)【2021】(主) 002 号</em></h2>
        <h2 class="h2"><em>总则</em></h2>
        <p class="p"><em>第一条 </em>本保险合同由保险条款、投保单、保险单、保险凭证以及批单等组成，以确定投保人、被保险人和保险人的权利与义务。凡涉及本保险合同的约定，均应采用书面形式。</p>
        <p class="p"><em>第二条 </em>凡在中华人民共和国境内（<em>港澳台地区除外</em>，下同），投保时身体健康、能正常工作或正常劳动的、符合本保险合同约定年龄的城镇职工和城乡居民，可作为本保险合同的被保险人。<em>被保险人在合同签发时不得少于
          3 人。</em></p>
        <p class="p"><em>第三条 </em>凡符合中华人民共和国法律（<em>不包括港澳台地区法律</em>，下同） 规定，本保险合同订立时对被保险人有保险利益的机关、企业、事业单位和社会团体均可作为投保人。</p>
        <p class="p"><em>第四条 </em>除另有约定外，重大疾病保险金受益人为被保险人本人。</p>
        <h2 class="h2"><em>保险责任</em></h2>
        <p class="p"><em>第五条 </em>在保险期间内，保险人按如下约定承担保险责任：</p>
        <p class="p"><em>被保险人于等待期（30 日）后</em>经医院初次确诊患本保险合同约定的重大疾病（以下简称“重大疾病”，具体疾病种类及疾病定义以本条款释义为准），保险人按本合同约定的保险金额及给付比例，给付保险金<em>。</em>本合同的保险金包括
          “重度疾病保险金”和“轻度疾病保险金”，投保时是身体健康的被保险人，可以投保本保险项下的轻度疾病保险责任和重度疾病保险责任，保险人支付对应保险金；<em>被保险人已发生过轻度疾病的，续保可以投保本保险项下的重度疾病保险责任，保险人支付重度疾病保险金；已发生过重度疾病的被保险人，不可以投保本保险。</em>具体如下：
        </p>
        <p class="p">（一）重度疾病保险金</p>
        <p class="p">被保险人经医院确诊初次罹患本条款释义中“重度疾病”所定义的“重度疾病”，且此前未发生过本条款项下的“重度疾病”，保险人按照本条款基本保险金额的
          100%一次性给付重度疾病保险金。<em>“重度疾病保险金”仅给付一次。赔偿后，保险人对该被保险人的本合同项下所有保险责任全部终止。</em></p>
        <p class="p">（二）轻度疾病保险金</p>
        <p class="p">被保险人经医院确诊初次罹患本条款释义中“轻度疾病”所定义的“轻度疾病”，且此前未发生过本条款项下的“重大疾病”，保险人按照约定的给付比例一次性给付轻度疾病保险金。<em>“轻度疾病保险金”仅给付一次。赔偿后，保险人对该被保险人的所有轻度疾病保险责任终止。</em>
        </p>
        <p class="p"><em>轻度疾病保险金=重大疾病保险金×给付比例。该给付比例不超过 30%， 具体由投保人、保险人双方约定，并在保险单中载明。</em></p>
        <p class="p"><em>被保险人于等待期30 日内经医院确诊其初次患本合同约定的重大疾病， 保险人对投保人无息返还该被保险人对应的所交保险费，对该被保险人的 保险责任终止。</em></p>
        <p class="p"><em>本保险合同的续保不适用等待期。</em></p>
        <h2 class="h2"><em>责任免除</em></h2>
        <p class="p"><em>第六条 因下列情形之一，导致被保险人发生本保险合同约定的保险事故的，保险人不承担保险责任：</em></p>
        <p class="p"><em>（一）投保人对被保险人的故意杀害、故意伤害；</em></p>
        <p class="p"><em>（二）被保险人故意犯罪或抗拒依法采取的刑事强制措施；</em></p>
        <p class="p"><em>（三）被保险人故意自伤或自杀，但被保险人自杀时为无民事行为能力人的除外；</em></p>
        <p class="p"><em>（四）被保险人服用、吸食或注射毒品；</em></p>
        <p class="p"><em>（五）被保险人酒后驾驶、无合法有效驾驶证驾驶，或驾驶无有效行驶证的机动车；</em></p>
        <p class="p"><em>（六）被保险人患艾滋病或感染艾滋病病毒，因职业关系、输血原因及器官移植原因导致的感染艾滋病病毒不在此限；</em></p>
        <p class="p"><em>（七）战争、军事冲突、暴乱或武装叛乱；</em></p>
        <p class="p"><em>（八）核爆炸、核辐射或核污染；</em></p>
        <p class="p"><em>（九）遗传性疾病，先天性畸形、变形或染色体异常；</em></p>
        <p class="p"><em>（十）其他免除情形见释义重大疾病中加粗加黑字体的内容。</em></p>
        <p class="p"><em>发生上述情形（除投保人、被保险人故意制造保险事故的），被保险人身故的，保险人对该被保险人保险责任终止，并对投保人按日计算退还该被保险人的未满期保险费。投保人、被保险人故意制造保险事故的，保险人有权解除合同，并不退还保险费。</em>
        </p>
        <h2 class="h2"><em>保险金额和保险费</em></h2>
        <p class="p"><em>第七条 保险金额是保险人承担给付保险金的最高限额。</em></p>
        <p class="p">本保险重大疾病保险金额由投保人、保险人双方约定，并在保险单中载明。</p>
        <p class="p"><em>除另有约定外，投保人应在保险合同成立时交清保险费，投保人未按约定交付保险费的，保险合同不发生效力，合同生效前发生的保险事故，</em></p>
        <p class="p"><em>保险人不承担给付保险金的责任。</em></p>
        <h2 class="h2"><em>保险期间</em></h2>
        <p class="p"><em>第八条 本保险合同为非保证续保合同。</em>本保险期间<em>最长不超过一年</em>， 以保险单载明的起讫时间为准。<em>保险期间届满，投保人需要重新向保险人申请投保本保险，交纳保险费，并获得新的保险合同。</em>
        </p>
        <h2 class="h2"><em>保险人义务</em></h2>
        <p class="p"><em>第九条 </em>订立本保险合同时，采用保险人提供的格式条款的，保险人向投保人提供的投保单应当附格式条款，保险人应当向投保人说明本保险合同的内容。</p>
        <p class="p">
          对本保险合同中免除保险人责任的条款，保险人在订立合同时应当在投保单、保险单或者其他保险凭证上作出足以引起投保人注意的提示，并对该条款的内容以书面或口头形式向投保人作出明确说明；未作提示或明确说明的，该条款不产生效力。</p>
        <p class="p"><em>第十条 </em>本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</p>
        <p class="p"><em>第十一条 </em>投保人和保险人可以协商变更合同内容。</p>
        <p class="p">变更保险合同的，应当由保险人在保险单或者其他保险凭证上批注或者附贴批单，或者由投保人和保险人订立变更的书面协议。</p>
        <p class="p"><em>第十二条 </em>保险人依本条款第十六条取得的合同解除权，自保险人知道有解除事由之日起，<em>超过 30 日</em>不行使而消灭。</p>
        <p class="p"><em>第十三条 </em>保险人按照本条款第十九条的约定，认为被保险人提供的有关索赔的证明和资料不完整的，应当及时一次性通知投保人、被保险人补充提供。</p>
        <p class="p"><em>第十四条 </em>保险人收到被保险人的给付保险金的请求后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在 <em>30 日内</em>作出核定，但合同另有约定的除外。</p>
        <p class="p">保险人应当将核定结果通知被保险人；对属于保险责任的，在与被保险人达成给付保险金的协议之次日起 <em>10 日内</em>，履行给付保险金义务。保险合同对给付保险金期限有约定的，保险人应当按照约定履行给付保险金的义务。保险人未及时履行前款规定义务的，除支付保险金外，应当赔偿被保险人因此受到的损失。任何单位和个人不得非法干预保险人履行给付保险金的义务，也不得限制被保险人取得保险金的权利。
        </p>
        <p class="p"><em>保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起 3 日内向被保险人发出拒绝给付保险金通知书，并说明理由。</em></p>
        <p class="p"><em>第十五条 </em>保险人自收到给付保险金的请求和有关证明、资料之日起 <em>60 日内</em>，对其给付保险金的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定给付的数额后，应当支付相应的差额。
        </p>
        <h2 class="h2"><em>投保人、被保险人义务</em></h2>
        <p class="p"><em>第十六条 投保人应当如实填写投保单并回答保险人提出的询问，履行如实告知义务。</em></p>
        <p class="p"><em>投保人故意或者因重大过失未履行前款规定的如实告知义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除本保险合同。保险合同自保险人的解约通知书到达投保人时解除。</em>
        </p>
        <p class="p"><em>投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，并不退还保险费。</em></p>
        <p class="p"><em>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任， 但应当退还保险费。</em></p>
        <p class="p">保险人在合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</p>
        <p class="p"><em>第十七条 投保人住所或通讯地址变更时，应及时书面通知保险人。投保人未通知的，保险人按本保险合同所载的住所或通讯地址发送的有关通知，均视为已送达投保人。</em></p>
        <p class="p"><em>第十八条
          投保人、被保险人知道保险事故发生后，应当及时通知保险人。其故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金责任， </em>但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。
        </p>
        <p class="p"><em>上述约定，不包括因不可抗力而导致的迟延。</em></p>
        <h2 class="h2"><em>赔偿处理</em></h2>
        <p class="p"><em>第十九条 </em>保险金申请人向保险人申请给付保险金时，应提交以下与确认保险事故的性质、原因、损失程度等有关的证明和资料。<em>保险金申请人因特殊原因不能提供以下材料的，应提供其他具有同等证明效力的合法有效的材料。</em>
        </p>
        <p class="p"><em>（一）保险金给付申请书；</em></p>
        <p class="p"><em>（二）保险单原件；</em></p>
        <p class="p"><em>（三）被保险人身份证明；</em></p>
        <p class="p"><em>（四）符合本条款释义的医院出具的附有病理显微镜检查、血液检验</em></p>
        <p class="p"><em>及其他科学方法检验报告的疾病诊断证明书；</em></p>
        <p class="p"><em>（五）保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；</em></p>
        <p class="p"><em>（六）若保险金申请人委托他人申请的，还应提供经公证的授权委托书原件、委托人和受托人的身份证明等相关证明文件。</em></p>
        <p class="p"><em>保险金申请人未履行前款约定的索赔材料提供义务，导致保险人无法核实损失情况的，保险人对无法核实的部分不承担赔偿责任。</em></p>
        <p class="p"><em>第二十条 保险金申请人向保险人请求给付保险金的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</em></p>
        <h2 class="h2"><em>争议处理和法律适用</em></h2>
        <p class="p"><em>第二十一条 </em>因履行本保险合同发生的争议，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁；保险合同未载明仲裁机构或者争议发生后未达成仲裁协议的，依法向有管辖权的人民法院起诉。
        </p>
        <p class="p"><em>第二十二条 </em>与本保险合同有关的以及履行本保险合同产生的一切争议处理适用中华人民共和国法律（<em>不包括港澳台地区法律</em>）。</p>
        <h2 class="h2"><em>其他事项</em></h2>
        <p class="p"><em>第二十三条 </em>被保险人的投保年龄按周岁计算。投保人应在投保时将被保险人的真实年龄在投保单上写明，如果发生错误，保险人按照下列规定办理：</p>
        <p class="p"><em>（一）投保人申报的被保险人年龄不真实，并且其真实年龄超出本保险合同约定年龄限制的，保险人可以有权解除本保险合同或取消该被保险人资格，并向投保人退还该被保险人的未满期保险费；</em></p>
        <p class="p"><em>（二）投保人申报的被保险人年龄不真实，致使投保人支付的保险费少于应付保险费的，保险人有权更正并要求投保人补交保险费，或者在给付保险金时按照实付保险费与应付保险费的比例支付；</em></p>
        <p class="p"><em>（三）投保人申报的被保险人年龄不真实，致使投保人支付的保险费多于应付保险费的，保险人应当将多收的保险费无息退还投保人。</em></p>
        <p class="p"><em>第二十四条 被保险人发生变动时，按下列约定执行：</em></p>
        <p class="p"><em>（一）投保人因在职人员变动等原因需要增加被保险人的，应书面通知保险人，保险人审核同意，于收取相应保险费的次日零时起开始承担保险责任；</em></p>
        <p class="p"><em>（二）投保人因被保险人离职或其他原因退保时，应书面通知保险人，
          该被保险人资格自通知到达保险人之次日零时起丧失。如投保人要求的退保日期在通知到达日之后，则该被保险人资格自投保人要求的退保日零时起丧失。保险人对投保人退还该被保险人的未满期保险费。</em></p>
        <p class="p"><em>第二十五条 在本保险合同成立后，投保人可以书面形式通知保险人解除合同，但保险人已根据本保险合同约定给付保险金的除外。</em></p>
        <p class="p"><em>投保人解除本保险合同时，应提供下列证明文件和资料：</em></p>
        <p class="p"><em>（一）保险合同解除申请书；</em></p>
        <p class="p"><em>（二）保险单原件；</em></p>
        <p class="p"><em>（三）保险费交付凭证；</em></p>
        <p class="p"><em>（四）投保人身份证明或身份资格证明；</em></p>
        <p class="p"><em>（五）已通知被保险人退保的有效证明。</em></p>
        <p class="p">投保人要求解除本保险合同，保险人应当自收到解除合同通知之日起 30 日内，按照合同约定退还保险单的未满期保险费。</p>
        <p class="p">投保人解除本保险合同的，保险人应当按照中国银行保险监督管理委员会有关团体保险退保的规定将退保金通过银行转账或者原投保资金汇入路径退至投保人缴费账户，若出现下列情况，则退至其他指定账户：</p>
        <p class="p">
          （一）投保人原交款账户销户或原交款账户存在异常状态导致无法转账成功的，经投保人或者承继投保人权利的人提供有效证明文件，退保金可以通过银行转账至其指定账户，该指定账户应属于投保人或者承继投保人权利的人；</p>
        <p class="p">（二）投保人收入和支出账户不一致的，经投保人提供有效证明文件， 退保金可以通过银行转账至投保人指定账户，该指定账户应属于投保人；</p>
        <p class="p">（三）投保人为党政机关、事业单位且根据有关规定应将退保金转账至财政或国库账户的，或者按照仲裁结果或法院判决应将退保金转账至仲裁机构或法院指定账户的；</p>
        <p class="p">（四）经投保人提供有效证明文件，被保险人个人缴纳的保费可以退还给被保险人。</p>
        <h2 class="h2"><em>释义</em></h2>
        <p class="p"><em>【保险人】</em>指与投保人签订本保险合同的众诚汽车保险股份有限公司。</p>
        <p class="p"><em>【等待期】</em>从保险合同生效日起，至保险人开始承担给付保险金责任之日的一段期间。除另有约定外，本合同生效日起 30 日内（含第 30
