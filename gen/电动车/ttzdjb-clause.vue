        <p class="p"><em>【重大疾病】</em>指由专科医生明确诊断的下列疾病：</p>
        <p class="p"><em>重度疾病：指以下第（一）至（二十八）项的疾病</em></p>
        <p class="p">（一）恶性肿瘤——重度</p>
        <p class="p">指恶性细胞不受控制的进行性增长和扩散、浸润和破坏周围正常组织，
          可以经血管、淋巴管和体腔扩散转移到身体其他部位，病灶经组织病理学检查（涵盖骨髓病理学检查）结果明确诊断，临床诊断属于世界卫生组织</p>
        <p class="p">（WHO，World Health
          Organization）《疾病和有关健康问题的国际统计分类》第十次修订版（ICD－10）的恶性肿瘤类别及《国际疾病分类肿瘤学专辑》第三版（ICD-O-3）的肿瘤形态学编码属于 3、6、9（恶性肿瘤）范畴的疾病。</p>
        <p class="p"><em>下列疾病不属于“恶性肿瘤——重度”，不在保障范围内：</em></p>
        <p class="p"><em>（1）ICD-O-3肿瘤形态学编码属于0（良性肿瘤）、1（动态未定性肿瘤） 、 2（原位癌和非侵袭性癌）范畴的疾病，如：</em></p>
        <p class="p"><em>a.原位癌，癌前病变，非浸润性癌，非侵袭性癌，肿瘤细胞未侵犯基底层，上皮内瘤变，细胞不典型性增生等；</em></p>
        <p class="p"><em>b.交界性肿瘤，交界恶性肿瘤，肿瘤低度恶性潜能，潜在低度恶性肿瘤等；</em></p>
        <p class="p"><em>（2）TNM分期为Ⅰ期或更轻分期的甲状腺癌；</em></p>
        <p class="p"><em>（3）TNM分期为T1N0M0期或更轻分期的前列腺癌；</em></p>
        <p class="p"><em>（4）黑色素瘤以外的未发生淋巴结和远处转移的皮肤恶性肿瘤；</em></p>
        <p class="p"><em>（5）相当于Binet分期方案A期程度的慢性淋巴细胞白血病；</em></p>
        <p class="p"><em>（6）相当于Ann Arbor分期方案I期程度的何杰金氏病；</em></p>
        <p class="p"><em>（7）未发生淋巴结和远处转移且WHO分级为G1级别（核分裂像&lt;10/50 HPF和ki-67≤2%）或更轻分级的神经内分泌肿瘤。</em></p>
        <p class="p">（二）较重急性心肌梗塞</p>
        <p class="p">急性心肌梗死指由于冠状动脉阻塞或梗阻引起部分心肌严重的持久性缺血造成急性心肌坏死。急性心肌梗死的诊断必须依据国际国内诊断标准，
          符合（1）检测到肌酸激酶同工酶（CK-MB）或肌钙蛋白（cTn）升高和/或降低的动态变化，至少一次达到或超过心肌梗死的临床诊断标准；（2）同时存在下列之一的证据，包括：缺血性胸痛症状、新发生的缺血性心电图改变、新生成的病理性Q波、影像学证据显示有新出现的心肌活性丧失或新出现局部室壁运动异常、冠脉造影证实存在冠状动脉血栓。</p>
        <p class="p">较重急性心肌梗死指依照上述标准被明确诊断为急性心肌梗死，并且必须同时满足下列至少一项条件：</p>
        <p class="p">（1）心肌损伤标志物肌钙蛋白（cTn）升高，至少一次检测结果达到该检验正常参考值上限的15倍（含）以上；</p>
        <p class="p">（2）肌酸激酶同工酶（CK-MB）升高，至少一次检测结果达到该检验正常参考值上限的2倍（含）以上；</p>
        <p class="p">（3）出现左心室收缩功能下降，在确诊6周以后，检测左室射血分数（LVEF）低于50%（不含）；</p>
        <p class="p">（4）影像学检查证实存在新发的乳头肌功能失调或断裂引起的中度（含）以上的二尖瓣反流；</p>
        <p class="p">（5）影像学检查证实存在新出现的室壁瘤；</p>
        <p class="p">（6）出现室性心动过速、心室颤动或心源性休克。</p>
        <p class="p"><em>其他非冠状动脉阻塞性疾病所引起的肌钙蛋白（cTn）升高不在保障范围内。</em></p>
        <p class="p">（三）严重脑中风后遗症</p>
        <p class="p">
          指因脑血管的突发病变引起脑血管出血、栓塞或梗塞，须由头颅断层扫描（CT）、核磁共振检查（MRI）等影像学检查证实，并导致神经系统永久性的功能障碍。神经系统永久性的功能障碍，指疾病确诊180天后，仍遗留下列至少一种障碍：</p>
        <p class="p">1．一肢（含）以上肢体肌力2级（含）以下；</p>
        <p class="p">2．语言能力完全丧失，或严重咀嚼吞咽功能障碍；</p>
        <p class="p">3．自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <p class="p">（四）重大器官移植术或造血干细胞移植术</p>
        <p class="p">重大器官移植术，指因相应器官功能衰竭，已经实施了肾脏、肝脏、心脏、肺脏或小肠的异体移植手术。</p>
        <p class="p">造血干细胞移植术，指因造血功能损害或造血系统恶性肿瘤，已经实</p>
        <p class="p">施了造血干细胞（包括骨髓造血干细胞、外周血造血干细胞和脐血造血干细胞）的移植手术。</p>
        <p class="p">（五）冠状动脉搭桥术（或称冠状动脉旁路移植术）</p>
        <p class="p">指为治疗严重的冠心病，实际实施了切开心包进行的冠状动脉血管旁路移植的手术。</p>
        <p class="p"><em>所有未切开心包的冠状动脉介入治疗不在保障范围内。</em></p>
        <p class="p">（六）严重慢性肾衰竭</p>
        <p class="p">指双肾功能慢性不可逆性衰竭，依据肾脏病预后质量倡议（K/DOQI）
          制定的指南，分期达到慢性肾脏病5期，且经诊断后已经进行了至少90天的规律性透析治疗。规律性透析是指每周进行血液透析或每天进行腹膜透析。</p>
        <p class="p">（七）多个肢体缺失</p>
        <p class="p">指因疾病或意外伤害导致两个或两个以上肢体自腕关节或踝关节近端</p>
        <p class="p">（靠近躯干端）以上完全性断离。</p>
        <p class="p">（八）急性重症肝炎或亚急性重症肝炎</p>
        <p class="p">指因肝炎病毒感染引起肝脏组织弥漫性坏死，导致急性肝功能衰竭， 且经血清学或病毒学检查证实，并须满足下列全部条件：</p>
        <p class="p">1．重度黄疸或黄疸迅速加重；</p>
        <p class="p">2．肝性脑病；</p>
        <p class="p">3．B超或其他影像学检查显示肝脏体积急速萎缩；</p>
        <p class="p">4．肝功能指标进行性恶化。</p>
        <p class="p">（九）严重非恶性颅内肿瘤</p>
        <p class="p">指起源于脑、脑神经、脑被膜的非恶性肿瘤，ICD-O-3肿瘤形态学编码</p>
        <p class="p">
          属于0（良性肿瘤）、1（动态未定性肿瘤）范畴，并已经引起颅内压升高或神经系统功能损害，出现视乳头水肿或视觉受损、听觉受损、面部或肢体瘫痪、癫痫等，须由头颅断层扫描（CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实，且须满足下列至少一项条件：</p>
        <p class="p">1．已经实施了开颅进行的颅内肿瘤完全或部分切除手术；</p>
        <p class="p">2．已经实施了针对颅内肿瘤的放射治疗，如γ刀、质子重离子治疗等。<em>下列疾病不在保障范围内：</em></p>
        <p class="p"><em>（1）脑垂体瘤；</em></p>
        <p class="p"><em>（2）脑囊肿；</em></p>
        <p class="p"><em>（3）颅内 血管性疾病（如脑动脉瘤、脑动静脉畸形、海绵状血管瘤、毛细血管扩张症等）。</em></p>
        <p class="p">（十）严重慢性肝衰竭</p>
        <p class="p">指因慢性肝脏疾病导致肝功能衰竭。须满足下列全部条件：</p>
        <p class="p">1．持续性黄疸；</p>
        <p class="p">2．腹水；</p>
        <p class="p">3．肝性脑病；</p>
        <p class="p">4．充血性脾肿大伴脾功能亢进或食管胃底静脉曲张。</p>
        <p class="p"><em>因酗酒或药物滥用导致的肝功能衰竭不在保障范围内。</em></p>
        <p class="p">（十一）严重脑炎后遗症或严重脑膜炎后遗症</p>
        <p class="p">指因患脑炎或脑膜炎导致的神经系统永久性的功能障碍。神经系统永久性的功能障碍，指经相关专科医生确诊疾病180天后，仍遗留下列至少一种障碍：</p>
        <p class="p">1．一肢（含）以上肢体肌力2级（含）以下；</p>
        <p class="p">2．语言能力完全丧失，或严重咀嚼吞咽功能障碍；</p>
        <p class="p">3．由具有评估资格的专科医生根据临床痴呆评定量表（CDR，Clinical Dementia Rating）评估结果为3分；</p>
        <p class="p">4．自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <p class="p">（十二）深度昏迷</p>
        <p class="p">指因疾病或意外伤害导致意识丧失,对外界刺激和体内需求均无反应, 昏迷程度按照格拉斯哥昏迷分级（GCS，Glasgow Coma
          Scale）结果为5分或5分以下，且已经持续使用呼吸机及其他生命维持系统96小时以上。</p>
        <p class="p"><em>因酗酒或药物滥用导致的深度昏迷不在保障范围内。</em></p>
        <p class="p">（十三）双耳失聪<em>（保障自12周岁届满之日始）</em></p>
        <p class="p">指因疾病或意外伤害导致双耳听力永久不可逆性丧失，在500赫兹、1000赫兹和2000赫兹语音频率下，平均听阈大于等于91分贝，且经纯音听力测试、声导抗检测或听觉诱发电位检测等证实。</p>
        <p class="p">（十四）双目失明<em>（保障自12周岁届满之日始）</em></p>
        <p class="p">指因疾病或意外伤害导致双眼视力永久不可逆性丧失，双眼中较好眼须满足下列至少一项条件：</p>
        <p class="p">1．眼球缺失或摘除；</p>
        <p class="p">2．矫正视力低于0.02（采用国际标准视力表，如果使用其他视力表应进行换算）；</p>
        <p class="p">3．视野半径小于5度。</p>
        <p class="p">（十五）瘫痪</p>
        <p class="p">
          指因疾病或意外伤害导致两肢或两肢以上肢体随意运动功能永久完全丧失。肢体随意运动功能永久完全丧失，指疾病确诊180天后或意外伤害发生180天后，每肢三大关节中的两大关节仍然完全僵硬，或肢体肌力在2级（含）以下。</p>
        <p class="p">（十六）心脏瓣膜手术</p>
        <p class="p">指为治疗心脏瓣膜疾病，已经实施了切开心脏进行的心脏瓣膜置换或修复的手术。</p>
        <p class="p"><em>所有未切开心脏的心脏瓣膜介入手术不在保障范围内。</em></p>
        <p class="p">（十七）严重阿尔茨海默病<em>（保障至60周岁届满之日止）</em></p>
        <p class="p">
          指因大脑进行性、不可逆性改变导致智能严重衰退或丧失，临床表现为严重的认知能力障碍、精神行为异常和社交能力减退等，其日常生活必须持续受到他人监护。须由头颅断层扫描（CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实，并经相关专科医生确诊，
          且须满足下列至少一项条件：1.由具有评估资格的专科医生根据临床痴呆评定量表（CDR，Clinical Dementia Rating）评估结果为3分；</p>
        <p class="p">2.自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <p class="p"><em>阿尔茨海默病之外的其他类型痴呆不在保障范围内。</em></p>
        <p class="p">（十八）严重脑损伤</p>
        <p class="p">指因头部遭受机械性外力，引起脑重要部位损伤，导致神经系统永久性的功能障碍。须由头颅断层扫描（CT）、核磁共振检查（MRI）或正电子</p>
        <p class="p">发射断层扫描（PET）等影像学检查证实。神经系统永久性的功能障碍，指脑损伤180天后，仍遗留下列至少一种障碍：</p>
        <p class="p">1．一肢（含）以上肢体肌力2级（含）以下；</p>
        <p class="p">2．语言能力完全丧失，或严重咀嚼吞咽功能障碍；</p>
        <p class="p">3．自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <p class="p">（十九）严重原发性帕金森病<em>（保障至60周岁届满之日止）</em></p>
        <p class="p">是一种中枢神经系统的退行性疾病，临床表现为运动迟缓、静止性震颤或肌强直等，经相关专科医生确诊，且须满足自主生活能力完全丧失， 无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <p class="p"><em>继发性帕金森综合征、帕金森叠加综合征不在保障范围内。</em></p>
        <p class="p">（二十）严重Ⅲ度烧伤</p>
        <p class="p">指烧伤程度为Ⅲ度。且Ⅲ度烧伤的面积达到全身体表面积的20%或20% 以上。体表面积根据《中国新九分法》计算。</p>
        <p class="p">（二十一）严重特发性肺动脉高压</p>
        <p class="p">指不明原因的肺动脉压力持续性增高，进行性发展而导致的慢性疾病， 已经造成永久不可逆性的体力活动能力受限，达到美国纽约心脏病学会</p>
        <p class="p">（New York Heart Association, NYHA）心功能状态分级IV级，且静息状态下肺动脉平均压在36mmHg（含）以上。</p>
        <p class="p">（二十二）严重运动神经元病<em>（保障至60周岁届满之日止）</em></p>
        <p class="p">是一组中枢神经系统运动神经元的进行性变性疾病，包括进行性脊肌萎缩症、进行性延髓麻痹症、原发性侧索硬化症、肌萎缩性侧索硬化症，</p>
        <p class="p">经相关专科医生确诊，且须满足下列至少一项条件：</p>
        <p class="p">1．严重咀嚼吞咽功能障碍；</p>
        <p class="p">2．呼吸肌麻痹导致严重呼吸困难，且已经持续使用呼吸机7天（含）以上；</p>
        <p class="p">3．自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上的条件。</p>
        <p class="p">（二十三）<em>语言能力丧失（保障自12周岁届满之日始）</em></p>
        <p class="p">指因疾病或意外伤害导致语言能力完全丧失，经过积极治疗至少12个月（声带完全切除不受此时间限制），仍无法通过现有医疗手段恢复。</p>
        <p class="p"><em>精神心理因素所致的语言能力丧失不在保障范围内。</em></p>
        <p class="p">（二十四）重型再生障碍性贫血</p>
        <p class="p">指因骨髓造血功能慢性持续性衰竭导致的贫血、中性粒细胞减少及血小板减少，且须满足下列全部条件：</p>
        <p class="p">1．骨髓穿刺检查或骨髓活检结果支持诊断：骨髓细胞增生程度＜正常的25%；如≥正常的25%但＜50%，则残存的造血细胞应＜30%；</p>
        <p class="p">2．外周血象须具备以下三项条件中的两项：</p>
        <p class="p">（1）中性粒细胞绝对值＜0.5×109/L ；</p>
        <p class="p">（2）网织红细胞计数＜20×109/L；</p>
        <p class="p">（3）血小板绝对值＜20×109/L。</p>
        <p class="p">（二十五）主动脉手术</p>
        <p class="p">指为治疗主动脉疾病或主动脉创伤，已经实施了开胸（含胸腔镜下） 或开腹（含腹腔镜下）进行的切除、置换、修补病损主动脉血管、主动脉</p>
        <p class="p">创伤后修复的手术。主动脉指升主动脉、主动脉弓和降主动脉（含胸主动脉和腹主动脉），<em>不包括升主动脉、主动脉弓和降主动脉的分支血管。</em></p>
        <p class="p"><em>所有未实施开胸或开腹的动脉内介入治疗不在保障范围内。</em></p>
        <p class="p">（二十六）严重慢性呼吸衰竭</p>
        <p class="p">指因慢性呼吸系统疾病导致永久不可逆性的呼吸衰竭，经过积极治疗180天后满足以下所有条件：</p>
        <p class="p">1．静息时出现呼吸困难；</p>
        <p class="p">2．肺功能第一秒用力呼气容积（FEV1）占预计值的百分比＜30%；</p>
        <p class="p">3．在静息状态、呼吸空气条件下，动脉血氧分压（PaO2）＜50mmHg。</p>
        <p class="p">（二十七）严重克罗恩病</p>
        <p class="p">指一种慢性肉芽肿性肠炎，具有特征性的克罗恩病（Crohn病）病理组织学变化，须根据组织病理学特点诊断，且已经造成瘘管形成并伴有肠梗阻或肠穿孔。</p>
        <p class="p">（二十八）严重溃疡性结肠炎</p>
        <p class="p">指伴有致命性电解质紊乱的急性暴发性溃疡性结肠炎，病变已经累及全结肠，表现为严重的血便和系统性症状体征，须根据组织病理学特点诊断，且已经实施了结肠切除或回肠造瘘术。</p>
        <p class="p"><em>轻度疾病：指以下第（二十九）至（三十一）项的疾病</em></p>
        <p class="p">（二十九）恶性肿瘤——轻度</p>
        <p class="p">指恶性细胞不受控制的进行性增长和扩散，浸润和破坏周围正常组织，
          可以经血管、淋巴管和体腔扩散转移到身体其他部位，病灶经组织病理学检查（涵盖骨髓病理学检查）结果明确诊断，临床诊断属于世界卫生组织</p>
        <p class="p">（WHO，World Health
          Organization）《疾病和有关健康问题的国际统计分类》第十次修订版（ICD-10）的恶性肿瘤类别及《国际疾病分类肿瘤学专辑》第三版（ICD-O-3）的肿瘤形态学编码属于3、6、9（恶性肿瘤）范畴，
          但不在“恶性肿瘤——重度”保障范围的疾病。且特指下列六项之一：</p>
        <p class="p">（1）TNM分期为Ⅰ期的甲状腺癌；</p>
        <p class="p">（2）TNM分期为T1N0M0期的前列腺癌；</p>
        <p class="p">（3）黑色素瘤以外的未发生淋巴结和远处转移的皮肤恶性肿瘤；</p>
        <p class="p">（4）相当于Binet分期方案A期程度的慢性淋巴细胞白血病；</p>
        <p class="p">（5）相当于Ann Arbor分期方案Ⅰ期程度的何杰金氏病；</p>
        <p class="p">（6）未发生淋巴结和远处转移且WHO分级为G1级别（核分裂像＜ 10/50HPF和ki-67≤2%）的神经内分泌肿瘤。</p>
        <p class="p"><em>下列疾病不属于“恶性肿瘤——轻度”，不在保障范围内：</em></p>
        <p class="p"><em>ICD-O-3肿瘤形态学编码属于0（良性肿瘤）、1（动态未定性肿瘤）、 2（原位癌和非侵袭性癌）范畴的疾病，如：</em></p>
        <p class="p"><em>a．原位癌，癌前病变，非浸润性癌，非侵袭性癌，肿瘤细胞未侵犯基底层，上皮内瘤变，细胞不典型性增生等；</em></p>
        <p class="p"><em>b．交界性肿瘤，交界恶性肿瘤，肿瘤低度恶性潜能，潜在低度恶性肿瘤等。</em></p>
        <p class="p">（三十）较轻急性心肌梗死</p>
        <p class="p">急性心肌梗死指由于冠状动脉闭塞或梗阻引起部分心肌严重的持久性缺血造成急性心肌坏死。急性心肌梗死的诊断必须依据国际国内诊断标准，
          符合（1）检测到肌酸激酶同工酶（CK-MB）或肌钙蛋白（cTn）升高和/或降低的动态变化，至少一次达到或超过心肌梗死的临床诊断标准；（2）同时存在下列之一的证据，包括：缺血性胸痛症状、新发生的缺血性心电图改变、新生成的病理性Q波、影像学证据显示有新出现的心肌活性丧失或新出现局部室壁运动异常、冠脉造影证实存在冠状动脉血栓。</p>
        <p class="p">较轻急性心肌梗死指依照上述标准被明确诊断为急性心肌梗死，但未达到“较重急性心肌梗死”的给付标准。</p>
        <p class="p"><em>其他非冠状动脉阻塞性疾病引起的肌钙蛋白（cTn）升高不在保障范围内。</em></p>
        <p class="p">（三十一）轻度脑中风后遗症</p>
        <p class="p">
          指因脑血管的突发病变引起脑血管出血、栓塞或梗塞，须由头颅断层扫描（CT）、核磁共振检查（MRI）等影像学检查证实，并导致神经系统永久性的功能障碍，但未达到“严重脑中风后遗症”的给付标准，在疾病确诊180天后，仍遗留下列至少一种障碍：</p>
        <p class="p">（1）一肢（含）以上肢体肌力为3级；</p>
        <p class="p">（2）自主生活能力部分丧失，无法独立完成六项基本日常生活活动中的两项。</p>
        <p class="p"><em>以上28种重度疾病以及3种轻度疾病为中国保险行业协会制定并于2020年11月5日发布并施行的《重大疾病保险的疾病定义使用规范（2020 年修订版）》中列明的疾病。</em></p>
        <p class="p"><em>【续保】</em>保险合同即将期满时，被保险人向保险人提出申请，重新办理保险手续的行为。</p>
        <p class="p"><em>【无有效驾驶证】</em>被保险人存在下列情形之一者：</p>
        <p class="p">（一） 无驾驶证或驾驶证有效期已届满；</p>
        <p class="p">（二） 驾驶的机动车与驾驶证载明的准驾车型不符；</p>
        <p class="p">（三） 实习期内驾驶公共汽车、营运客车或者载有爆炸物品、易燃易爆化学物品、剧毒或者放射性等危险物品的机动车，实习期内驾驶机动车牵引挂车；</p>
        <p class="p">（四） 持未按规定审验的驾驶证，以及在暂扣、扣留、吊销、注销驾驶证期间驾驶机动车；</p>
        <p class="p">（五） 使用各种专用机械车、特种车的人员无国家有关部门核发的有效操作证，驾驶营业性客车的驾驶人无国家有关部门核发的有效资格证书；</p>
        <p class="p">（六） 依照法律法规或公安机关交通管理部门有关规定不允许驾驶机动车的其他情况下驾车</p>
        <p class="p"><em>【无有效行驶证】</em>指下列情形之一：</p>
        <p class="p">（一）机动车被依法注销登记的，或未按规定检验或检验不合格；</p>
        <p class="p">（二）无公安机关交通管理部门核发的行驶证、号牌，或临时号牌或临时移动证的机动交通工具。</p>
        <p class="p"><em>【患艾滋病或感染艾滋病病毒】</em>艾滋病指人类免疫缺陷病毒引起的获得性 免疫缺陷综合征，英文缩写为AIDS。艾滋病病毒指人类免疫缺陷病毒，英
          文缩写为HIV。在人体血液或其它样本中检测到艾滋病病毒或其抗体呈阳性， 没有出现临床症状或体征的，为感染艾滋病病毒；如果同时出现了明显临 床症状或体征的，为患艾滋病。</p>
        <p class="p"><em>【遗传性疾病】</em>指生殖细胞或受精卵的遗传物质（染色体和基因）发生突变或畸变所引起的疾病，通常具有由亲代传至后代的垂直传递的特征。</p>
        <p class="p"><em>【先天性畸形、变形或染色体异常】</em>指被保险人出生时就具有的畸形、变形或染色体异常。先天性畸形、变形和染色体异常依照世界卫生组织《疾病和有关健康问题的国际统计分类》第十次修订版（ICD-10）确定。
        </p>
        <p class="p"><em>【未满期保险费】指本合同所具有的价值，通常体现为解除合同时，根据精算原理计算的，由保险人退还的保险费金额。</em></p>
        <p class="p"><em>未满期保险费=保险费×（1-m/n），其中，m 为已生效天数，n 为保险期间的天数，经过天数不足一日的按一日计算。</em></p>
        <p class="p"><em>【保险金申请人】</em>指被保险人本人。</p>
        <p class="p"><em>【不可抗力】</em>指不能预见、不能避免并不能克服的客观情况。</p>
        <p class="p"><em>【周岁】</em>指按有效身份证件中记载的出生日期计算的年龄，自出生之日起为零周岁，每经过一年增加一岁，不足一年的不计。</p>
        <p class="p"><em>【专科医生】</em>专科医生应当同时满足以下四项资格条件：1．具有有效的中华人民共和国《医师资格证书》；2．具有有效的中华人民共和国《医师执业证书》，并按期到相关部门登记注册；3．具有有效的中华人民共和国主治医师或主治医师以上职称的《医师职称证书》；4．在国家《医院分级管理标准》二级或二级以上医院的相应科室从事临床工作三年以上。
        </p>
        <p class="p"><em>【组织病理学检查】</em>组织病理学检查是通过局部切除、钳取、穿刺等手术方法，从患者机体采取病变组织块，经过包埋、切片后，进行病理检查的方法。</p>
        <p class="p">通过采集病变部位脱落细胞、细针吸取病变部位细胞、体腔积液分离病变细胞等方式获取病变细胞，制成涂片，进行病理检查的方法，属于细胞病理学检查，不属于组织病理学检查。</p>
        <p class="p"><em>【ICD-10与ICD-0-3】</em>《疾病和有关健康问题的国际统计分类》第十次修订版（ICD-10），是世界卫生组织（WHO）发布的国际通用的疾病分类方法。</p>
        <p class="p">
          《国际疾病分类肿瘤学专辑》第三版（ICD-O-3），是WHO发布的针对ICD中肿瘤形态学组织学细胞类型、动态、分化程度的补充编码。其中形态学编码：0代表良性肿瘤；1代表动态未定性肿瘤；2代表原位癌和非侵袭性癌；
          3代表恶性肿瘤（原发性）；6代表恶性肿瘤（转移性）；9代表恶性肿瘤（原发性或转移性未肯定）。如果出现ICD-10与ICD-O-3不一致的情况，以ICD-O-3为准。</p>
