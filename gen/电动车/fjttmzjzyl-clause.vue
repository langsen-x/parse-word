        <h2 class="h2"><em>总则</em></h2>
        <p class="p"><em>第一条 </em>本附加险合同附加于各种团体健康保险合同（以下简称主险合同）。主险合同所附条款、投保单及其附件、保险单、保险凭证以及批单等，
          均为本附加险合同的组成部分。凡涉及本附加险合同的约定，均应采用书面形式。</p>
