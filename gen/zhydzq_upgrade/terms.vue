    <p class="p"><em>被保险人移植人工器官、洗牙、牙齿治疗或手术及镶补所产生的费用，但因意外伤害引起的必要的一般牙齿治疗或手术除外；</em></p>
    <p class="p"><em>被保险人先天性疾病（</em>见释义第4条<em>）和症状、遗传性疾病、先天性畸形或缺陷的治疗和康复所产生的费用；</em></p>
    <p class="p"><em>被保险人在本保险合同保险期间开始前已患上的疾病和症状，以及由该疾病引发的其他疾病（无论发生在本保险合同保险期间开始前后）；</em></p>
    <p class="p"><em>根据被保险人的主治医生或救援机构的意见，可以被合理延迟至被保险人返回其原出发地（</em>见第5条释义<em>）后进行，而被保险人坚持在当地进行治疗或手术所产生的费用；</em></p>
    <p class="p"><em>任何因当地急救组织或第三方提供服务而被保险人不需负责给付的费用或任何已包含在旅行收费中的费用；</em></p>
    <p class="p"><em>到达医院前，被保险人任何未经保险人同意擅自使用或自助选择救助服务所发生的费用；</em></p>
