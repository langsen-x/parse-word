    <p class="p">
      （四）本附加条款承担的牙科治疗费用仅限于因遭受意外伤害事故导致的牙齿伤害，经医院或保险人认可的其他医疗机构的医生诊断，必须进行的为减轻剧痛而支付的合理且必须的紧急牙科治疗费用，包括医生诊断费、手术费、药费（仅限医生处方中用于减轻疼痛的药品）。</p>
