    <p class="p">由保险人认可的机构出具的意外伤害事故证明文件；</p>
    <p class="p">若是商务旅行，需提供被保险人雇主加盖公章的被保险人商务旅行的证明；</p>
    <p class="p">其他与确认保险事故的性质、原因、损失程度等有关的证明和资料；</p>
    <p class="p"><em>（二）以上资料和证明是保险索赔的重要依据，如索赔申请人未能及时提供有关单证，导致保险人无法索赔真实性的，保险人对无法核实部分不负赔偿责任。</em></p>
    <p class="p">（三）所有本附加条款的损失计算和保险金支付在涉及外国货币时，均折合人民币计算，并以人民币赔偿。有关汇率以保险事故发生日的中国银行外汇牌价人民币汇率中间价为准。</p>
    <p class="p"><em>（四）若被保险人的损失已从其他途径或其他保险公司获得赔偿的，保险人可根据有关单位或其他保险公司出具的相关单证或给付保险金证明，在按照本附加条款赔偿保险金时，相应扣减被保险人已从其他途径或其他保险公司获得的赔偿金。</em>
    </p>
    <p class="p">（五）当赔付金额未达实际支出住院医疗费用的金额时，索赔申请人可以书面形式向保险人申请发还原始单据，保险人在加盖印章并注明已赔付金额后发还原始单据。</p>
