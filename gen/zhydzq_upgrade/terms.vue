    <p class="p"><em>间接损失，包括但不限于：(i)若非发生现金或其他财产损失，被保险人应该已经获得的收益；(ii)
      业务中断、延迟、市值损失；(iii)报告发生保险事故的费用、确定本保险合同项下应付款项的费用以及其他开支；(iv)全部或部分未付或拖欠贷款或构成向被保险人的贷款的款项；(v) 由于被保险人账户内资金不足造成的损失；(vi)
      以及任何其他第三方的损失；</em></p>
    <p class="p"><em>机械故障、电气故障、软件故障或数据错误，包括但不限于供电中断、电涌、降低电压、停电，或电信、卫星系统故障；</em></p>
