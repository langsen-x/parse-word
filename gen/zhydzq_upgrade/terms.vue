    <p class="p"><em>第八条 释义</em></p>
    <p class="p"><em>1</em>、<em>意外伤害：</em></p>
    <p class="p">指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</p>
    <p class="p"><em>2、突发性疾病</em>： </p>
    <p class="p"><em>被保险人在本附加条款规定的保险期间内，在旅行时内首次罹患的突然发生的急性疾病或出现的症状，并经医院医生诊断及证实被保险人罹患的疾病危及被保险人生命及不适宜继续原定行程，但不包括本保险合同生效前罹患的任何疾病或出现的任何症状及任何慢性疾病。</em>
    </p>
    <p class="p"><em>3、医生：</em></p>
    <p class="p">领有执业医师执照、在医疗机构内行医并拥有处方权的医生<em>（被保险人本人、其配偶或其直系亲属除外）</em>。</p>
    <p class="p"><em>4、住院：</em></p>
    <p class="p"><em>指</em>被保险人经医生根据临床诊断，必须正式办理入院手续，并入住医院之正式病房进行治疗，且连续住院二十四小时以上，<em>但不包括入住门诊观察室、家庭病床、挂床住院（指被保险人住院过程中一日内未接受与入院诊断相关的检查和治疗，或一日内住院不满二十四小时，遵医嘱到外院接受临时诊疗的除外）及不合理的住院。如被保险人因非医疗目的自行离开病房十二小时（含）以上，视为自动出院。</em>
    </p>
    <p class="p"><em>5、住院天数</em>：</p>
    <p class="p"><em>指被保险人在医院住院部病房内的住院治疗天数。住院满二十四小时为一天，但不含被保险人在住院治疗期间擅自离院期间的天数。</em></p>
    <p class="p"><em>6、公共交通工具</em>：</p>
    <p class="p">指领有当地政府主管部门依法颁发的公共交通营运执照，以收费方式合法载客的以下交通工具：</p>
    <p class="p">公共汽车、长途汽车 、渡船、气垫船、水翼船、轮船、火车、有轨电车、轨道列车（包括地铁、轻轨及磁悬浮列车）；</p>
    <p class="p">经营固定航班的航空公司经营的来往商业客运机场的定翼飞机；</p>
    <p class="p">航空公司所经营的且往来商业客运机场之间或有营运执照的直升机站之间营运的直升飞机；</p>
    <p class="p">按固定路线和时间表营运的固定机场客车。</p>
    <p class="p"><em>公共交通工具不包括出租车。</em></p>
    <p class="p"><em>若上述所列的各种公共交通工具用于非公共交通工具的目的和用途，均不符合本附加条款中“公共交通工具”的定义。另政府、企业及私人包机亦不在公共交通工具定义之内。</em></p>
    <p class="p"><em>7、疾病末期</em></p>
    <p class="p">指被保险人经医生诊断确定为严重疾病末期，并经我们认可的医生认定其所患疾病依现有医疗技术无法治愈且根据医学及临床经验其平均存活期间在六个月以下。</p>
    <p class="p"><em>8、先天性疾病：</em></p>
    <p class="p">
      指被保险人一出生就具有的疾病（症状或体征）。这些疾病是因人的遗传物质（包括染色体以及位于其中的基因）发生了对人体有害的改变而引起的，或因母亲怀孕期间受到内外环境中某些物理、化学和生物等因素的作用，使胎儿局部体细胞发育异常，导致婴儿出生时有关器官系统在结构或功能上呈现异常。</p>
    <p class="p">本附加条款未尽释义名词，以本附加条款所附属的主合同条款中的释义为准。</p>
