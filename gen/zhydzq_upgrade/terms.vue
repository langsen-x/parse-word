    <p class="p"><em>第一条 附加保险条款订立</em></p>
    <p class="p">本附加保险条款（以下简称“本附加条款”）须附加于旅行人身意外伤害保险（以下简称“主险”）条款使用。本附加条款作为主险合同之有效组成部分，与投保单、保险单及明细表、主险条款、批单等具有同等法律效力。</p>
    <p class="p"><em>第二条 保险责任</em></p>
    <p class="p">在保险期间内，被保险人持有有效证件在中华人民共和国境内或境外旅行时，因遭受<em>意外伤害</em>（见释义第1条）事故或罹患<em>突发性疾病</em>（见释义第2条），经<em>医生</em>（见释义第3条）诊断必须<em>住院</em>（见释义第4条），且符合如下条件：
    </p>
    <p class="p">如在境外旅行，<em>住院天数</em>（见释义第5条）超过七日；如在境内旅行，住院天数超过十天；</p>
    <p class="p">生活不能自理且需要成人照料。</p>
    <p class="p">如果被保险人的随行人员中有照料被保险人住院期间生活的，保险人委托的救援机构或其授权代表（以下简称“救援机构”）将安排不超过一位的随行人员在照料被保险人住院期间，在住院所在地的合理住宿<em>（限三星级及三星级以下酒店的标准间）</em>。
    </p>
    <p class="p">
      如果被保险人的随行人员中无人照料被保险人住院期间生活的，救援机构将安排其一位成年直系亲属从该直系亲属的户籍所在地或经常居住地前往被保险人入住的医院探望并照料。救援机构将根据情况合理安排其往返乘坐飞机经济舱、轮船或火车等<em>公共交通工具</em>（见释义第6条），并安排合理住宿<em>（限三星级及三星级以下酒店的标准间）</em>。
    </p>
    <p class="p">保险人根据救援服务机构的安排，负责承担相应的往返经济舱机票、轮船船票或火车票的费用和住宿费用，但以保险单或保险凭证中约定的保险金额为限。</p>
    <p class="p"><em>前述“住宿费用”<span style="color: #666666"> </span>不包括食物、饮料、通讯及其他服务费用。</em></p>
    <p class="p"><em>第三条 责任免除</em></p>
    <p class="p"><em>因下列情形之一，直接或间接导致保险事故发生的，保险人不承担赔偿责任：</em></p>
    <p class="p"><em>（一）被保险人康复性治疗、物理治疗、安胎及分娩（包括剖腹产、流产及引产）等； </em></p>
    <p class="p"><em>（二）慢性病或旅行前已罹患疾病的治疗；</em></p>
    <p class="p"><em>（三）流行疫病或大规模流行疫病导致的治疗或预防发生的医疗；</em></p>
    <p class="p"><em>（四）脊椎间盘突出症或错位的治疗；</em></p>
    <p class="p"><em>（五）避孕或绝育手术发生的治疗；</em></p>
    <p class="p"><em>（六）药物过敏发生的治疗；</em></p>
    <p class="p"><em>（七）扁桃腺、腺样体、疝气、女性生殖器官疾病的治疗或上述疾病导致的手术产生的治疗；但为避免生命危险或健康永久性损伤而导致被保险人需立即接受的紧急治疗或手术的除外；</em></p>
    <p class="p"><em>（八）此次旅行之前已被有资质的职业医师诊断出为疾病末期</em>（见释义第7条）<em>，</em></p>
    <p class="p"><em>（九）被保险人美容、整形、矫形术、非必须紧急性治疗的手术、心理咨询及和角膜屈光成形手术；</em></p>
    <p class="p"><em>（十）被保险人健康护理(含体检、疗养、特别护理或静养) 等非治疗性的行为及无客观病征证明其不健康及以捐献身体器官为目的的医疗行为；</em></p>
    <p class="p"><em>（十一）被保险人移植人工器官、洗牙、验光、牙齿治疗或手术及镶补，但因意外伤害引起的必要的一般牙齿治疗或手术除外；</em></p>
    <p class="p"><em>（十二）被保险人先天性疾病</em>（见释义第8条）<em>和症状、遗传性疾病、先天性畸形或缺陷的治疗和康复；</em></p>
    <p class="p"><em>（十三）被保险人在本保险合同保险期间开始前已患上的疾病和症状，以及由该疾病引发的其他疾病（无论发生在本保险合同保险期间开始前后）；</em></p>
    <p class="p"><em>（十四）根据被保险人的主治医生或救援机构授权医生的意见，可以被合理延迟至被保险人返回原出发地后进行而被保险人坚持在当地进行的治疗或手术；</em></p>
    <p class="p"><em>（十五）无当地医院出具原始收据的费用或医疗证明；</em></p>
    <p class="p"><em>（十六）被保险人拒绝听从救援机构提出的建议；</em></p>
    <p class="p"><em>（十七）主险条款规定的责任免除事项。</em></p>
    <p class="p"><em>第四条 保险金额</em></p>
    <p class="p"><em>保险金额是保险人对保险单中载明的被保险人在保险期间内旅行，因发生本附加条款责任范围内的事件而应承担的给付该项保险金责任的最高限额。保险金额由投保人、保险人双方约定，并在保险单或保险凭证中载明。保险人和投保人可以在本保险合同项下约定免赔额等限制条件。</em>
    </p>
    <p class="p"><em>第五条 保险期间</em></p>
    <p class="p">除非另有约定，本附加条款的保险期间与主险合同的保险期间一致。</p>
    <p class="p"><em>第六条 保险金申请</em></p>
    <p class="p">（一）被保险人作为保险合同索赔申请人向保险人申请索赔，应填写保险金索赔申请书，并提供下列证明文件、资料：</p>
    <p class="p">1、保险单和保险凭证正本；</p>
    <p class="p">2、被保险人户籍证明或身份证明；</p>
    <p class="p">3、被保险人与探望人的关系证明文件原件或复印件；</p>
    <p class="p">4、探望人的住宿费用及公共交通工具费用的清单，发票原件及登机牌原件。</p>
    <p class="p">5、医院出具的附有病理检查、代验检查及其他医疗仪器检查报告的医疗诊断证明、病历及医疗、医药费原始单据、出院小结原件；</p>
    <p class="p">6、保险人认可的意外事故证明文件；</p>
    <p class="p">7、若是商务旅行，需被保险人的雇主提供的加盖公章的被保险人商务旅行的证明；</p>
    <p class="p">8、其他与确认保险事故的性质、原因、损失程度等有关的证明和资料。</p>
    <p class="p"><em>（二）以上资料和证明是保险索赔的重要依据，如索赔申请人未能及时提供有关单证，导致保险人无法核实索赔真实性的，保险人对无法核实部分不负赔偿责任。</em></p>
    <p class="p">（三）所有本附加条款的损失计算和保险金支付在涉及外国货币时，均折合人民币计算，并以人民币赔偿。有关汇率以探望人从其户籍所在地或经常居住地出发日的中国银行外汇牌价人民币汇率中间价为准。</p>
    <p class="p"><em>（四）若被保险人的损失已从其他途径或其他保险公司获得赔偿的，保险人可根据有关单位或其他保险公司出具的相关单证或给付保险金证明，在按照本附加条款赔偿保险金时，有权相应扣减被保险人已从其他途径或其他保险公司获得的赔偿金。</em>
    </p>
