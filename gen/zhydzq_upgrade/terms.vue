    <p class="p"><em>被保险人在任何宾馆或汽车旅馆结账离开时，遗忘于该酒店或汽车旅馆的随身行李或贵重物品遗失、失窃 或损坏；或者遗忘于任何空中交通工具、船只、列车、出租车或公共汽车中的物品遗失、失窃或损坏；</em>
    </p>
    <p class="p"><em>走私、非法的运输或贸易；</em></p>
    <p class="p"><em>经承运人、酒店或任何其他责任方修理后能正常运行或恢复其正常功能的物品；</em></p>
    <p class="p"><em>自被保险人知道或应当知道保险事故发生之时起二十四小时内被保险人未向保险事故发生地海关、警方或其他有关部门报案并领取其出具的保险事故证明；</em></p>
    <p class="p"><em>可以从公共交通工具承运人、旅行社、酒店或依据其他保险单项下获得赔偿的损失；</em></p>
    <p class="p"><em>因贬值导致的损失；</em></p>
    <p class="p"><em>被保险人原出发地</em>（见释义第4条）<em>发生的物品遗失，失窃或损坏；</em></p>
    <p class="p"><em>非于该次旅行时托运的行李、邮寄或船运的纪念品或物品；</em></p>
    <p class="p"><em>主险条款规定的其它责任免除事项。</em></p>
    <p class="p"><em> 保险金额</em></p>
    <p class="p"><em>保险金额是保险人对保险单或保险凭证中载明的被保险人在保险期间内旅行，因发生本附加条款责任范围内的事件而应承担的给付该项保险金责任的最高限额。保险金额由投保人、保险人双方约定，并在保险单中载明。保险人和投保人可以在本保险合同项下约定免赔额、单件物品限额等限制条件。</em>
    </p>
    <p class="p"><em>保险期间</em></p>
    <p class="p">除非另有约定，本附加条款的保险期间与主险合同的保险期间一致。</p>
    <p class="p"><em>被保险人义务</em></p>
    <p class="p"><em>（一）被保险人应妥善照管其行李物品。</em></p>
    <p class="p">
      <em>（二）如本附加条款项下承保的行李及随身物品发生遗失或损坏，被保险人必须立即采取措施查寻、保护或挽救该行李或物品。由于被保险人未履行上述义务而导致损失扩大的，保险人对扩大的部分不承担赔偿责任；</em></p>
    <p class="p"><em>（三）被保险人须于知道或应当知道保险事故发生之时起二十四小时内向保险事故发生地海关、警方或其他有关部门报案并领取其出具的保险事故证明。</em></p>
    <p class="p"><em>（四）如被保险人的行李及物品在公共交通工具、酒店或旅行社遗失或损坏的，被保险人需提供对方为其出具的保险事故证明。</em></p>
    <p class="p"><em>保险金申请</em></p>
    <p class="p">（一）由被保险人作为本保险合同下索赔申请人，向保险人申请索赔时应填写索赔申请书，并提供下列证明文件、资料：</p>
    <p class="p">保险单和保险凭证正本；</p>
    <p class="p">被保险人行李及随身物品损失清单及其发票原件； </p>
    <p class="p">被保险人户籍证明或身份证明；</p>
    <p class="p">保险事故发生地海关或警方或其他有关部门出具的保险事故证明文件正本；</p>
    <p class="p">如被保险人的个人行李及随身物品在酒店、公共交通工具或旅行社安排的交通工具内损失的，该酒店、公共交通工具承运人或旅行社出具的保险事故证明文件正本，包括保险事故日期、经过及损失情况；</p>
    <p class="p">修理、修复的发票原件；</p>
    <p class="p">若是商务旅行，需被保险人的雇主提供的加盖公章的被保险人商务旅行的证明；</p>
    <p class="p">其他与确认保险事故的性质、原因、损失程度等有关的证明和资料。</p>
    <p class="p"><em>（二）以上资料和证明是保险索赔的重要依据，如索赔申请人未能及时提供有关单证，导致保险人无法核实索赔申请的真实性的，保险人对无法核实部分不负赔偿责任。</em></p>
    <p class="p">（三）所有本附加条款的损失计算和保险金支付在涉及外国货币时，均折合人民币计算，并以人民币赔偿。有关汇率以保险事故发生日的中国银行外汇牌价人民币汇率中间价为准。</p>
    <p class="p"><em>（四）若被保险人的损失已从公共交通工具承运人、酒店、旅行社、其他途径或其他保险公司获得赔偿的，保险人可根据有关单位或其他保险公司出具的相关单证或给付保险金证明，在按照本附加条款赔偿保险金时，相应扣减被保险人已从公共交通工具承运人、酒店、旅行社、其他途径或其他保险公司获得的赔偿金。如果在保险人按照本附加条款赔偿保险金后，遗失、被盗窃或被抢劫的物件被发现或归还，被保险人应立即通知保险人并于该物件被发现或归还之日起30日内向保险人退回已领取的保险金。</em>
    </p>
    <p class="p"><em>(五)保险人依据本附件条款最终赔付的金额应扣除保险人在同一保险事故下已赔偿的行李延误保险金。</em></p>
    <p class="p"><em>代位求偿</em></p>
    <p class="p">因第三者对保险标的的损害而造成保险事故的，保险人自向被保险人赔偿保险金之日起，在赔偿金额范围内代位行使被保险人对第三者请求赔偿的权利，被保险人应当向保险人提供必要的文件和所知道的有关情况。</p>
    <p class="p"><em>被保险人已经从第三者取得赔偿的，保险人赔偿保险金时，可以相应扣减被保险人已从第三者取得的赔偿金额。</em></p>
    <p class="p"><em>保险事故发生后，在保险人未赔偿保险金之前，被保险人放弃对第三者请求赔偿权利的，保险人不承担赔偿责任；保险人向被保险人赔偿保险金后，被保险人未经保险人同意放弃对第三者请求赔偿权利的，该行为无效；由于被保险人故意或者因重大过失致使保险人不能行使代位请求赔偿的权利的，保险人可以扣减或者要求返还相应的保险金。</em>
    </p>
    <p class="p"><em>附加条款的效力</em></p>
    <p class="p">本附加条款所附属的主险合同效力终止，本附加条款效力随之终止。主险合同无效，本附加条款亦无效。</p>
