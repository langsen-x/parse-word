    <p class="p"><em>释义</em></p>
    <p class="p"><em>意外伤害：</em></p>
    <p class="p">指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</p>
    <p class="p"><em>医院</em>：</p>
    <p class="p">在中华人民共和国境内（不包括香港特别行政区、澳门特别行政区、台湾地区）的医院是指经中华人民共和国卫生部门评审确定的二级或以上的公立医院或投保人与保险人协商共同指定的其他医院或医疗机构。</p>
    <p class="p">在中国境外（包括香港特别行政区、澳门特别行政区、台湾地区）的医院是指保险人认可的，根据所在国家法律合法成立、运营并符合以下标准的医疗机构：</p>
    <p class="p">主要运营目的是以住院形式提供接待病人并为其提供医疗护理和治疗；</p>
    <p class="p">在一名或若干名医生的指导下为病人治疗，其中至少有一名合法执业资格的驻院医生驻诊；</p>
    <p class="p">维持足够妥善的设备为病人提供医学诊断和治疗，并于机构内或由其管理的其他场所提供进行各种手术的设备；</p>
    <p class="p">有由合法执业的护士提供或指导的二十四小时全职护理服务。</p>
    <p class="p"><em>本附加条款中所指医院不包括以下或类似的医疗机构：</em></p>
    <p class="p"><em>精神病院；</em></p>
    <p class="p"><em>老人院、疗养院、戒毒中心和戒酒中心；</em></p>
    <p class="p"><em>健康中心或天然治疗所、疗养或康复院。</em></p>
    <p class="p"><em>先天性疾病：</em></p>
    <p class="p">
      指被保险人一出生就具有的疾病（症状或体征）。这些疾病是因人的遗传物质（包括染色体以及位于其中的基因）发生了对人体有害的改变而引起的，或因母亲怀孕期间受到内外环境中某些物理、化学和生物等因素的作用，使胎儿局部体细胞发育异常，导致婴儿出生时有关器官系统在结构或功能上呈现异常。</p>
    <p class="p"><em>原出发地：</em></p>
    <p class="p">
      若被保险人进行旅行的目的地为中华人民共和国境内(不包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指被保险人在中华人民共和国境内的户籍所在地或经常居住地；若被保险人进行的旅行目的地为中华人民共和国境外(包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指中华人民共和国境内。</p>
    <p class="p"><em>担保：</em></p>
    <p class="p">指对于住院期间发生的医疗费用或押金，在保险人授权的条件下，救援机构在保险金额内负责为被保险人住院期间医疗费用进行担保，出具担保函，而无需被保险人自付。</p>
