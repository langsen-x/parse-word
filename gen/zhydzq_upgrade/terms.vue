    <p class="p"><em>第一条 附加保险条款订立</em></p>
    <p class="p"><em>本附加保险条款（以下简称“本附加条款”）须附加于旅行人身意外伤害保险（以下简称“主险”）条款使用。</em>本附加条款作为主险合同之有效组成部分，与投保单、保险单及明细表、主险条款、批单等具有同等法律效力。
    </p>
    <p class="p"><em>第二条 保险责任</em></p>
    <p class="p">在保险期间内，被保险人持有有效证件在中华人民共和国境内或境外旅行期间，因从事<em>高风险运动</em>（见第1条释义）遭受<em>意外伤害</em>（见第2条释义）事故导致身故或残疾的，保险人依照下列约定给付保险金。
    </p>
    <p class="p"><em>（一）身故保险责任</em></p>
    <p class="p"><em>在保险期间内，被保险人持有有效证件旅行时从事高风险运动遭受意外伤害事故，并自事故发生之日起180日内以该意外事故为直接原因导致身故的，</em>保险人按附加高风险运动保险金额给付身故保险金，<em>并于给付保险金后终止对该被保险人的保险责任。</em>
    </p>
    <p class="p">
      在保险期间内，被保险人持有有效证件旅行时从事高风险运动遭受意外伤害事故，且自该事故发生日起下落不明，后经人民法院宣告死亡的，保险人按附加高风险运动保险金额给付身故保险金。但若被保险人被宣告死亡后生还的，保险金受领人应于知道或应当知道被保险人生还之日起30日内退还保险人给付的身故保险金。</p>
    <p class="p"><em>被保险人身故前保险人已按本条第二款约定给付残疾保险金的，保险人在给付身故保险金时应扣除已给付的保险金。</em></p>
    <p class="p"><em>（二）残疾保险责任</em></p>
    <p class="p"><em>在保险期间内，被保险人持有有效证件旅行时从事高风险运动遭受意外伤害事故，并自该事故发生之日起180日内以该意外事故为直接原因造成</em>《人身保险伤残评定标准及代码》（由中国保险监督管理委员会发布，保监发[2014]6号，标准编号为JR/T0083-2013，简称《评定标准》）所列伤残程度之一的，<em>保险人按《评定标准》所列保险金给付比例乘以附加高风险运动保险金额给付残疾保险金。如自该意外事故发生之日起第180日治疗仍未结束的，按当日的身体情况进行伤残鉴定，并据此给付残疾保险金。</em>
    </p>
    <p class="p"><em>被保险人如在本次意外伤害事故之前已有残疾，保险人按合并后的伤残程度在《评定标准》中所对应的保险金给付比例给付残疾保险金，但应扣除原有伤残（被保险人在本保险合同生效前已有伤残，或在本保险合同保险期间内非保险事故及责任免除事项所致《评定标准》所列伤残，以及保险人已给付了残疾保险金的均视为原有伤残）程度在《评定标准》所对应的残疾保险金。</em>
    </p>
    <p class="p"><em>（三）保险人对每一被保险人的保险金给付总额以保险单载明的该被保险人的附加高风险运动保险金额为限。一次或累计给付同一被保险人的保险金达到该被保险人的附加高风险运动保险金额时，保险人对该被保险人的上述各项保险责任终止。</em>
    </p>
    <p class="p"><em>第三条 原因除外</em></p>
    <p class="p"><em>因下列原因造成被保险人身故或残疾的，保险人不承担给付保险金责任：</em></p>
    <p class="p"><em>（一）投保人的故意行为；</em></p>
    <p class="p"><em>（二）被保险人自致伤害或自杀，但被保险人自杀时为无民事行为能力人的除外；</em></p>
    <p class="p"><em>（三）被保险人实施或企图实施违法行为、犯罪行为或拒捕；</em></p>
    <p class="p"><em>（四）因被保险人挑衅或故意行为而导致的打斗、殴斗、被袭击或被谋杀；</em></p>
    <p class="p"><em>（五）被保险人因精神错乱或失常而导致的意外；</em></p>
    <p class="p"><em>（六）任何形式的恐怖行动或企图发生的恐怖行动；</em></p>
    <p class="p"><em>（七）任何生物、化学、原子能武器，原子能或核能装置所造成的爆炸、灼伤、污染或辐射；</em></p>
    <p class="p"><em>（八）直接或间接由流行疫病或大规模流行疫病爆发引起；</em></p>
    <p class="p"><em>（九）被保险人妊娠、流产、分娩、疾病、药物过敏；</em></p>
    <p class="p"><em>（十）被保险人接受医疗检查、麻醉、整容手术及其他内、外科手术；</em></p>
    <p class="p"><em>（十一）被保险人未遵医嘱，私自服用、涂用、注射药物；</em></p>
    <p class="p"><em>（十二）被保险人受细菌、病毒或寄生虫感染（但因受伤以致伤口脓肿者除外）；或被保险人食物中毒；或被保险人猝死；</em></p>
    <p class="p"><em>（十三）保险期间开始前已存在的受伤及其并发症； </em></p>
    <p class="p"><em>（十四）使用人工空气呼吸器潜水、狩猎、跳伞、滑翔、探险活动、武术比赛、摔跤比赛、特技表演、非徒步的速度竞赛、赛马、马球、马术表演、赛车、拳击、室外滑雪、室外滑冰等运动或活动（但有正式经营执照的机构经营的并符合安全规范的潜水、骑马、场地滑雪、场地滑冰、滑水、漂流项目、驾驶卡丁车，帆船，帆板，皮划艇，漂流，观景直升机、不在此列）； </em>
    </p>
    <p class="p"><em>（十五）被保险人参与任何职业、半职业或设有奖金、报酬的体育活动；</em></p>
    <p class="p"><em>（十六）被保险人参与执行军警任务或以执法身份执行任务； </em></p>
    <p class="p"><em>（十七）被保险人受雇于商业船舶并执行职务；于海军、空军服军役；职业性操作或测试任何种类交通工具；从事石油或化工业、森林砍伐业、建筑工程业、运输业、采掘业、采矿业、空中摄影、处理爆炸物、水上作业、高空作业等职业活动（任何体力劳动或与操作机器有关的工作）；</em>
    </p>
    <p class="p"><em>（十八）非法搭乘交通工具或搭乘未经保险事故发生地相关政府部门登记许可的交通工具；</em></p>
    <p class="p"><em>（十九）航空或飞行活动，包括身为飞行驾驶员或空勤人员，但以缴费乘客身份乘坐客运民航班机的除外；</em></p>
    <p class="p"><em>（二十）被保险人以接受医生治疗或疗养为目的而进行旅行；被保险人违反医生的嘱咐而旅行或当被保险人在其身体条件不适宜于旅行时进行旅行；</em></p>
    <p class="p"><em>（二十一）被保险人身体状况尚适宜旅行情况下未遵循主治医生建议立即返回中国境内或被保险人日常居住地或日常工作地做进一步治疗而导致病情恶化。</em></p>
    <p class="p"><em>第四条 期间除外</em></p>
    <p class="p"><em>被保险人在下列期间遭受伤害导致身故或残疾的，保险人不承担给付保险金责任：</em></p>
    <p class="p"><em>（一）战争（无论宣战与否）、军事行动、暴动或武装叛乱期间；</em></p>
    <p class="p"><em>（二）被保险人醉酒或受毒品、管制药物的影响期间；</em></p>
    <p class="p"><em>（三）被保险人酒后驾车、无有效驾驶证驾驶或驾驶无有效行驶证的机动车期间；</em></p>
    <p class="p"><em>（四）被保险人因受当地司法当局拘禁或被判入狱期间。</em></p>
    <p class="p"><em>第五条 地区除外</em></p>
    <p class="p"><em>对于在下列国家和地区发生的保险事故，保险人不承担保险责任：</em></p>
    <p class="p"><em>阿富汗，布隆迪，中非共和国，乍得共和国，刚果（民主共和国），东帝汶（民主共和国），厄立特里亚，几内亚，海地，伊拉克，科特迪瓦共和国，利比里亚，所罗门群岛，索马里，苏丹、南极洲、布韦岛、赫德岛和麦克唐纳群岛、南乔治亚岛和南桑威奇群岛及其他救援公司无法提供服务的正在遭受联合国、欧盟、美国经济或贸易制裁的国家或地区。</em>
    </p>
    <p class="p"><em>若由于本保险合同中责任免除的情形导致的被保险人死亡，保险人将退还本附加条款的保单现金价值。</em></p>
    <p class="p"><em>第六条 保险金额</em></p>
    <p class="p"><em>附加高风险运动保险金额是保险人对被保险人在保险单中载明的保险期间内旅行发生本附加条款第二条所述的保险责任事故，保险人应当承担给付该项保险金责任的最高限额。附加高风险运动</em>保险金额由投保人、保险人双方约定，并在保险单或批单中载明。<em>保险人和投保人可以在本附加条款项下约定免赔额及赔付比例等限制条件。</em>
    </p>
    <p class="p"><em>第七条 保险期间</em></p>
    <p class="p"><em>除非另有约定，本附加条款的保险期间与主</em>险合同约定的保险期间一致。</p>
    <p class="p"><em>第八条 保险金申请</em></p>
    <p class="p"><em>保险金申请人向保险人申请给付保险金时，应提交以下材料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。保险金申请人未能提供有关材料，导致保险人无法确定保险事故的性质、原因、损失程度等的，保险人对无法确定的部分不承担给付保险金的责任。</em>
    </p>
    <p class="p"><em>（一）身故保险金申请</em></p>
    <p class="p">1、保险金申请书；</p>
    <p class="p">2、保险单或其他保险凭证原件；</p>
    <p class="p">3、保险金申请人的身份证明；</p>
    <p class="p">
      4、公安部门出具的被保险人户籍注销证明、二级以上（含二级）或保险人认可的医疗机构出具的被保险人身故证明书。若被保险人为宣告死亡，保险金申请人应提供人民法院出具的宣告死亡证明文件。如被保险人在境外身故的，需要提供中华人民共和国驻保险事故发生地所在国使、领馆或保险事故发生地政府有关机构出具的被保险人死亡证明或验尸报告；</p>
    <p class="p">5、保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；</p>
    <p class="p">6、若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件；</p>
    <p class="p">7、若被保险人投保时未指定身故受益人，保险金申请人应提供法定继承人公证书；</p>
    <p class="p">8、若是商务旅行，需被保险人的雇主单位提供的加盖雇主单位公章的被保险人商务旅行的证明。</p>
    <p class="p"><em>（二）残疾保险金申请</em></p>
    <p class="p">1、保险金申请书；</p>
    <p class="p">2、保险单或其他保险凭证原件；</p>
    <p class="p">3、被保险人身份证明；</p>
    <p class="p">4、二级以上（含二级）或保险人认可的医疗机构或司法鉴定机构出具的残疾鉴定诊断书；</p>
    <p class="p">5、保险金申请人所能提供的其他与本项申请相关的材料；</p>
    <p class="p">6、若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件；</p>
    <p class="p">7、若是商务旅行，需被保险人的雇主单位提供的加盖雇主单位公章的被保险人商务旅行的证明。</p>
    <p class="p"><em>第九条 附加条款的效力</em></p>
    <p class="p">本附加条款所附属的主险合同效力终止，本附加条款效力随之终止。主险合同无效，本附加条款亦无效。</p>
    <p class="p"><em>第十条 释义</em></p>
    <p class="p"><em>1、高风险运动：</em></p>
    <p class="p"><em>指比一般常规性的运动风险等级更高、更容易发生人身伤害的运动，在进行此类运动前需有充分的心理准备和行动上的准备，必须具备一般人不具备的相关知识和技能或者必须在接受专业人士提供的培训或训练之后方能掌握。被保险人进行此类运动时须具备相关防护措施或设施，以避免发生损失或减轻损失，包括有正式经营执照的机构经营的并符合安全规范的潜水，滑水，场地滑雪，场地滑冰，驾驶卡丁车，</em>帆船，帆板，皮划艇，漂流，观景直升机，骑马。
    </p>
    <p class="p"><em>2、意外伤害：</em></p>
    <p class="p">指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</p>
    <p class="p">本附加条款的未释义名词，以本附加条款所附属的主险条款中的释义为准。</p>
    <p class="p"><em>第十一条 条款的适用</em></p>
    <p class="p">本附加条款与主险条款约定不一致之处，以本附加条款为准；本附加条款未尽之处，以主险条款约定为准。</p>
