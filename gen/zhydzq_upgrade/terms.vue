    <p class="p"><em>第三方：</em></p>
    <p class="p">指除本保险合同项下当事人双方以外的具有民事行为能力的自然人或法人或其他组织。</p>
    <p class="p"><em>个人行李：</em></p>
    <p class="p">指被保险人在旅行中为了穿着、使用或者便利而携带的必要及适量的物品和其他个人财物。</p>
    <p class="p"><em>重新购置价：</em></p>
    <p class="p">指个人行李及随身物品遗失、遭受损失或损毁时出发地的市场价格，但须扣除损耗及折旧费用。</p>
    <p class="p"><em>原出发地：</em></p>
    <p class="p">
      若被保险人进行旅行的目的地为中华人民共和国境内(不包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指被保险人在中华人民共和国境内的户籍所在地或经常居住地；若被保险人进行的旅行目的地为中华人民共和国境外(包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指中华人民共和国境内。</p>
    <p class="p">本附加条款的未释义名词，以本附加条款所附属的主险合同条款中的释义为准。</p>
