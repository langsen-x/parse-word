    <p class="p"><em>高风险运动：</em></p>
    <p class="p"><em>指比一般常规性的运动风险等级更高、更容易发生人身伤害的运动，在进行此类运动前需有充分的心理准备和行动上的准备，必须具备一般人不具备的相关知识和技能或者必须在接受专业人士提供的培训或训练之后方能掌握。被保险人进行此类运动时须具备相关防护措施或设施，以避免发生损失或减轻损失，包括有正式经营执照的机构经营的并符合安全规范的潜水，滑水，场地滑雪，场地滑冰，驾驶卡丁车，</em>帆船，帆板，皮划艇，漂流，观景直升机，骑马。
    </p>
    <p class="p"><em>意外伤害：</em></p>
    <p class="p">指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</p>
