    <p class="p"><em>健康中心或天然治疗所、疗养或康复院。</em></p>
    <p class="p"><em>医生</em></p>
    <p class="p">指领有执业医师执照、在医疗机构内行医并拥有处方权的医生（被保险人本人、其配偶或其直系亲属除外）。</p>
    <p class="p"><em>住院：</em></p>
    <p class="p"><em>指被保险人经医生根据临床诊断，必须正式办理入院手续，入住医院之正式病房进行治疗，且连续住院二十四小时以上，但不包括入住门诊观察室、家庭病床、其他挂床住院（指被保险人住院过程中一日内未接受与入院诊断相关的检查和治疗，或一日内住院不满二十四小时，遵医嘱到外院接受临时诊疗的除外）及不合理的住院。如被保险人因非医疗目的自行离开病房十二小时（含）以上，视为自动出院。</em>
    </p>
    <p class="p"><em>住院天数</em>：</p>
    <p class="p"><em>指被保险人在医院住院部病房内的住院治疗天数。住院满二十四小时为一天，但不含被保险人在住院治疗期间擅自离院期间的天数。</em></p>
    <p class="p">本附加条款的未释义名词，以本附加条款所附属的主险合同条款中的释义为准。</p>
