    <p class="p"><em>第一条 附加保险条款订立</em></p>
    <p class="p">本附加保险条款（以下简称“本附加条款”）须附加于旅行人身意外伤害保险（以下简称“主险”）条款使用。本附加条款作为主险合同之有效组成部分，与投保单、保险单及明细表、主险条款、批单等具有同等法律效力。</p>
    <p class="p"><em>第二条 保险责任</em></p>
    <p class="p">在保险期间内，被保险人持有有效证件在中华人民共和国境内或境外旅行期间，持有效客票以乘客身份乘坐<em>公共交通工具</em>（见释义第1条）时，在该交通工具内因交通事故导致身故或残疾的，保险人依照下列约定给付保险金。
    </p>
    <p class="p"><em>（一）身故保险责任</em></p>
    <p class="p"><em>在保险期间内，被保险人持有有效证件在境外或境内旅行时，持有效客票以乘客身份乘坐公共交通工具在交通工具内部时发生交通事故，并自事故发生之日起180日内以该意外事故为直接原因导致身故的，保险人按附加公共交通工具意外伤害保险金额给付身故保险金，并于给付保险金后终止对该被保险人的保险责任。</em>
    </p>
    <p class="p">
      在保险期间内，被保险人持有有效证件在境外或境内旅行时，持有效客票以乘客身份乘坐公共交通工具在交通工具内部时发生交通事故，且自该事故发生日起下落不明，后经人民法院宣告死亡的，保险人按附加公共交通工具意外伤害保险金额给付身故保险金。但若被保险人被宣告死亡后生还的，保险金受领人应于知道或应当知道被保险人生还之日起30日内退还保险人给付的身故保险金。</p>
    <p class="p"><em>被保险人身故前保险人已按本条第二款约定给付残疾保险金的，保险人在给付身故保险金时应扣除已给付的保险金。</em></p>
    <p class="p"><em>（二）残疾保险责任</em></p>
    <p class="p"><em>在保险期间内，被保险人持有有效证件在境外或境内旅行时，持有效客票以乘客身份乘坐公共交通工具在交通工具内部时发生交通事故，并自该事故发生之日起180日内以该意外事故为直接原因造成</em>《人身保险伤残评定标准及代码》（由中国保险监督管理委员会发布，保监发[2014]6号，标准编号为JR/T0083-2013，简称《评定标准》）所列伤残程度之一的，<em>保险人按《评定标准》所列保险金给付比例乘以附加公共交通工具意外伤害保险金额给付残疾保险金。如自该意外事故发生之日起第180日治疗仍未结束的，按当日的身体情况进行伤残鉴定，并据此给付残疾保险金。</em>
    </p>
    <p class="p"><em>被保险人如在本次意外伤害事故之前已有残疾，保险人按合并后的伤残程度在《评定标准》中所对应的保险金给付比例给付残疾保险金，但应扣除原有伤残（被保险人在本保险合同生效前已有伤残，或在本保险合同保险期间内非保险事故及责任免除事项所致《评定标准》所列伤残，以及保险人已给付了残疾保险金的均视为原有伤残）程度在《评定标准》所对应的残疾保险金。</em>
    </p>
    <p class="p"><em>（三）保险人对每一被保险人的保险金给付总额以保险单载明的该被保险人的附加公共交通工具意外伤害保险金额为限。一次或累计给付同一被保险人的保险金达到该被保险人的附加公共交通工具意外伤害保险金额时，保险人对该被保险人的上述各项保险责任终止。</em>
    </p>
    <p class="p"><em>第三条 原因除外</em></p>
    <p class="p"><em>因下列原因中的一种或数种直接和间接导致被保险人身故或残疾的，保险人不承担给付保险金的责任：</em></p>
    <p class="p"><em>(一)投保人的故意行为； </em></p>
    <p class="p"><em>(二)被保险人违反承运人关于安全乘坐公共交通工具的规定；</em></p>
    <p class="p"><em>(三)被保险人故意犯罪或拒捕、自杀、故意自伤； </em></p>
    <p class="p"><em>(四)被保险人斗殴、酗酒</em>（见释义第2条）<em>，服用、吸食或注射毒品； </em></p>
    <p class="p"><em>(五)被保险人乘坐的公共交通工具驾驶员酒后驾驶、无合法有效驾驶证驾驶</em>（见释义第3条），<em>或驾驶无有效行驶证</em>（见释义第4条）<em>的机动交通工具； </em></p>
    <p class="p"><em>(六)被保险人因精神疾病而导致的意外； </em></p>
    <p class="p"><em>(七)被保险人因受国家管制药物的影响而导致的意外或未遵医嘱，私自服用、涂用、注射药物； </em></p>
    <p class="p"><em>(八)被保险人患艾滋病</em>（见释义第5条）<em>或感染艾滋病病毒</em>（见释义第6条）<em>期间发生的意外伤害；</em></p>
    <p class="p"><em>(九)交通工具自始发地出发以后，未到达目的地之前，被保险人部分或全部身体在公共交通工具之外，如汽车和火车的车厢外部，轮船的甲板之外等，飞机的舱门之外所遭受的意外伤害；</em></p>
    <p class="p"><em>(十)战争、军事行动、暴乱或武装叛乱； </em></p>
    <p class="p"><em>(十一)核爆炸、核辐射或核污染。 </em></p>
    <p class="p"><em>第四条 期间除外</em></p>
    <p class="p"><em>被保险人在下列期间遭受公共交通工具意外伤害导致身故或残疾的，保险人不承担给付保险金责任：</em></p>
    <p class="p"><em>（一）战争（无论宣战与否）、军事行动、暴动或武装叛乱期间；</em></p>
    <p class="p"><em>（二）被保险人醉酒或受毒品、管制药品</em>（见释义第7条）<em>的影响期间；</em></p>
    <p class="p"><em>（三）被保险人乘坐的公共交通工具驾驶员酒后驾车、无合法有效驾驶证驾驶或驾驶无有效行驶证的机动车期间；</em></p>
    <p class="p"><em>（四）被保险人因受所在地当地司法当局拘禁或被判入狱期间。</em></p>
    <p class="p"><em>第五条 地区除外</em></p>
    <p class="p"><em>对于在下列国家和地区发生的保险事故，保险人不承担保险责任：</em></p>
    <p class="p"><em>阿富汗，布隆迪，中非共和国，乍得共和国，刚果（民主共和国），东帝汶（民主共和国），厄立特里亚，几内亚，海地，伊拉克，科特迪瓦共和国，利比里亚，所罗门群岛，索马里，苏丹、南极洲、布韦岛、赫德岛和麦克唐纳群岛、南乔治亚岛和南桑威奇群岛及其他救援公司无法提供服务的正在遭受联合国、欧盟、美国经济或贸易制裁的国家或地区。</em>
    </p>
    <p class="p"><em>若由于本保险合同中责任免除的情形导致的被保险人死亡，保险人将按照下面公式退还保险单的现金价值：</em></p>
    <p class="p"><em>现金价值=保险费×[1-(保险单已经过天数/保险期间天数)] ×（1-25%）。</em></p>
    <p class="p"><em>经过天数不足一天的按一天计算。</em></p>
    <p class="p"><em>第六条 保险金额</em></p>
    <p class="p"><em>附加公共交通工具意外伤害保险金额是保险人对保险单中载明的被保险人在保险期间内旅行，因遭受本附加条款第二条所述的事故导致身故或残疾的，保险人承担给</em>付本项保险金责任的最高限额。附加公共交通工具意外伤害保险金额由投保人、保险人双方约定，并在保险单或批单中载明。<em>保险人和投保人可以在本附加条款项下约定免赔额等限制条件。</em>
    </p>
    <p class="p"><em>第七条 保险期间</em></p>
    <p class="p">除非另有约定，本附加条款的保险期间与主险合同约定的保险期间一致。</p>
    <p class="p"><em>第八条 保险金申请</em></p>
    <p class="p"><em>保险金申请人</em>（见释义第8条）向保险人申请给付保险金时，应提交以下材料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。<em>保险金申请人未能提供有关材料，导致保险人无法确定保险事故的性质、原因、损失程度等的，保险人对无法确定部分不承担给付保险金的责任。</em>
    </p>
    <p class="p"><em>（一）身故保险金申请</em></p>
    <p class="p">1、保险金申请书；</p>
    <p class="p">2、保险单或其他保险凭证正本；</p>
    <p class="p">3、保险金申请人的身份证明；</p>
    <p class="p">
      4、公安部门出具的被保险人户籍注销证明、二级以上（含二级）医院或保险人认可的其他医疗机构出具的被保险人身故证明书。若被保险人为宣告死亡，保险金申请人应提供人民法院出具的宣告死亡证明文件。如被保险人在境外身故的，需要提供中华人民共和国驻该国使、领馆或保险事故发生地政府有关机构出具的被保险人死亡证明或验尸报告；</p>
    <p class="p">5、保险金申请人所能够提供的与确认保险事故的性质、原因、伤害程度等有关的其他证明和资料；</p>
    <p class="p">6、若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件；</p>
    <p class="p">7、若被保险人投保时未指定身故受益人，保险金申请人应提供法定继承人公证书；</p>
    <p class="p">8、若是<em>商务旅行</em>（见释义第9条），需被保险人的雇主单位提供的加盖雇主单位公章的被保险人商务旅行的证明。</p>
    <p class="p"><em>（二）残疾保险金申请</em></p>
    <p class="p">1、保险金申请书；</p>
    <p class="p">2、保险单或其他保险凭证正本；</p>
    <p class="p">3、被保险人身份证明；</p>
    <p class="p">4、二级以上（含二级）医院或保险人认可的其他医疗机构或司法鉴定机构出具的残疾鉴定诊断书；</p>
    <p class="p">5、保险金申请人所能提供的其他与本项申请相关的材料；</p>
    <p class="p">6、若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件；</p>
    <p class="p">7、若是商务旅行，需被保险人的雇主单位提供的加盖雇主单位公章的被保险人商务旅行的证明。</p>
    <p class="p"><em>第九条 附加条款的效力</em></p>
    <p class="p">本附加条款所附属的主险合同效力终止，本附加条款效力随之终止。主险合同无效，本附加条款亦无效。</p>
    <p class="p"><em>第十条 释义</em></p>
    <p class="p"><em>1、公共交通工具：</em></p>
    <p class="p">指领有当地政府主管部门依法颁发的公共交通营运执照，以收费方式合法载客的以下交通工具：</p>
    <p class="p">（1）公共汽车、长途汽车 、渡船、气垫船、水翼船、轮船、火车、有轨电车、轨道列车（包括地铁、轻轨及磁悬浮列车）；</p>
    <p class="p">（2）经营固定航班的航空公司经营的来往商业客运机场的定翼飞机<em>；</em></p>
    <p class="p">（3）航空公司所经营的且往来商业客运机场之间或有营运执照的直升机站之间营运的直升飞机；</p>
    <p class="p">（4）按固定路线和时间表营运的固定机场客车。</p>
    <p class="p"><em>公共交通工具不包括出租车。</em></p>
    <p class="p"><em>若上述所列的各种公共交通工具用于非公共交通工具的目的和用途，均不符合本附加条款中“公共交通工具”的定义。此外，政府、企业及私人包机亦不在公共交通工具定义之内。 </em></p>
    <p class="p"><em>2、酗酒：</em></p>
    <p class="p"><em>指酒精摄入过量。长期过量饮酒导致身体脏器严重损害，或一次大量饮酒导致急性酒精中毒或自制力丧失造成自身伤害、斗殴肇事或交通肇事。酒精过量应由医疗机构或公安部门判定。</em></p>
    <p class="p"><em>3、无合法有效驾驶证驾驶</em></p>
    <p class="p">指被保险人乘坐的公共交通工具驾驶员存在下列情形之一者：</p>
    <p class="p">（1）无驾驶证或驾驶证有效期已届满；</p>
    <p class="p">（2）驾驶的机动车与驾驶证载明的准驾车型不符；</p>
    <p class="p">（3）实习期内驾驶公共汽车、营运客车或者载有爆炸物品、易燃易爆化学物品、剧毒或者放射性等危险物品的机动车，实习期内驾驶的机动车牵引挂车；</p>
    <p class="p">（4）持未按规定审验的驾驶证，以及在暂扣、扣留、吊销、注销驾驶证期间驾驶机动车；</p>
    <p class="p">（5）使用各种专用机械车、特种车的人员无国家有关部门核发的有效操作证，驾驶营业性客车的驾驶人无国家有关部门核发的有效资格证书；</p>
    <p class="p">（6）依照法律法规或公安机关交通管理部门有关规定不允许驾驶机动车的其他情况下驾车。</p>
    <p class="p"><em>4、无有效行驶证</em></p>
    <p class="p">指被保险人乘坐的公共交通工具存在下列情形之一：</p>
    <p class="p">（1）机动车被依法注销登记的；</p>
    <p class="p">（2）无公安机关交通管理部门核发的行驶证、号牌，或临时号牌或临时移动证的机动交通工具；</p>
    <p class="p">（3）未在规定检验期限内进行机动车安全技术检验或检验未通过的机动交通工具，或未依法按时进行或通过机动车安全技术检验。</p>
    <p class="p"><em>5、艾滋病：</em></p>
    <p class="p">指获得性免疫缺陷综合征（AIDS），应依照世界卫生组织（WHO）制订的诊断标准确诊。</p>
    <p class="p"><em>6、艾滋病病毒：</em></p>
    <p class="p">指人类免疫缺陷病毒（HIV）。如在血液样本中发现人类免疫缺陷病毒（HIV）或其抗体，则可认定为感染艾滋病病毒或患艾滋病。</p>
    <p class="p"><em>7、管制药品：</em></p>
    <p class="p">指根据《中华人民共和国药品管理法》及有关法规被列为特殊管理的药品，包括但不限于麻醉药品、精神药品、毒性药品及放射性药品。</p>
    <p class="p"><em>8、保险金申请人：</em></p>
    <p class="p">指受益人或被保险人的继承人或依法享有保险金请求权的其他人。</p>
    <p class="p"><em>9、商务旅行：</em></p>
    <p class="p">指被保险人经其雇主委派，从事以商务为目的的旅行。该旅行并不包括被保险人往来其日常居住地与日常工作地或被保险人的个人旅游或旅行。</p>
