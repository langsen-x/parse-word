    </p>
    <p class="p">
      （一）如意外伤害事故发生在境外的，自意外伤害事故发生之日起九十日内，被保险人在事故发生地所在国家或地区的医院进行治疗所发生的，符合本附加条款约定的，实际支出的合理、正常且必须的医疗费用，包括医生诊断费、手术费、住院费、药费（仅限于医生处方所指定的药品）、X光检查、医疗用品、救护车等费用。</p>
    <p class="p">
      （二）如意外伤害事故发生在境内的，自意外伤害事故发生之日起九十日内，被保险人在境内医院进行治疗所发生的，符合本附加条款约定的，实际支出的合理、正常且必须的、符合当地社会医疗保险主管部门规定的基本医疗保险范围内的医疗费用。<em>保险人对境内治疗地基本医疗保险支付范围之外的药品、检查、治疗、材料等费用不负赔偿责任。</em>
