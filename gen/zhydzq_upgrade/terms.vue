    <p class="p">主要运营目的是以住院形式提供接待病人并为其提供医疗护理和治疗；</p>
    <p class="p">在一名或若干名医生的指导下为病人治疗，其中至少有一名合法执业资格的驻院医生驻诊；</p>
    <p class="p">维持足够妥善的设备为病人提供医学诊断和治疗，并于机构内或由其管理的其他场所提供进行各种手术的设备；</p>
