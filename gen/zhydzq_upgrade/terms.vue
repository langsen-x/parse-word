    <p class="p"><em>第一条 附加保险条款订立</em></p>
    <p class="p">本附加保险条款（以下简称“本附加条款”）须附加于旅行人身意外伤害保险（以下简称“主险”）条款使用。本附加条款作为主险合同之有效组成部分，与投保单、保险单及明细表、主险条款、批单等具有同等法律效力。</p>
    <p class="p"><em>第二条 保险责任</em></p>
    <p class="p">在保险期间内，被保险人持有有效证件在中华人民共和国境内或境外旅行时，因遭受<em>意外伤害</em>（见释义第1条）事故导致<em>严重受伤</em>（见释义第2条）或罹患<em>突发性疾病</em>（见释义第3条），经保险人委托的救援机构或其授权代表（以下简称“救援机构”）确认被保险人需要以下救援服务，保险人将按照本保险合同约定承担如下紧急医疗运送和医疗送返费用，但以保险金额为限：
    </p>
    <p class="p">（一）紧急医疗运送</p>
    <p class="p">
      将被保险人运送到距离保险事故发生地最近的且具备适当医疗护理条件的所在地医院；如果救援机构认为事故发生地医院的医疗条件不能保证被保险人得到及时充分的医疗救助时，被保险人将被转运到其他医疗条件合适的所在地医院或者邻近国家的医院。</p>
    <p class="p">在运送过程中，因病情需要，救援机构将派遣医护人员护送被保险人。</p>
    <p class="p">对被保险人的紧急医疗运送手段，以在保险事故发生地能够提供的最合适的手段为限。如果以空运为转运方式，一般使用商业航班。如果救援机构认为必要并经保险人认可，可以包机或者使用医疗救护专用机运送被保险人。</p>
    <p class="p">（二）医疗送返</p>
    <p class="p">救援机构认为被保险人的伤势或病情已稳定，救援机构将安排被保险人乘坐商业航班返回其<em>原出发地</em>（见释义第4条）。如救援机构认为必要，可以在转运被保险人回原出发地过程中提供医疗护送。</p>
    <p class="p">
      如果救援机构认为被保险人伤势或病情允许，将根据被保险人的指定安排其返回原出发地的机场；如果被保险人未指定或者不能指定有关地点，救援机构将安排被保险人回到最近的可直飞到达原出发地的机场。该次医疗送返责任自被保险人被送至上述机场时终止。</p>
    <p class="p"><em>如果救援机构认为被保险人在抵达原出发地时需入院治疗，救援机构将安排被保险人到上述机场所在地被保险人指定的任意一家医院，但如果被保险人未指定或者不能指定有关医院，救援机构将安排被保险人到距离其原出发地机场最近的、具备适当医疗、护理条件的医院，该次医疗送返后本附加条款保险责任终止。对于返回原出发地机场之后仍需继续的救援，救援机构仅负责救援的安排与实施，保险人不承担相关费用。</em>
    </p>
    <p class="p"><em>如果救援机构认为被保险人的健康状况允许其乘坐商业航班返回原出发地，被保险人应使用其开始旅行时购买的原始回程机票或电子机票凭证。如果被保险人所购买的原始回程机票或电子机票凭证，由于救援过程而过期失效，救援机构将安排被保险人回程的经济舱等级机票，并由保险人承担其回程经济舱等级机票的费用。但被保险人需把原始回程机票或电子机票凭证交给救援机构或向救援机构提供先前购买回程机票的证明。若被保险人无原始回程机票或电子机票凭证，或无法提供任何证明，则被保险人从所在地返回原出发地的单程机票费由被保险人自付。</em>
    </p>
    <p class="p"><em>如果被保险人经救援机构的医生认定无法独自返回原出发地，救援机构可安排被保险人的一名随行人员陪同其返回其原出发地。被保险人的随行人员送返时应使用其开始旅行时购买的原始回程机票或电子机票凭证。若原始回程机票或电子凭证由于救援过程而过期失效，救援机构将安排该随行人员回程的经济舱等级机票，并由保险人承担其回程经济舱等级机票的费用。但该随行人员需把原始回程机票或电子机票凭证交给救援机构或向救援机构提供先前购买回程机票的证明。若无原始回程机票或电子机票凭证或无法提供任何证明，则该随行人员从所在地返回原出发地的单程机票费由被保险人自付。</em>
    </p>
    <p class="p">如果被保险人经救援机构的医生认定无法独自返回原出发地，且救援机构认为被保险人的随行人员中没有适合陪送的人员, 救援机构将安排被保险人的一名国内直系家庭成员接送被保险人并立即返回原出发地,
      保险人将承担该陪送人员的往返程经济舱等级机票费用及旅行签证费用。</p>
    <p class="p">
      如果被保险人经救援机构的医生认定无法独自返回原出发地，且救援机构认为被保险人的随行人员和国内直系家庭成员中均没有适合陪送被保险人的人员，救援机构将安排一名护送人员陪同被保险人返回原出发地，保险人将承担该护送人员的往返经济舱等级机票费用及旅行签证费用。</p>
    <p class="p"><em>以上救援服务所需的费用（除被保险人自负费用外）经保险人核实确认后直接支付给救援机构，费用总数最高以保险单或保险凭证中所载的本附加条款保险金额为限。倘若实际费用超过本附加条款的保险金额，则超出部分由被保险人或其亲属自行与救援机构结算。</em>
    </p>
    <p class="p">
      救援机构根据被保险人身体状况或治疗需要，有权决定运送或送返的方式和目的地。运送和送返方式包括配备专业医生、护士和必要的运输工具。保险人根据救援机构安排，承担紧急医疗运送和送返过程中产生的运输、运输途中医疗护理及医疗设备和用品的费用，但以保险金额为限。</p>
    <p class="p"><em>任何未经救援机构批准及安排所发生的紧急医疗运送和医疗送返费用，保险人不负责承担。</em>如果在紧急医疗情况下，投保人、被保险人及其旅伴无法及时通知救援机构，保险人将有权根据投保人所选择的保险计划，以及在相同情况下由救援机构提供或安排服务所需要的合理的费用标准就被保险人实际支出的相关费用进行赔偿。
    </p>
    <p class="p"><em>第三条 责任免除</em></p>
    <p class="p"><em>因下列情形之一，直接或间接导致被保险人需要医疗运送及送返的，保险人不承担赔偿责任</em>：</p>
    <p class="p"><em>（一）被保险人康复性治疗、物理治疗、安胎及分娩（包括剖腹产、流产及引产）；</em></p>
    <p class="p"><em>（二）被保险人美容、整形、矫形术、非必须紧急性治疗的手术、心理咨询及和角膜屈光成形手术；</em></p>
    <p class="p"><em>（三）被保险人健康护理(含体检、疗养、特别护理或静养)等非治疗性的行为及无客观病征证明其不健康及以捐献身体器官或接受器官移植为目的的医疗行为；</em></p>
    <p class="p"><em>（四）被保险人移植人工器官、洗牙、验光、牙齿治疗或手术及镶补，但因意外伤害引起的必要的牙齿治疗或手术除外；</em></p>
    <p class="p"><em>（五）被保险人先天性疾病</em>（见第5条释义）<em>和症状、遗传性疾病、先天性畸形或缺陷的治疗和康复；</em></p>
    <p class="p"><em>（六）被保险人在本保险合同保险期间开始前已患上的疾病和症状，以及由该疾病引发的其他疾病（无论发生在本保险合同保险期间开始前后）；</em></p>
    <p class="p"><em>（七）未能取得医院或医生证明；</em></p>
    <p class="p"><em>（八）根据救援机构的意见，可以不需医疗运送或送返而被保险人坚持进行的医疗运送或送返；</em></p>
    <p class="p"><em>（九）任何因第三者提供服务而被保险人不需负责给付的费用或任何已包含在旅行收费中的费用；</em></p>
    <p class="p"><em>（十）任何未经救援机构批准并安排的运送和送返费用；</em></p>
    <p class="p"><em>（十一）直接或间接因流行疫病或大规模流行疫病爆发导致的救援；</em></p>
    <p class="p"><em>（十二）主险条款规定的责任免除事项。</em></p>
    <p class="p"><em>被保险人应当严格遵守保险人和救援机构所决定的救援程序，否则保险人有权不承担本附加条款所规定的保险责任，立即停止所有的救援服务，不支付任何由于不遵守救援机构的意见和没有征得救援机构同意而产生的费用。若被保险人拒绝救援机构所建议的救援程序，保险人将不承担因此而带来的任何后果和产生的任何费用。</em>
    </p>
    <p class="p"><em>第四条 保险金额</em></p>
    <p class="p"><em>投保人必须在被保险人</em>开始旅行前投保本附加条款。保险金额是被保险人在保险期间内旅行时发生保险责任事故，由保险人承担给付该项保险金责任的最高限额。保险金额由投保人、保险人双方约定，并在保险单中载明。不同被保险人的附加医疗运送和送返保险金额可以不同。保险金额一经确定，投保单次保障计划的，中途不得变更。<em>保险人和投保人可以在本保险合同项下约定免赔额等限制条件。</em>
    </p>
    <p class="p"><em>第五条 保险期间</em></p>
    <p class="p">除非另有约定，本附加条款的保险期间与主险合同的保险期间一致。</p>
    <p class="p"><em>第六条 被保险人义务</em></p>
    <p class="p">（一）在保险期间内，被保险人发生保险责任范围内的保险事故需要紧急医疗运送和送返时，应立即拨打指定的救援电话与救援机构联系。</p>
    <p class="p">（二）被保险人需偿付救援机构代被保险人先行垫付的不属于本附加条款责任范围内的任何费用。</p>
    <p class="p"><em>第七条 保险金申请</em></p>
    <p class="p"><em>被保险人对于发生所有符合本附加条款规定的保险事故，均应按照本附加条款第六条的规定及时通知救援机构并遵守救援机构安排的救援程序，对于保险人通过救援机构按照本保险合同的约定提供服务并承担的相应费用，由救援机构向保险人进行索赔。保险人不接受任何非通过救援机构提出的索赔。</em>
    </p>
    <p class="p"><em>第八条 附加条款的效力</em></p>
    <p class="p">本附加条款所附属的主险合同效力终止，本附加条款效力随之终止。主险合同无效，本附加条款亦无效。</p>
    <p class="p"><em>第九条 其他事项</em></p>
    <p class="p"><em>（一）由于保险人及救援机构无法控制的原因，直接或间接造成无法履行或延误履行紧急救援责任的，保险人不承担相应责任。保险人无法控制的原因包括但不限于自然灾害、罢工、航班条件、战争、保险事故发生地或运送地的政府或国际组织行为以及其他不可抗力。</em>
    </p>
    <p class="p"><em>（二）保险人根据救援机构意见对紧急救援做出安排，有权拒绝任何不利于被保险人健康状况和安全的请求。如保险人或救援机构认为费用有不合理之处，保险人有权将费用限制在合理正常的范围之内。</em></p>
    <p class="p">（三）对被保险人所进行的任何救助和服务都要遵守有关国际公约，以及相关国家和地区的法律规定。</p>
    <p class="p">
      （四）被保险人应当严格遵守保险人和救援机构所决定的救援程序，否则保险人可以不承担本附加条款所规定的一切保险责任，立即停止所有的救援服务，不支付任何由于不遵守救援机构的意见和没有征得救援机构同意而产生的费用。若被保险人拒绝救援机构所建议的救援程序，保险人将不承担因此而带来的任何后果。</p>
    <p class="p"><em>第十条 释义</em></p>
    <p class="p"><em>1、意外伤害：</em></p>
    <p class="p">指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</p>
    <p class="p"><em>2、严重受伤:</em></p>
    <p class="p">指经医院医生诊断及证实被保险人所受的伤危及被保险人生命及不适宜继续原定行程。</p>
    <p class="p"><em>3、突发性疾病</em>： </p>
    <p class="p"><em>被保险人在本附加条款规定的保险期间内，在旅行时内首次罹患的突然发生的急性疾病或出现的症状，并经医院医生诊断及证实被保险人罹患的疾病危及被保险人生命及不适宜继续原定行程，但不包括本保险合同生效前罹患的任何疾病或出现的任何症状及任何慢性疾病。</em>
    </p>
    <p class="p"><em>4、原出发地： </em></p>
    <p class="p">
      若被保险人进行旅行的目的地为中华人民共和国境内(不包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指被保险人在中华人民共和国境内的户籍所在地或经常居住地；若被保险人进行的旅行目的地为中华人民共和国境外(包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指中华人民共和国境内。</p>
    <p class="p"><em>5、先天性疾病：</em></p>
    <p class="p">
      指被保险人一出生就具有的疾病（症状或体征）。这些疾病是因人的遗传物质（包括染色体以及位于其中的基因）发生了对人体有害的改变而引起的，或因母亲怀孕期间受到内外环境中某些物理、化学和生物等因素的作用，使胎儿局部体细胞发育异常，导致婴儿出生时有关器官系统在结构或功能上呈现异常。</p>
    <p class="p">本附加条款的未释义名词，以本附加条款所附属的主险条款中的释义为准。</p>
