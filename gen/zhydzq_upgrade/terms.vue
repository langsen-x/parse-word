    </p>
    <p class="p">如果上述保险事故发生在境外旅行期间，且被保险人返回境内后五日内到二级或二级以上医院或保险人认可的其他医疗机构就诊，经医生诊断必须继续住院治疗，保险人仍然承担给付意外住院津贴的责任。</p>
    <p class="p">保险人支付意外住院津贴的总天数（包括境内住院和境外住院）最高不超过180天，如果保险单或保险凭证中约定的最高给付天数小于180天，则以保险单或保险凭证中的约定为准。</p>
