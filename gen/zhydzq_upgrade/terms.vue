    <p class="p"><em>保险责任</em></p>
    <p class="p">在保险期间内，被保险人持有有效证件在中华人民共和国境内或境外旅行时，遭受<em>意外伤害</em>（见释义第1条）事故或罹患<em>突发性疾病</em>（见释义第2条），并以此为直接且单独原因导致被保险人于三十天内身故，保险人委托的救援机构或其授权代表（以下简称“救援机构”）依被保险人遗愿或其家属的愿望，在被保险人身故地法律法规允许的范围内按下列情况安排遗体保存或火化，且将被保险人之遗体或骨灰送返被保险人的<em>原出发地</em>（见释义第3条）或者安排就地丧葬：
    </p>
    <p class="p">
      如选择遗体运送回其原出发地的，救援机构以经济交通方式将被保险人的遗体从事故发生地运至可直飞到达的原出发地的机场，如原出发地没有机场的，运送至距离原出发地最近的机场。如果保险单或保险凭证中没有特别约定，保险人所承担的灵柩费以身故地普通灵柩标准为准；如果保险单或保险凭证中有特别约定的，灵柩费以保险单或保险凭证中的约定为准。</p>
    <p class="p">
      如选择火葬的，救援机构负责将被保险人遗体在身故地火葬并以经济交通方式将骨灰运回被保险人的原出发地。如果保险单或保险凭证中没有特别约定，火葬费用以身故地普通丧葬标准为准；如果保险单或保险凭证中有特别约定的，火葬费用以保险单或保险凭证中的约定为准。</p>
    <p class="p">
      如选择就地安葬遗体的，救援机构负责安排被保险人遗体就地安葬。如果保险单或保险凭证中没有特别约定，安葬费用以身故地普通安葬标准为准；如果保险单或保险凭证中有特别约定的，安葬费用以保险单或保险凭证中的约定为准。</p>
    <p class="p">
      如被保险人遗愿及其家属的愿望无法及时查知的，或被保险人遗愿违反身故地法律法规规定且其家属愿望无法及时查知的，救援机构将负责在身故地法律、法规允许的范围内将被保险人遗体在身故地火葬并以经济交通方式将其骨灰运回被保险人的原出发地，火葬费用以身故地普通丧葬标准为准；</p>
    <p class="p"><em>如被保险人遗愿及其家属的愿望违反身故地法律、法规规定的，经救援机构告知后仍不愿更改的，本保险合同于救援机构得知被保险人家属不愿更改的决定之时终止，保险责任同时终止，保险人将退还投保人本附加条款对应的现金价值（见释义第4条）。</em>
    </p>
    <p class="p">
      被保险人身故时身边无直系亲属的，救援机构可以安排一位被保险人直系亲属从原出发地前往被保险人身故所在地处理后事。如果保险单或保险凭证中没有特别约定，保险人负责承担由此产生的一张往返经济舱机票费用和三星级及三星级以下酒店标准间的住宿费用；如果保险单或保险凭证中有特别约定的，保险人在保险单或保险凭证中的约定的额度内负责承担相关<em>公共交通工具</em>（见释义第5条）费用和住宿费用。
    </p>
    <p class="p"><em>任何未经救援机构批准并安排所发生的费用，保险人不负责支付。</em>若在紧急医疗情况下，投保人、被保险人及其旅伴无法及时通知救援机构，保险人将有权根据投保人所选择的保险计划，以及在相同情况下由救援机构提供或安排服务所需要的通常的合理的费用标准就被保险人实际支出的相关费用在保险金额范围内进行赔偿。
    </p>
    <p class="p"><em>责任免除</em></p>
    <p class="p"><em>因下列情形之一，直接或间接导致被保险人需要身故遗体送返的，保险人不承担赔偿责任</em>：</p>
    <p class="p"><em>被保险人康复性治疗、物理治疗、安胎及分娩（包括剖腹产、流产及引产）； </em></p>
    <p class="p"><em>被保险人美容、整形、矫形术、非必须紧急性治疗的手术、心理咨询及和角膜屈光成形手术；</em></p>
    <p class="p"><em>被保险人健康护理(含体检、疗养、特别护理或静养)等非治疗性的行为及无客观病征证明其不健康及以捐献身体器官或接受器官移植为目的的医疗行为；</em></p>
    <p class="p"><em>被保险人移植人工器官、洗牙、验光、牙齿治疗或手术及镶补，但因意外伤害引起的必要的一般牙齿治疗或手术除外；</em></p>
    <p class="p"><em>被保险人非因意外伤害而进行的视力矫正或因视力矫正而作的眼科验光检查；</em></p>
    <p class="p"><em>腰间盘突出症；</em></p>
    <p class="p"><em>药物过敏或其他医疗导致的伤害；</em></p>
    <p class="p"><em>扁桃腺、腺状肿、疝气、女性生殖器官疾病的治疗与外科手术，但若为避免生命危险或健康永久性损伤而导致被保险人须立即接受的紧急治疗或手术，不适用本项责任免除规定；</em></p>
    <p class="p"><em>被保险人先天性疾病</em>（见释义第6条）<em>和症状、遗传性疾病、先天性畸形或缺陷的治疗和康复；</em></p>
    <p class="p"><em>根据被保险人的主治医生的意见，可以被合理延迟至被保险人返回境内后进行而被保险人坚持在境外进行的治疗或手术；</em></p>
    <p class="p"><em>被保险人旅行的目的是为了进行治疗或该旅行违背医嘱；</em></p>
    <p class="p"><em>被保险人在本保险合同保险期间开始前已患上的疾病和症状，以及由该疾病引发的其他疾病（无论发生在本保险合同保险期间开始前后）；</em></p>
    <p class="p"><em>未能取得医院或医生证明；</em></p>
    <p class="p"><em>任何因第三者提供服务而被保险人不需负责给付的费用或任何已包含在旅行收费中的费用；</em></p>
    <p class="p"><em>任何未经救援机构批准并安排的遗体送返费用；</em></p>
    <p class="p"><em>直接或间接因流行疫病或大规模流行疫病爆发导致的救援；</em></p>
    <p class="p"><em>主险条款规定的责任免除事项；</em></p>
    <p class="p"><em>被保险人及其家属应当严格遵守保险人和救援机构所决定的救援程序，否则保险人有权不承担本附加条款所规定的保险责任，并立即停止所有的救援服务，不支付任何由于被保险人或其家属不遵守救援机构的决定或进行没有征得救援机构同意的行为而产生的费用。若被保险人及其家属拒绝救援机构所建议的救援程序，保险人将不承担因此而带来的任何后果和产生的任何费用，包括但不限于保险赔偿责任。</em>
    </p>
    <p class="p"><em>保险金额</em></p>
    <p class="p"><em>保险金额是保险人对保险单或保险凭证中载明的被保险人在保险期间内旅行，承担给付该项保险金责任的最高限额。保险金额由投保人、保险人双方约定，并在保险单或保险凭证中载明。保险人和投保人可以在本保险合同项下约定免赔额等限制条件。</em>
    </p>
    <p class="p"><em>保险期间</em></p>
    <p class="p">除非另有约定，本附加条款的保险期间与主险合同约定的保险期间一致。</p>
    <p class="p"><em>被保险人义务</em></p>
    <p class="p">（一）在保险期间内，被保险人发生保险责任范围内的保险事故需要紧急救援时，应立即拨打指定的救援电话与救援机构联系。</p>
    <p class="p"><em>（二）被保险人亲属应当自行与救援机构结算救援机构代被保险人先行垫付的不属于本附加条款责任范围内的任何费用。</em></p>
    <p class="p">（三）对于救援机构根据被保险人的具体情况决定的救援程序及安排，被保险人应当遵守。</p>
    <p class="p"><em>保险金申请</em></p>
    <p class="p"><em>被保险人发生所有符合本附加条款责任范围内的保险事故，均应按照本附加条款第六条的规定及时通知救援机构并遵守救援机构安排的救援程序。对于保险人通过救援机构按照本保险合同的约定提供服务并承担的相应费用，保险人在保险单或保险凭证中所载的本附加条款保险金额范围内，直接支付给救援机构，由救援机构向保险人进行索赔。保险人不接受任何非通过救援机构提出的索赔。倘若实际费用超过本附加条款的保险金额，则超出部分由被保险人或其亲属自行与救援机构结算。</em>
    </p>
    <p class="p"><em>附加条款的效力</em></p>
    <p class="p">本附加条款所附属的主险合同效力终止，本附加条款效力随之终止。主险合同无效，本附加条款亦无效。</p>
    <p class="p"><em>其他事项</em></p>
    <p class="p"><em>由于保险人及救援机构无法控制的原因，直接或间接造成无法履行或延误履行紧急救援责任的，保险人不承担相应责任。保险人无法控制的原因包括但不限于：自然灾害、罢工、航班条件、战争、保险事故发生地或运送所在地的政府或国际组织行为以及其他不可抗力。</em>
    </p>
    <p class="p"><em>保险人有权根据救援机构意见对紧急救援做出安排。如保险人或救援机构认为费用有不合理之处，保险人有权将费用限制在合理正常的范围之内，并以保险金额为限。</em></p>
    <p class="p">对被保险人所进行的任何救助和服务都要遵守有关国际公约，以及相关国家和地区的法律规定。</p>
    <p class="p"><em>释义</em></p>
    <p class="p"><em>意外伤害：</em></p>
    <p class="p"><em>指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</em></p>
    <p class="p"><em>突发性疾病</em>：</p>
    <p class="p"><em>指被保险人在本附加条款规定的保险期间内，在旅行时内首次罹患的突然发生的急性疾病或出现的症状，并经医院医生诊断及证实被保险人罹患的疾病危及被保险人生命及不适宜继续原定行程，但不包括本保险合同生效前罹患的任何疾病或出现的任何症状及任何慢性疾病。</em>
    </p>
    <p class="p"><em>原出发地：</em></p>
    <p class="p">
      若被保险人进行旅行的目的地为中华人民共和国境内(不包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指被保险人在中华人民共和国境内的户籍所在地或经常居住地；若被保险人进行的旅行目的地为中华人民共和国境外(包括香港特别行政区、澳门特别行政区及台湾地区)，则原出发地指中华人民共和国境内。</p>
    <p class="p"><em>现金价值</em></p>
    <p class="p">现金价值=保险费×[1-(保险单已经过天数/保险期间天数)] ×（1-25%）</p>
    <p class="p">经过天数不足一天的按一天计算。</p>
    <p class="p"><em>公共交通工具：</em></p>
    <p class="p">指领有当地政府主管部门依法颁发的公共交通营运执照，以收费方式合法载客的以下交通工具：</p>
    <p class="p">公共汽车、长途汽车 、渡船、气垫船、水翼船、轮船、火车、有轨电车、轨道列车（包括地铁、轻轨及磁悬浮列车）；</p>
    <p class="p">经营固定航班的航空公司经营的来往商业客运机场的定翼飞机；</p>
    <p class="p">航空公司所经营的且往来商业客运机场之间或有营运执照的直升机站之间营运的直升飞机；</p>
    <p class="p">按固定路线和时间表营运的固定机场客车。</p>
    <p class="p"><em>公共交通工具不包括出租车。</em></p>
    <p class="p"><em>若上述所列的各种公共交通工具用于非公共交通工具的目的和用途，均不符合本附加条款中“公共交通工具”的定义。另外，政府、企业及私人包机亦不在公共交通工具定义之内。</em></p>
    <p class="p"><em>先天性疾病：</em></p>
    <p class="p">
      指被保险人一出生就具有的疾病（症状或体征）。这些疾病是因人的遗传物质（包括染色体以及位于其中的基因）发生了对人体有害的改变而引起的，或因母亲怀孕期间受到内外环境中某些物理、化学和生物等因素的作用，使胎儿局部体细胞发育异常，导致婴儿出生时有关器官系统在结构或功能上呈现异常。</p>
    <p class="p">本附加条款的未释义名词，以本附加条款所附属的主险条款中的释义为准。</p>
    <p class="p"><em>条款的适用</em></p>
    <p class="p">本附加条款与主险条款不一致之处，以本附加条款为准；本附加条款未尽之处，以主险条款为准。</p>

