    <p class="p"><em>因下列情形之一，直接或间接导致被保险人住院的，保险人不承担给付意外住院津贴的责任</em>：</p>
    <p class="p"><em>投保人故意造成被保险人的死亡、伤残或疾病的；</em></p>
    <p class="p"><em>被保险人故意自伤；</em></p>
