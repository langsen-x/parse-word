        <h1 class="h1"><em>中国大地财产保险股份有限公司营业中断保险（B款）条款</em></h1>
        <h1 class="h1"><em>（注册号：C00001030612020052900821）</em></h1>
        <h2 class="h2"><em>总则</em></h2>
        <p class="p"><em>第一条</em> 本保险合同（以下简称为“本合同”）由保险条款、投保单、保险单或其他保险凭证、批单以及其他有关书面约定组成。凡涉及本合同的约定，均应采用书面形式。
        </p>
        <p class="p"><em>第二条</em> 在中华人民共和国境内（不包括港澳台地区，下同）依法从事生产经营活动的企业、事业单位、国家机关、社会团体、其他组织或个人，均可作为本合同的投保人和被保险人。
        </p>
        <h2 class="h2">保险责任</h2>
        <p class="p"><em>第三条</em>
            在保险期间内，被保险人在中华人民共和国境内依法从事生产经营活动的过程中，由于下列原因造成保险单中载明的被保险人的生产经营工具、营业场所（以下统称“生产经营必要财产”）遭受直接物质损坏或灭失，导致被保险人的营业中断，对由此产生的毛利润损失，保险人按照本合同的约定负责赔偿：
        </p>
        <p class="p">（一） 火灾、爆炸；</p>
        <p class="p">（二） 雷击、暴雨、洪水、暴风、龙卷风、冰雹、暴雪、冰凌、突发性滑坡、崩塌、泥石流、地面突然下陷下沉；</p>
        <p class="p">（三）飞行物体及其他空中运行物体坠落。</p>
        <p class="p">本保险合同所称毛利润按照下述公式之一计算确定，具体以保险单载明的为准：</p>
        <p class="p">毛利润=营业利润+约定的维持费用，或毛利润=约定的维持费用-营业亏损×约定的维持费用/全部的维持费用</p>
        <p class="p">
            本保险合同所称维持费用是指被保险人为维持正常的营业活动而发生的、不随被保险人营业收入的减少而成正比例减少的成本或费用。约定的维持费用由投保人与保险人在订立本保险合同时协商确定，并在保险单中载明。</p>
        <h2 class="h2"><em>责任免除</em></h2>
        <p class="p"><em>第四条 下列原因造成的损失、费用，保险人不负责赔偿：</em></p>
        <p class="p"><em>（一）投保人、被保险人的故意或重大过失行为；</em></p>
        <p class="p"><em>（二）战争、敌对行动、军事行为、武装冲突、罢工、骚乱、暴动、恐怖活动；</em></p>
        <p class="p"><em>（三）地震及其次生灾害、海啸；</em></p>
        <p class="p"><em>（四）核辐射、核爆炸、核污染或其他放射性污染；</em></p>
        <p class="p"><em>（五）大气污染、土地污染、水污染或其他各种污染；</em></p>
        <p class="p"><em>（六）行政行为或司法行为；</em></p>
        <p class="p"><em>（七）公共供电、供水、供气或其他能源供应中断；</em></p>
        <p class="p"><em>（八）自然磨损、折旧、物质本身变化或其他渐变原因；</em></p>
        <p class="p"><em>（九）内在或潜在缺陷、变质、霉烂、受潮、虫咬、鸟啄、氧化、锈蚀、渗漏、自燃、烘焙。</em></p>
        <p class="p"><em>第五条 对于下列损失、费用，保险人不负责赔偿：</em></p>
        <p class="p"><em>（一）生产经营必要财产等直接财产损失；</em></p>
        <p class="p"><em>（二）违约金、罚款、罚金或惩罚性赔偿。</em></p>
        <p class="p"><em>第六条 其他不属于本保险责任范围内的损失、费用，保险人也不负责赔偿。</em></p>
        <h2 class="h2"><em>保险金额</em></h2>
        <p class="p"><em>第七条</em>
            保险金额根据被保险人会计账表中毛利润的相关数据由投保人与保险人在订立本保险合同时协商确定，并在保险单中载明。其中，日均毛利润保险金额参照投保之日前十二个月被保险人的实际日均毛利润，由投保人与保险人双方协商确定，并在保险单中载明。
        </p>
        <p class="p"><em>保险期间、最高赔偿天数与免赔天数</em></p>
        <p class="p"><em>第八条</em> 除另有约定外，保险期间为一年，以保险单载明的起讫时间为准。</p>
        <p class="p"><em>第九条</em> 每次事故最高赔偿天数最长不超过一年，由投保人与保险人双方协商确定，并在保险单中载明。
        </p>
        <p class="p"><em>第十条</em> <em>每次事故免赔天数由投保人与保险人双方协商确定，并在保险单中载明。</em></p>
        <h2 class="h2"><em>保险人义务</em></h2>
        <p class="p"><em>第十一条</em>
            订立本合同，保险人向投保人提供的投保单应当附本保险条款，保险人应当向投保人说明合同的内容。对合同中免除保险人责任的条款，保险人在订立合同时应当在投保单、保险单或者其他保险凭证上作出足以引起投保人注意的提示，并对该条款的内容以书面或者口头形式向投保人作出明确说明；未作提示或者明确说明的，该条款不产生效力。
        </p>
        <p class="p"><em>第十二条</em> 本合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</p>
        <p class="p"><em>第十三条</em> 保险人依本保险条款第十七条取得的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。
        </p>
        <p class="p">
            保险人在本合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担赔偿责任。</p>
        <p class="p"><em>第十四条</em> 保险事故发生后，投保人、被保险人提供的有关索赔的证明和资料不完整的，保险人应当及时一次性通知投保人、被保险人补充提供。
        </p>
        <p class="p"><em>第十五条</em> 保险人收到被保险人的赔偿保险金的请求后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在三十日内作出核定，但本合同另有约定的除外。
        </p>
        <p class="p">
            保险人应当将核定结果通知被保险人；对属于保险责任的，在与被保险人达成赔偿保险金的协议后十日内，履行赔偿保险金义务。本合同对赔偿保险金的期限另有约定的，保险人应当按照约定履行赔偿保险金的义务。保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人发出拒绝赔偿保险金通知书，并说明理由。</p>
        <p class="p"><em>第十六条</em>
            保险人自收到赔偿保险金的请求和有关证明、资料之日起六十日内，对其赔偿数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定赔偿的数额后，应当支付相应的差额。
        </p>
        <h2 class="h2"><em>投保人与被保险人义务</em></h2>
        <p class="p"><em>第十七条</em> 订立保险合同，保险人就保险标的或者被保险人的有关情况提出询问的，投保人应当如实告知。
        </p>
        <p class="p">
            投保人故意或者因重大过失未履行前款约定的如实告知义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除保险合同。</p>
        <p class="p">
            <em>投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担赔偿责任，并不退还保险费。</em>
        </p>
        <p class="p">
            <em>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担赔偿责任，但应当退还保险费。</em>
        </p>
        <p class="p"><em>第十八条</em> 除另有约定外，投保人应在保险合同成立时一次性交清全部保险费。投保人未按照本保险合同的约定交付保险费的，保险人有权解除本合同。
        </p>
        <p class="p"><em>第十九条</em> 被保险人应当遵守国家有关消防、安全、生产操作、劳动保护等方面规定，加强管理，采取合理的预防措施，尽力避免或减少责任事故的发生，维护保险标的的安全。
        </p>
        <p class="p">
            保险人可以对被保险人遵守前款约定的情况进行检查，向投保人、被保险人提出消除不安全因素和隐患的书面建议，投保人、被保险人应该认真付诸实施。</p>
        <p class="p">投保人、被保险人未按照约定履行其对保险标的的安全应尽责任的，保险人有权要求增加保险费或者解除合同。</p>
        <p class="p"><em>第二十条</em> 在本合同有效期内，保险标的的危险程度显著增加的，被保险人应当及时通知保险人，保险人可以要求增加保险费或者解除合同。
        </p>
        <p class="p">
            <em>被保险人未履行前款约定的通知义务的，因保险标的的危险程度显著增加而发生的保险事故，保险人不承担赔偿责任。</em>
        </p>
        <p class="p"><em>第二十一条</em> 知道保险事故发生后，被保险人应当：</p>
        <p class="p"><em>（一）尽力采取必要、合理的措施，防止或减少损失。否则，对因此扩大的损失，保险人不承担赔偿责任。</em></p>
        <p class="p"><em>（二）立即通知保险人，并书面说明事故发生的原因、经过和损失情况。故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担赔偿责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。</em>
        </p>
        <p class="p">
            <em>（三）保护事故现场，允许并且协助保险人进行事故原因和损失情况的调查，以及对被保险人相关的会计凭证</em>和账表的检查。<em>对于拒绝或者妨碍保险人进行上述调查或检查导致无法确定事故原因或核实损失情况的，保险人对无法核实的部分不承担赔偿责任。</em>
        </p>
        <p class="p"><em>第二十二条</em> 被保险人请求赔偿时，应向保险人提供下列证明和资料：</p>
        <p class="p">（一）保险单正本；</p>
        <p class="p">（二）索赔申请；</p>
        <p class="p">（三）相关的会计凭证及账表；</p>
        <p class="p">（四）生产经营必要财产维修证明（适用生产经营必要财产遭受直接物质损坏的情况）；</p>
        <p class="p">（五）事故报告书；</p>
        <p class="p">（六）投保人、被保险人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料。</p>
        <p class="p">
            <em>投保人、被保险人未履行前款约定的单证提供义务，导致保险人无法核实损失情况的，保险人对无法核实的部分不承担赔偿责任。</em>
        </p>
        <h2 class="h2"><em>赔偿处理</em></h2>
        <p class="p"><em>第二十三条</em> 保险事故发生后，被保险人应当及时对其遭受损坏的生产经营必要财产进行修复，并在修理或加固前会同保险人进行检验，确定所需修理或加固天数（以下简称“修复天数”），<em>否则，保险人有权重新核定。</em>
        </p>
        <p class="p"><em>第二十四条</em> 保险事故发生后，保险人按照以下方式计算赔偿：</p>
        <p class="p">赔偿金额=日均毛利润×（赔偿天数-<em>免赔天数</em>）</p>
        <p class="p">
            日均毛利润为保险事故发生之日前十二个月中与被保险人因保险事故而营业中断期间对应的日历期间内被保险人的日均实际毛利润；其中生产经营必要财产灭失的，前述营业中断期间自营业中断之日起至保险单载明的每次事故最高赔偿天数届满之日止。若被保险人实际运营期不满十二个月，则日均毛利润为保险事故发生之日前三个月或被保险人实际运营期内的日均实际毛利润（两者以低者为准）。日均毛利润最高不超过保险单中载明的日均毛利润保险金额。</p>
        <p class="p">
            赔偿天数为保险事故发生后，被保险人营业中断的天数或被保险人会同保险人依据第二十三条确定的修复天数（两者以低者为准），最多不超过保险单中载明的每次事故最高赔偿天数。</p>
        <p class="p">在保险期间内，保险人对多次事故的累计赔偿金额不超过保险单中载明的保险金额。</p>
        <p class="p"><em>第二十五条</em>
            <em>保险事故发生时，如果存在重复保险，保险人按照本保险合同的相应保险金额与其他保险合同及本保险合同相应保险金额总和的比例承担赔偿责任。</em>
        </p>
        <p class="p">
            <em>其他保险人应承担的赔偿金额，本保险人不负责垫付。若被保险人未如实告知导致保险人多支付赔偿金，保险人有权向被保险人追回多支付的部分。</em>
        </p>
        <p class="p">第二十六条
            发生保险责任范围内的损失，应由有关责任方负责赔偿的，保险人自向被保险人赔偿保险金之日起，在赔偿金额范围内代位行使被保险人对有关责任方请求赔偿的权利，被保险人应当向保险人提供必要的文件和所知道的有关情况。</p>
        <p class="p">
            <em>被保险人已经从有关责任方取得赔偿的，保险人赔偿保险金时，可以相应扣减被保险人已从有关责任方取得的赔偿金额。</em>
        </p>
        <p class="p">
            <em>保险事故发生后，在保险人未赔偿保险金之前，被保险人放弃对有关责任方请求赔偿的权利的，保险人不承担赔偿责任。</em>保险人向被保险人赔偿保险金后，被保险人未经保险人同意放弃对有关责任方请求赔偿的权利的，该行为无效。<em>由于被保险人的故意或者因重大过失致使保险人不能行使代位请求赔偿的权利的，保险人可以扣减或者要求返还相应的保险金。</em>
        </p>
        <p class="p"><em>第二十七条</em> 被保险人对保险人请求赔偿的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。
        </p>
        <h2 class="h2"><em>争议处理</em></h2>
        <p class="p"><em>第二十八条</em> 因履行本合同发生的争议，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁；保险单未载明仲裁机构且争议发生后未达成仲裁协议的，应向人民法院起诉。
        </p>
        <p class="p"><em>第二十九条</em> 本合同的争议处理适用中华人民共和国法律（不含港澳台地区法律）。</p>
        <h2 class="h2"><em>其他事项</em></h2>
        <p class="p"><em>第三十条</em> 除法律另有规定或本合同另有约定外，本合同的解除及其相关事宜按照如下约定办理：</p>
        <p class="p">
            （一）本合同成立后，投保人可以解除本合同，保险人不得解除本合同；本合同自一方当事人的解除通知送达另一方时解除。</p>
        <p class="p">
            （二）保险责任开始前，投保人解除本合同的，应当按本合同的约定（以不超过保险费的5%为限）向保险人支付退保手续费，保险人应当退还剩余部分保险费。</p>
        <p class="p">
            （三）保险责任开始后，投保人解除本合同的，保险人按短期费率计收保险责任开始之日起至合同解除之日止期间的保险费，并退还剩余部分保险费；保险人解除本合同的，保险人按照日比例计收自保险责任开始之日起至合同解除之日止期间的保险费，并退还剩余部分保险费。</p>
