            等待期由投保人与保险人协商确定，并在本合同中载明；未在本合同中载明的，则为三十日。同时满足以下三个条件的，免除等待期：</p>
        <p class="p">（一）非首次投保本保险；</p>
        <p class="p">（二）投保人在上一个保险期间届满前重新向保险人投保本保险；</p>
        <p class="p">（三）本合同的保险期间与上一个保险合同的保险期间之间连续不间断。</p>
        <h2 class="h2"><em>不保证续保</em></h2>
        <p class="p">第十五条
            本合同为不保证续保合同。本合同保险期间届满，投保人可重新向保险人投保本保险，经保险人同意，交纳保险费，获得新的保险合同，但本保险不保证续保。</p>
        <h2 class="h2"><em>保险人义务</em></h2>
        <p class="p">第十六条 保险人同意承保的，应当及时向投保人签发保险单或其他保险凭证。</p>
        <p class="p">第十七条
            保险人认为保险金申请人提供的有关索赔的证明和资料不完整的，应当及时一次性通知保险金申请人补充提供。</p>
        <p class="p">第十八条
            保险人收到保险金申请人提供的本保险条款“保险金申请与给付”部分约定的保险金申请证明和资料后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在三十日内作出核定。</p>
        <p class="p">
            保险人应当将核定结果通知保险金申请人。对属于保险责任的，在与保险金申请人达成给付保险金的协议后十日内，履行给付保险金的义务；对不属于保险责任的，应当自作出核定之日起三日内向保险金申请人发出拒绝给付保险金通知书，并说明理由。</p>
        <p class="p">第十九条
            保险人自收到保险金申请人提供的本保险条款“保险金申请与给付”部分约定的保险金申请证明和资料之日起六十日内，对其给付保险金的数额不能确定的，应当按根据已有证明和资料可以确定的数额先予支付；保险人最终确定给付保险金的数额后，支付相应的差额。</p>
        <h2 class="h2"><em>投保人、被保险人义务</em></h2>
        <p class="p">第二十条
            投保人应当在订立本合同时一次交清保险费。投保人未按照本合同的约定交清保险费的，本合同不生效。</p>
        <p class="p">第二十一条 订立本合同时，保险人就保险标的或被保险人的有关情况提出询问的，投保人应当如实告知。</p>
        <p class="p">
            投保人故意或因重大过失未履行前款规定的义务，足以影响保险人决定是否同意承保或提高保险费率的，保险人有权解除本合同。</p>
        <p class="p">前款规定的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。</p>
        <p class="p">
            投保人故意不履行如实告知义务的，保险人对于本合同解除前发生的保险事故，不承担给付保险金的责任，并不退还保险费。</p>
        <p class="p">
            投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于本合同解除前发生的保险事故，不承担给付保险金的责任，但退还保险费。</p>
        <p class="p">
            保险人在本合同订立时已经知道投保人、被保险人未如实告知的情况的，保险人不得解除本合同；发生保险事故的，保险人承担给付保险金的责任。</p>
        <p class="p">第二十二条
            投保人、被保险人或保险金申请人应于被保险人每次住院治疗开始后及时通知保险人。故意或因重大过失未及时予以通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金的责任，但保险人通过其他途径已经及时知道或应当及时知道保险事故发生的不在此限。</p>
        <p class="p">上述约定，不包括因不可抗力而导致的迟延。</p>
        <p class="p">第二十三条
            投保人住所或通讯地址变更时，应当及时以书面形式通知保险人。投保人未及时通知的，保险人按本合同所载的最后住所或通讯地址发送的有关通知或其他文件，均视为已发送给投保人。</p>
        <p class="p">保险金申请与给付</p>
        <p class="p">第二十四条 保险金申请人向保险人申请给付保险金时，应当提交下列证明和资料：</p>
        <p class="p">（一）保险金给付申请书；</p>
        <p class="p">（二）保险单或其他保险凭证、批单；</p>
        <p class="p">（三）保险金申请人的身份证明，若保险金申请人系受托申请，还应当提供授权委托书；</p>
        <p class="p">（四）保险人指定或认可的医疗机构出具的疾病诊断证明书、病历、出院小结及医疗费用原始收据；</p>
        <p class="p">（五）投保人、被保险人或受益人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料。</p>
        <p class="p">
            保险金申请人未能提供有关证明和资料，导致保险人无法核实保险事故的性质、原因、损失程度的，保险人对无法核实部分不承担给付保险金的责任。</p>
        <p class="p">第二十五条
            收到保险事故发生的通知或信息的，保险人有权对投保人、被保险人、受益人和有关医疗机构等进行调查和检查，投保人、被保险人、受益人等应当予以充分配合。</p>
        <p class="p">第二十六条
            保险金申请人向保险人请求给付保险金的诉讼时效期间为二年，自其知道或应当知道保险事故发生之日起计算。</p>
        <h2 class="h2"><em>保险合同的变更与解除</em></h2>
        <p class="p">第二十七条
            除本合同另有约定外，经投保人、保险人双方协商同意后，在不违反法律和监管规定的情况下，可变更本合同的有关内容，在本合同保险单或其他保险凭证上加以批注或附贴批单后生效，或由投保人和保险人订立合同变更书面协议后生效。</p>
        <p class="p">第二十八条
            在本合同成立后，投保人可以书面形式通知保险人解除本合同。保险人根据本合同的约定已给付保险金，或已发生本合同约定的保险事故但是尚未给付保险金的，投保人不得要求解除本合同，但是受益人放弃保险金请求权的不在此限。投保人解除本合同时，应当提供下列证明和资料：</p>
        <p class="p">（一）解除合同通知书；</p>
        <p class="p">（二）保险单或其他保险凭证、批单；</p>
        <p class="p">（三）投保人身份证明；</p>
        <p class="p">（四）保险费发票或收据；</p>
        <p class="p">（五）保险人合理要求的其他有关证明和资料。</p>
        <p class="p">
            本合同的效力至保险人接到解除合同通知书之日二十四时或通知书上载明的合同终止时间（以较晚者为准）终止。自收到前款约定的证明和资料之日起三十日内，保险人退还现金价值。</p>
        <h2 class="h2"><em>争议处理与法律适用</em></h2>
        <p class="p">第二十九条
            因履行本合同发生的争议，由当事人协商解决。协商不成的，提交本合同载明的仲裁机构仲裁；本合同未载明仲裁机构或争议发生后未达成仲裁协议的，依法向人民法院起诉。</p>
        <p class="p">第三十条
            与本合同有关的以及履行本合同产生的一切争议处理适用中华人民共和国法律（不包括港澳台地区法律）。</p>
        <h2 class="h2"><em>释义</em></h2>
        <p class="p">周岁：以法定身份证明文件中记载的出生日期为基础计算的实足年龄。</p>
        <p class="p">意外伤害：指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。</p>
        <p class="p">等待期：指自保险期间开始之日起计算的一段时间，在等待期内发生事故的,保险人不承担给付保险金的责任。</p>
        <p class="p">
            保险人指定或认可的医疗机构：包括保险人指定医疗机构，以及符合下列条件的医疗机构（除本合同另有约定外）：中华人民共和国境内（不包括港澳台地区）三级公立医院，具有符合有关医院管理规定设置标准的医疗设备，并且有合格医师及护士提供二十四小时的医疗和护理等服务的机构，但不包括主要为康复、护理、疗养、戒酒、戒毒或相类似目的的医疗机构。</p>
        <p class="p">
            住院：指入住保险人指定或认可的医疗机构的正式病房接受全日二十四小时监护治疗，并办理入出院手续，不包括门（急）诊观察室诊疗、家庭病床、挂床住院、其他非正式的病床住院及不合理住院。被保险人因非医疗目的自行离开病房十二小时以上（含）的，视为自动出院。</p>
        <p class="p">
            每次实际住院日数：指自该次住院的入院之日起至出院前一日止期间的日数，不包括未经办理出院手续擅自离院的日数。</p>
        <p class="p">既往症：指在本合同生效前罹患的被保险人已知或应该知道的有关疾病。通常有以下情况： </p>
        <p class="p">（一）本合同生效前，医生已有明确诊断，长期治疗未间断；</p>
        <p class="p">（二）本合同生效前，医生已有明确诊断，治疗后症状未完全消失，有间断用药情况；</p>
        <p class="p">（三）本合同生效前，未经医生诊断和治疗，但症状或体征明显且持续存在，以普通人医学常识应当知晓。</p>
        <p class="p">
            并发症：一种是指一种疾病在发展过程中引起另一种疾病或症状的发生，后者即为前者的并发症；另一种并发症是指在诊疗护理过程中，病人由患一种疾病合并发生了与这种疾病有关的另一种或几种疾病。</p>
        <p class="p">
            遗传性疾病：指生殖细胞或受精卵的遗传物质（染色体和基因）发生突变或畸变所引起的疾病，通常具有由亲代传至后代的垂直传递的特征。许多遗传性疾病在出生时并未显现。</p>
        <p class="p">
            先天性畸形、变形或染色体异常：指被保险人出生时就具有的畸形、变形或染色体异常。先天性畸形、变形或染色体异常确定依照世界卫生组织《疾病和有关健康问题的国际分类》（ICD－10）确定。先天性畸形、变形或染色体异常可能来自于父母遗传，或因胎儿在子宫内时受到伤害或感染，或因胎儿在出生时发生异常或受到伤害。</p>
        <p class="p">潜水：指以辅助呼吸器材在江、河、湖、海、水库、运河等水域进行的水下活动。</p>
        <p class="p">攀岩：指攀登悬崖、楼宇外墙、人造悬崖、冰崖、冰山等运动。 </p>
        <p class="p">
            武术比赛：指两人或两人以上对抗性柔道、空手道、跆拳道、散打、拳击等各种拳术及各种使用器械的对抗性比赛。</p>
        <p class="p">
            探险：指明知在某种特定的自然条件下有失去生命或使身体受到伤害的危险，而故意使自己置身其中的行为。如江河漂流、徒步穿越沙漠或人迹罕至的原始森林等活动。</p>
        <p class="p">特技：指从事马术、杂技、驯兽等方面的表演、运动或其他专门活动的特殊技能。</p>
        <p class="p">
            职业病：指在生产或劳动过程中，一种或几种对健康有害的因素引起的疾病。对健康有害的因素称为职业性危害。职业病的范围以事故发生时国家正式颁布的最新种类为准。</p>
        <p class="p">
            性传播疾病：指发生在生殖器官的内源性或外源性通过性行为或非性行为传播的传播性疾病，包括但不限于梅毒、淋病、尖锐湿疣、疱疹、软下疳、淋巴肉牙肿、非淋菌性尿道炎（包括支原体、衣原体阳性）。</p>
        <p class="p">
            医疗事故：指医疗机构及其医务人员在医疗活动中，违反医疗卫生管理法律、行政法规、部门规章和诊疗护理规范、常规，过失造成患者人身伤害的事故。</p>
        <p class="p">慢性病：指满足下列条件之一的伤害、疾病或症状：</p>
        <p class="p">（一）持续接受三个月以上的医学必需的治疗；</p>
        <p class="p">（二）预期病程长久且无可合理预计的康复日期，可能复发、需要连续或定期护理。</p>
        <p class="p">
            法定传染病：指《中华人民共和国传染病防治法》列明的甲、乙、丙三类传染病，包括该法未列明但在发生后被国家有关部门依法认定为法定传染病的疾病。</p>
        <p class="p">恐怖活动：指恐怖主义性质的下列行为：</p>
        <p class="p">
            （一）组织、策划、准备实施、实施造成或意图造成人员伤亡、重大财产损失、公共设施损坏、社会秩序混乱等严重社会危害的活动的；</p>
        <p class="p">
            （二）宣扬恐怖主义，煽动实施恐怖活动，或非法持有宣扬恐怖主义的物品，强制他人在公共场所穿戴宣扬恐怖主义的服饰、标志的；</p>
        <p class="p">（三）组织、领导、参加恐怖活动组织的；</p>
        <p class="p">
            （四）为恐怖活动组织、恐怖活动人员、实施恐怖活动或恐怖活动培训提供信息、资金、物资、劳务、技术、场所等支持、协助、便利的；</p>
        <p class="p">（五）其他恐怖活动。</p>
        <p class="p">
            感染艾滋病病毒或患有艾滋病：在人体血液或其它样本中检测到艾滋病病毒或其抗体呈阳性，没有出现临床症状或体征的，为感染艾滋病病毒；如果同时出现了明显临床症状或体征的，为患有艾滋病。艾滋病病毒指人类免疫缺陷病毒，英文缩写为HIV。艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合征，英文缩写为AIDS。</p>
        <p class="p">
            毒品：指中华人民共和国刑法规定的鸦片、海洛因、甲基苯丙胺（冰毒）、吗啡、大麻、可卡因以及国家规定管制的其他能够使人形成瘾癖的麻醉药品和精神药品。</p>
        <p class="p">
            管制药品：指根据《中华人民共和国药品管理法》及有关法规被列为特殊管理的药品，包括麻醉药品、精神药品、毒性药品及放射性药品。</p>
        <p class="p">
            酒后驾驶：指经检测或鉴定，发生事故时车辆驾驶人员每百毫升血液中的酒精含量达到或超过道路交通法规规定的标准。</p>
        <p class="p">无合法有效驾驶证驾驶：包括下列任何情形：</p>
        <p class="p">（一）无驾驶证驾驶或持有效期已届满的驾驶证驾驶；</p>
        <p class="p">（二）驾驶的机动交通工具与驾驶证载明的准驾车型不符；</p>
        <p class="p">
            （三）实习期内驾驶公共汽车、营运客车或载有爆炸物品、易燃易爆化学物品、剧毒或放射性物品等危险物品的机动交通工具，实习期内驾驶机动交通工具牵引挂车；</p>
        <p class="p">（四）持未按规定审验的驾驶证驾驶，以及驾驶证被暂扣、扣留、吊销或注销期间驾驶机动交通工具；</p>
        <p class="p">
            （五）使用各种专用机械车、特种车的人员无国家有关部门核发的有效操作证，驾驶营业性客车的驾驶人无国家有关部门核发的有效资格证书；</p>
        <p class="p">
            （六）在依照法律法规或公安机关交通管理部门有关规定不允许驾驶机动交通工具的其它情况下驾驶机动交通工具。</p>
        <p class="p">无合法有效行驶证：包括下列任何情形：</p>
        <p class="p">（一）机动交通工具被依法注销登记；</p>
        <p class="p">（二）机动交通工具无公安机关交通管理部门核发的行驶证、号牌，或临时号牌或临时移动证；</p>
        <p class="p">
            （三）机动交通工具未在规定检验期限内进行安全技术检验或检验未通过，未依法按时进行或通过安全技术检验。</p>
        <p class="p">
            挂床：指住院过程中一日内未接受与入院诊断相关的检查和治疗或一日内住院不满二十四小时，遵医嘱到外院接受临时诊疗的不在此限。</p>
        <p class="p">不可抗力：指不能预见、不能避免并不能克服的客观情况。</p>
