    <p class="p"><em>（二）保险人有权以被保险人的名义，为保险人的利益自付费用向任何责任方提出索赔的要求；未经保险人书面同意， 被保险人不得接受责任方就有关损失作出的付款或赔偿安排或放弃对责任方的索赔权利，
      否则，由此引起的后果将由被保险人承担。</em></p>
    <p class="p">（三）在诉讼或处理索赔过程中， 保险人有权自行处理任何诉讼或解决任何索赔案件，被保险人有义务向保险人提供一切所需的资料和协助。</p>
    <p class="p"><em>第二十一条</em>索赔欺诈</p>
    <p class="p"><em>（一）未发生保险事故，被保险人谎称发生了保险事故，向保险人提出赔偿保险金请求的，保险人有权解除合同， 并不退还保险费。</em></p>
    <p class="p">（二）投保人、被保险人故意制造保险事故的， 保险人有权解除合同， 不承担赔偿保险金的责任，也不退还保险费。</p>
    <p class="p"><em>（三）保险事故发生后， 投保人、被保险人以伪造、变造的有关证明、资料或者其他证据， 编造虚假的事故原因或者夸大损失程度的， 保险人对其虚报的部分不承担赔偿保险金的责任。</em></p>
    <p class="p"><em>投保人、被保险人上述规定行为之一， 致使保险人支付保险金或者支出费用的， 应当退回或者赔偿。</em></p>
