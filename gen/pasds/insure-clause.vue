    <p class="p">被保险人已经从有关责任方取得赔偿的， 保险人赔偿保险金时， 可以相应扣减</p>
    <p class="p"><em>被保险人已从有关责任方取得的赔偿金额。</em></p>
    <p class="p"><em>保险事故发生后， 在保险人未赔偿保险金之前， 被保险人放弃对有关责任方请求赔偿权利的， 保险人不承担赔偿责任； 保险人向被保险人赔偿保险金后， 被保险人未经保险人同意放弃对有关责任方请求赔偿权利的，
      该行为无效； 由于被保险人故意或者因重大过失致使保险人不能行使代位请求赔偿的权利的， 保险人可以扣减</em></p>
    <p class="p"><em>或者要求返还相应的保险金。第二十三条 </em>重复保险</p>
    <p class="p"><em>本保险合同负责赔偿损失、费用或责任时，若另有其他保障相同的保险存在，不论是否由被保险人或他人以其名义投保，也不论该保险赔偿与否， 保险人仅负责按本保险金额与保险金额总和的比例分摊赔偿责任。</em>
    </p>
    <h2 class="h2">争议处理和法律适用</h2>
