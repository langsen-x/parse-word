      <p class="p"><em>第二条 </em>除本保险合同另有约定外，凡投保时年龄在0周岁<em>（见释义1）</em>（<em>投保时被保险人为0周岁的，应当为出生满30日且已健康出院的婴儿</em>）至65周岁，身体健康、能正常工作或生活的自然人可作为本保险合同的被保险人。
