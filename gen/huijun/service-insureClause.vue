      <h2 class="h2"><em>释义</em></h2>
      <p class="p"><em>1、周岁：</em>指按有效身份证件中记载的出生日期计算的年龄，自出生之日起为零周岁，每经过一年增加一岁，不足一年的不计。</p>
      <p class="p"><em>2、意外伤害：</em>指以外来的、突发的、非本意的、非疾病的客观事件为直接且单独的原因致使身体受到的伤害。<em>以下情形属于疾病范畴，非本条款所指意外伤害：</em></p>
      <p class="p"><em>（1）猝死：指由潜在疾病、身体机能障碍或其他非外来性原因所导致的、在出现急性症状后发生的突然死亡，以医院的诊断或公安、司法机关的鉴定为准；</em></p>
      <p class="p"><em>（2）过敏及由过敏引发的变态反应性疾病；</em></p>
      <p class="p"><em>（3）高原反应；</em></p>
      <p class="p"><em>（4）中暑；</em></p>
      <p class="p"><em>（5）细菌、病毒或其他病原体导致的感染性疾病。</em></p>
      <p class="p"><em>3、等待期：</em>指自保险合同生效之日起计算的一段时间，具体天数由保险人和投保人在投保时约定并在本保险合同上载明。<em>在等待期内发生保险事故的,保险人不承担给付保险金的责任。</em>
      </p>
      <p class="p"><em>4、医院：</em>是指经中华人民共和国卫生部门审核认定的二级或二级以上的公立医院，且仅限于上述医院的普通部，<em>不包括如下机构或医疗服务：</em></p>
      <p class="p"><em>（1）特需医疗、外宾医疗、干部病房、联合病房、国际医疗中心、VIP部、联合医院、A级病房；</em></p>
      <p class="p"><em>（2）诊所、康复中心、家庭病床、护理机构；</em></p>
      <p class="p"><em>（3）休养、戒酒、戒毒中心。</em></p>
      <p class="p"><em>该医院必须具有系统的、充分的诊断设备，全套外科手术设备及能够提供</em>二十四小时的医疗与护理服务的能力或资质。</p>
      <p class="p"><em>5、住院：</em>是指被保险人因意外伤害或疾病而入住医院或指定医疗机构的正式病房接受全日24小时监护治疗的过程，并正式办理入出院手续。<em>但不包括下列情况：</em></p>
      <p class="p"><em>（1）被保险人在医院或指定医疗机构的（门）急诊观察室、家庭病床（房）入住；</em></p>
      <p class="p"><em>（2）被保险人在特需病房、外宾病房或其它不属于基本医疗保险范畴的高等级病房入住；</em></p>
      <p class="p"><em>（3）被保险人入住康复科、康复病床或接受康复治疗；</em></p>
      <p class="p"><em>（4）被保险人住院期间一天内未接受与入院诊断相关的检查和治疗或一天内住院不满二十四小时,但遵医嘱到外院接受临时治疗的除外；</em></p>
      <p class="p"><em>（5）被保险人住院体检；</em></p>
      <p class="p"><em>（6）挂床住院及其他不合理的住院。挂床住院指办理正式住院手续的被保险人，在住院期间每日非24小时在院。具体表现包括在住院期间连续若干日无任何治疗，只发生护理费、诊疗费、床位费等情况。</em>
      </p>
      <p class="p"><em>6、必需且合理：指符合以下两个条件：</em></p>
      <p class="p"><em> （1）符合通常惯例：</em>指与接受医疗服务所在地通行治疗规范、通行治疗方法、平均医疗费用价格水平一致的费用。是否符合通常惯例由保险人根据客观、审慎、合理的原则进行审核；如果被保险人对审核结果有不同意见，可由双方认同的权威医学机构或者权威医学专家进行审核鉴定。
      </p>
      <p class="p"><em>（2）医学必要：</em>指被保险人接受治疗或服务、使用器械或服用药品符合以下条件：</p>
      <p class="p">a.医师处方要求且对治疗被保险人疾病或伤害合适且必需；</p>
      <p class="p">b.在范围、持续期、强度、护理上不超过为被保险人提供安全、恰当、合适的诊断或治疗所需的水平；</p>
      <p class="p">c.与接受治疗当地普遍接受的医疗专业实践标准一致；</p>
      <p class="p">d.非主要为了个人舒适或为了被保险人父母、家庭、医师或其他医疗提供方的方便；</p>
      <p class="p">e.非病人学术教育或职业培训的一部分或与之相关；</p>
      <p class="p">f.非试验性或研究性。</p>
      <p class="p">对是否医学必需由保险人根据客观、审慎、合理的原则进行审核；如果被保险人对审核结果有不同意见，可由双方认同的权威医学机构或者权威医学专家进行审核鉴定。</p>
      <p class="p"><em>7、住院医疗费用：</em></p>
      <p class="p">（1）床位费</p>
      <p class="p"><em>指住院期间实际发生的、不高于双人病房的住院床位费（不包括单人病房、套房、家庭病床）。</em></p>
      <p class="p">（2）加床费</p>
      <p class="p">
        指未满18周岁的被保险人在住院治疗期间，保险人根据合同约定给付其合法监护人（限一人）在合同约定的医院或指定医疗机构留宿发生的加床费；或女性被保险人在住院治疗期间，保险人根据合同约定给付其一周岁以下哺乳期婴儿在合同约定的医院或医疗机构留宿发生的加床费。</p>
      <p class="p">（3）重症监护室床位费</p>
      <p class="p">
        指住院期间出于医学必要被保险人需在重症监护室进行合理且必要的医疗而产生的床位费。重症监护室指配有中心监护台、心电监护仪及其他监护抢救设施，相对封闭管理，符合重症监护病房（ICU）、冠心病重症监护病房（CCU）标准的单人或多人监护病房。</p>
      <p class="p">（4）药品费</p>
      <p class="p"><em>指住院期间实际发生的合理</em>且必要的由医生开具的具有国家药品监督管理部门核发的药品批准文号或者进口药品注册证书、医药产品注册证书的国产或进口药品的费用。<em>但不包括营养补充类药品，免疫功能调节类药品，美容及减肥类药品，预防类药品，以及下列中药类药品：</em>
      </p>
      <p class="p"><em>a.主要起营养滋补作用的单方、复方中药或中成药品，如花旗参，冬虫草，白糖参，朝鲜红参，玳瑁，蛤蚧，珊瑚，狗宝、红参，琥珀，灵芝，羚羊角尖粉，马宝，玛瑙，牛黄，麝香，西红花，血竭，燕窝，野山参，移山参，珍珠（粉），紫河车，阿胶，血宝胶囊，红桃K口服液，十全大补丸，十全大补膏等；</em>
      </p>
      <p class="p"><em>b.部分可以入药的动物及动物脏器，如鹿茸，海马，胎盘，鞭，尾，筋，骨等；</em></p>
      <p class="p"><em>c.用中药材和中药饮片炮制的各类酒制剂等。</em></p>
      <p class="p">（5）膳食费</p>
      <p class="p">
        指住院期间根据医生的医嘱，由作为合同约定的医院或指定医疗机构内部专属部门的、为住院病人配餐的食堂配送的膳食费用。膳食费应包含在医疗账单内,根据各医院或指定医疗机构的惯例，可以作为独立的款项,也可以合并在病房费等其他款项内。</p>
      <p class="p">（6）治疗费</p>
      <p class="p"><em>指住院期间以治疗疾病为目的，提供必要的医学手段而发生的合理的治疗者的技术劳务费和医疗器械使用费，以及消耗品的费用，具体以就诊合同约定的医院或指定医疗机构的费用项目划分为准。本项责任不包含如下费用：物理治疗、中医理疗及其他特殊疗法费用。</em>
      </p>
      <p class="p">护理费</p>
      <p class="p">指住院期间根据医嘱所示的护理等级确定的护理费用。</p>
      <p class="p">（8）检查检验费</p>
      <p class="p">
        指住院期间实际发生的，以诊断疾病为目的，采取必要的医学手段进行检查及检验而发生的合理的医疗费用，包括X光费、心电图费、B超费、脑电图费、内窥镜费、肺功能仪费、分子生化检验费和血、尿、便常规检验费等。</p>
      <p class="p">（9）手术费</p>
      <p class="p">指当地卫生行政部门规定的手术项目的费用。包括手术费、麻醉费、手术监测费、手术材料费、术中用药费、手术设备费；<em>若因器官移植而发生的手术费用，不包括器官本身的费用和获取器官过程中的费用。</em>
      </p>
      <p class="p">（10）医生费</p>
      <p class="p">指包括外科医生、麻醉师、内科医生、专科医生的费用。</p>
      <p class="p">（11）救护车使用费</p>
      <p class="p">指住院期间以抢救生命或治疗疾病为目的，根据医生建议，被保险人需医院转诊过程中的医院用车费用，且<em>救护车的使用仅限于同一城市中的医疗运送。</em></p>
      <p class="p"><em>8、化学疗法：</em>指针对于恶性肿瘤的化学治疗。化疗是使用医学界公认的化疗药物以杀死癌细胞、抑制癌细胞生长繁殖为目的而进行的治疗。本保险合同所指的化疗为被保险人根据医嘱,在合同约定的医院或指定医疗机构进行的静脉注射化疗。
      </p>
      <p class="p"><em>9、放射疗法：</em>指针对恶性肿瘤的放射治疗。放疗是使用各种不同能量的射线照射肿瘤组织，以抑制和杀灭癌细胞为目的而进行的治疗。本附加条款所指的放疗为被保险人根据医嘱，在合同约定的医院或指定医疗机构的专门科室进行的放疗。
      </p>
      <p class="p"><em>10、肿瘤免疫疗法：</em>指应用免疫学原理和方法，使用肿瘤免疫治疗药物提高肿瘤细胞的免疫原性和对效应细胞杀伤的敏感性，激发和增强机体抗肿瘤免疫应答，并应用免疫细胞和效应分子输注宿主体内，协同机体免疫系统杀伤肿瘤、抑制肿瘤生长。本保险合同所指的肿瘤免疫治疗药物需符合法律、法规要求并经过国家食品药品监督管理总局批准用于临床治疗。
      </p>
      <p class="p"><em>11、肿瘤内分泌疗法：</em>指针对于恶性肿瘤的内分泌疗法，用药物抑制激素生成和激素反应，杀死癌细胞或抑制癌细胞的生长。本保险合同所指的内分泌治疗药物需符合法律、法规要求并经过国家食品药品监督管理总局批准用于临床治疗。
      </p>
      <p class="p"><em>12、肿瘤靶向疗法：</em>指在细胞分子水平上，针对已经明确的致癌点来设计相应的靶向治疗药物，利用具有一定特异性的载体，将药物或其他杀伤肿瘤细胞的活性物质选择性地运送到肿瘤部位攻击癌细胞的疗法。本保险合同所指的靶向治疗的药物需具有国家药品监督管理部门核发的药品批准文号或者进口药品注册证书、医药产品注册证书。
      </p>
      <p class="p"><em>13、专科医生：</em>专科医生应当同时满足以下四项资格条件：</p>
      <p class="p">（1）具有有效的中华人民共和国《医师资格证书》；</p>
      <p class="p">（2）具有有效的中华人民共和国《医师执业证书》，并按期到相关部门登记注册；</p>
      <p class="p">（3）具有有效的中华人民共和国主治医师或主治医师以上职称的《医师职称证书》；</p>
      <p class="p">（4）在二级或二级以上合同约定的医院或指定医疗机构的相应科室从事临床工作三年以上。</p>
      <p class="p">14、初次确诊：指自被保险人出生之日起第一次经医院或指定医疗机构确诊患有某种疾病，而不是指自本保险合同生效之后第一次经医院或指定医疗机构确诊患有某种疾病。</p>
      <p class="p"><em>15、恶性肿瘤——重度</em></p>
      <p class="p"><em>指恶性细胞不受控制的进行性增长和扩散，浸润和破坏周围正常组织，可以经血管、淋巴管和体腔扩散转移到身体</em>其他部位，病灶经<em>组织病理学检查（见释义36）</em>（涵盖骨髓病理学检查）结果明确诊断，临床诊断属于世界卫生组织（WHO，World
        Health Organization）<em>《疾病和有关健康问题的国际统计分类》第十次修订版（ICD-10）的恶性肿瘤类别及《国际疾病分类肿瘤学专辑》第三版（ICD-O-3）的肿瘤形态学编码属于
          3、6、9（恶性肿瘤）范畴的疾病（释义37）</em>，<em>下列疾病不属于“恶性肿瘤——重度”，不在保障范围内：</em></p>
      <p class="p"><em>（1）ICD-O-3 肿瘤形态学编码属于 0（良性肿瘤）、1（动态未定性肿瘤）、2（原位癌和非侵袭性癌）范畴的疾病，如： </em></p>
      <p class="p"><em>a.原位癌，癌前病变，非浸润性癌，非侵袭性癌，肿瘤细胞未侵犯基底层，上皮内瘤变，细胞不典型性增生等； </em></p>
      <p class="p"><em>b.交界性肿瘤，交界恶性肿瘤，肿瘤低度恶性潜能，潜在低度恶性肿瘤等； </em></p>
      <p class="p"><em>（2）TNM 分期（释义38）为Ⅰ期或更轻分期的甲状腺癌（释义39）； </em></p>
      <p class="p"><em>（3）TNM 分期为 T1N0M0期或更轻分期的前列腺癌； </em></p>
      <p class="p"><em>（4）黑色素瘤以外的未发生淋巴结和远处转移的皮肤恶性肿瘤；</em></p>
      <p class="p"><em>（5）相当于 Binet 分期方案 A 期程度的慢性淋巴细胞白血病； </em></p>
