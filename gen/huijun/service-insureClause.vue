        和“恶性肿瘤异地转诊公共交通费用保险金”由投保人和保险人按照以下情况分别约定给付标准，并在保险单中载明：</em></p>
      <p class="p"><em>1、一般医疗保险金及恶性肿瘤医疗保险金：</em></p>
      <p class="p"><em>（1）若被保险人以参加基本医疗保险身份投保，并以基本医疗保险身份就诊并结算，赔付比例为100%。</em></p>
      <p class="p"><em>（2）若被保险人以参加基本医疗保险身份投保，但未以基本医疗保险身份就诊并结算，赔付比例为60%。</em></p>
      <p class="p"><em>（3）若被保险人未以参加基本医疗保险身份投保，赔付比例为100%。</em></p>
      <p class="p"><em>2、质子重离子医疗保险金：赔付比例为100%。</em></p>
      <p class="p"><em>3、恶性肿瘤住院津贴保险金：</em></p>
      <p class="p"><em>按照100元/天给付恶性肿瘤住院津贴，无免赔天数。单次最高给付不超过90天，全年累计给付不超过180天。</em></p>
      <p class="p"><em>4、恶性肿瘤异地转诊公共交通费用保险金：</em></p>
      <p class="p"><em>赔付比例为100%。</em></p>
      <h2 class="h2"><em>责任免除</em></h2>
      <p class="p"><em>第七条 因下列情形之一造成被保险人医疗费用支出的，保险人不承担给付保险金的责任：</em></p>
      <p class="p"><em>（一）被保险人在初次投保前所患既往症（见释义19）；投保前出现的疾病；</em></p>
      <p class="p"><em>（二）遗传性疾病（见释义20），先天性畸形、变形或染色体异常（见释义21）（以世界卫生组织颁布的《疾病和有关健康问题的国际统计分类（ICD－10）》为准；</em></p>
      <p class="p"><em>（三）疗养、视力矫正手术、各种健康体检项目及预防性医疗项目、牙科保健及牙科治疗、康复治疗、非意外事故所致整容手术；</em></p>
      <p class="p"><em>（四）皮肤色素沉着、痤疮治疗、红斑痤疮治疗；雀斑、老年斑、痣的治疗和去除；对浅表静脉曲张、蜘蛛脉、除瘢痕疙瘩型外的其它瘢痕、纹身去除、皮肤变色的治疗或手术；激光美容、除皱、除眼袋、开双眼皮、治疗斑秃、白发、秃发、脱发、植毛、脱毛、隆鼻、隆胸；</em>
      </p>
      <p class="p"><em>（五）各种矫形及生理缺陷的手术和检查治疗项目，包括但不限于平足及各种非功能性整容、矫形手术费用；</em></p>
      <p class="p"><em>（六）各种健美治疗项目，包括但不限于营养、减肥、增胖、增高费用；</em></p>
      <p class="p"><em>（七）不孕不育治疗、人工受精、怀孕、分娩（含难产）、流产、堕胎、节育（含绝育）、产前产后检查以及由以上原因引起的并发症；</em></p>
      <p class="p"><em>（八）包皮环切术、包皮剥离术、包皮气囊扩张术、性功能障碍治疗；</em></p>
      <p class="p"><em>（九）除心脏瓣膜、人工晶体、人工关节之外的其他人工器官材料费、安装和置换等费用、各种康复治疗器械、假体、义肢、自用的按摩保健和治疗用品、所有非处方医疗器械；</em></p>
      <p class="p"><em>（十）耐用医疗设备（指各种康复设备、矫形支具以及其他耐用医疗设备）的购买或租赁费用；</em></p>
      <p class="p"><em>（十一）未经医生处方自行购买的药品或在非合同约定的医院或指定医疗机构药房或指定药房购买的药品、滋补类中草药及其泡制的各类酒制剂、医生开具的超过30天部分的药品费用；</em></p>
      <p class="p"><em>（十二）各种医疗咨询和健康预测，如健康咨询、睡眠咨询、性咨询、心理咨询（依照世界卫生组织《疾病和有关健康问题的国际统计分类》（ICD-10）确定的精神和行为障碍以外的一般心理问题，如职场问题、家庭问题、婚恋问题、个人发展、情绪管理等）等费用；</em>
      </p>
      <p class="p"><em>（十三）投保人对被保险人的故意杀害、故意伤害；</em></p>
      <p class="p"><em>（十四）被保险人故意自伤、故意犯罪或抗拒依法采取的刑事强制措施；</em></p>
      <p class="p"><em>（十五）被保险人殴斗、醉酒（见释义22），主动吸食或注射毒品（见释义23）；</em></p>
      <p class="p"><em>（十六）被保险人酒后驾驶（见释义24）、无合法有效驾驶证（见释义25）驾驶或驾驶无有效行驶证照（见释义26）的机动车（见释义27）导致交通意外引起的医疗费用；</em></p>
      <p class="p"><em>（十七）从事潜水、跳伞、攀岩、蹦极、驾驶滑翔机或滑翔伞、探险、武术比赛、摔跤比赛、特技表演、赛马、赛车等高风险运动（见释义28）导致的伤害引起的治疗；</em></p>
      <p class="p"><em>（十八）由于职业病（见释义29）、医疗事故（见释义30）引起的医疗费用；</em></p>
      <p class="p"><em>（十九）核爆炸、核辐射或核污染、化学污染；恐怖袭击、战争、军事冲突、暴乱或武装叛乱；</em></p>
      <p class="p"><em>（二十）不符合入院标准、挂床住院或住院病人应当出院而拒不出院（从合同约定的医院或指定医疗机构确定出院之日起发生的一切医疗费用）；</em></p>
      <p class="p"><em>（二十一）被保险人接受实验性治疗，即未经科学或医学认可的医疗；</em></p>
      <p class="p"><em>（二十二）未被治疗所在地权威部门批准的治疗，未获得治疗所在地政府许可或批准的药品或药物；</em></p>
      <p class="p"><em>（二十三）各类医疗鉴定，包括但不限于医疗事故鉴定、精神病鉴定、孕妇胎儿性别鉴定、验伤鉴定、亲子鉴定、遗传基因鉴定费用；</em></p>
      <p class="p"><em>（二十四）被保险人存在精神和行为障碍（以世界卫生组织颁布的《疾病和有关健康问题的国际统计分类（ICD-10）》为准）；</em></p>
      <p class="p"><em>（二十五）被保险人非因职业原因或器官移植原因感染艾滋病病毒或患艾滋病（见释义31）引起的治疗；</em></p>
      <p class="p"><em>（二十六）被保险人患性病引起的医疗费用。</em></p>
      <h2 class="h2"><em>保险金额</em></h2>
      <p class="p"><em>第八条 </em>保险金额是保险人承担赔偿或者给付保险金责任的最高限额。</p>
      <p class="p"><em>本保险合同的保险金额包括“一般医疗保险金额”、“恶性肿瘤医疗保险金额”、“质子重离子医疗保险金额”、“恶性肿瘤住院津贴保险金额”和“恶性肿瘤异地转诊公共交通费用保险金额”。以上每项责任的保险金额由投保人与保险人在订立本保险合同时协商确定，并在保险单中载明。</em>
      </p>
      <h2 class="h2"><em>保险期间</em></h2>
      <p class="p"><em>第九条</em> 本保险合同的保险期间为一年，具体由投保人和保险人在订立本保险合同时协商确定，以保险单上载明的起讫时间为准。</p>
      <p class="p"><em>不保证续保合同</em></p>
      <p class="p"><em>第十条</em> <em>本保险合同为不保证续保合同。本保险合同保险期间为一年。保险期间届满，投保人需要重新向保险人申请投保本保险合同，并经保险人同意，交纳保险费，获得新的保险合同。</em>如果投保人未按照约定重新向保险人申请投保本保险合同并交纳保险费，或保险人审核不同意重新投保的，则本保险合同在保险期间届满时终止。
        当投保人重新向保险人申请投保时，被保险人的年龄可至100周岁。</p>
      <h2 class="h2"><em>保险人义务</em></h2>
      <p class="p"><em>第十一条 </em>订立保险合同时，采用保险人提供的格式条款的，保险人向投保人提供的投保单应当附格式条款，保险人应当向投保人说明保险合同的内容。对保险合同中免除保险人责任的条款，保险人在订立合同时应当在投保单、保险单或者其他保险凭证上作出足以引起投保人注意的提示，并对该条款的内容以书面或者口头形式向投保人作出明确说明；未作提示或者明确说明的，该条款不产生效力。
      </p>
      <p class="p"><em>第十二条 </em>本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</p>
      <p class="p"><em>第十三条 </em>保险人依据第十九条、第二十条所取得的保险合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。自保险合同成立之日起超过二年的，保险人不得解除合同；发生保险事故的，保险人承担给付保险金责任。
      </p>
      <p class="p"><em>保险人在合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</em></p>
      <p class="p"><em>第十四条 </em>保险人按照第二十四条的约定，认为投保人、被保险人或者受益人提供的有关索赔的证明和资料不完整的，应当及时一次性通知投保人、被保险人或者受益人补充提供。</p>
      <p class="p"><em>第十五条 </em>保险人收到被保险人或者受益人的给付保险金的请求后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在三十日内作出核定。但保险责任的核定必须依赖于特定证明、鉴定、判决、裁定或其他证据材料的，保险人应在被保险人或者受益人提供或自行取得上述证据材料起三十日内作出核定。<em>本保险合同另有约定的除外。</em>
      </p>
      <p class="p">
        保险人应当将核定结果通知被保险人或者受益人；对属于保险责任的，在与被保险人或者受益人达成给付保险金的协议后十日内，履行赔偿保险金义务。保险合同对给付保险金的期限有约定的，保险人应当按照约定履行给付保险金的义务。保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人或者受益人发出拒绝给付保险金通知书，并说明理由。</p>
      <p class="p"><em>第十六条 </em>保险人自收到给付保险金的请求和有关证明、资料之日起六十日内，对其赔偿保险金的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定给付的数额后，应当支付相应的差额。
      </p>
      <p class="p"><em>第十七条 </em>保险人有权对理赔进行核查，投保人和被保险人有义务提供保险人所要求的相关材料。如果受益人向保险人提起虚假的保险金申请，保险人有权追回已支付的相应保险金，并对其它虚假理赔的申请且尚未支付的款项拒绝支付，并有权解除本保险合同。
      </p>
      <h2 class="h2"><em>投保人、被保险人义务</em></h2>
      <p class="p"><em>第十八条 </em>投保人应该按照本保险合同约定向保险人交纳保险费。</p>
      <p class="p"><em>本</em>保险合同保费支付方式分为一次性支付保险费和分期支付保险费两种方式，由投保人、保险人在订立本保险合同时协商确定，并在保险单中载明。</p>
      <p class="p"><em>约定一次性缴付保险费的，投保人应当在本保险合同成立时一次性缴清保险费。保险费缴清前，本保险合同不生效。 </em></p>
      <p class="p"><em>约定分期支付保险费的，在交纳首期保险费后，投保人应当在每个保险费约定支付日交纳其余各期对应保险费。若投保人未按照保险合同约定的付款时间足额缴付当期保险费的，保险人允许投保人在约定缴费延长期内补交对应期次的保险费。若投保人未按照保险合同约定的付款时间足额缴付当期保费，且在缴费延长期截止日仍未足额补交当期保险费的，保险期间终止在上一缴费周期的止期，对于保险期间终止后发生的保险事故，保险人不承担保险责任。</em>
      </p>
      <p class="p"><em>若被保险人在缴费延长期内发生保险事故，保险人仍按照本保险合同约定给付相应保险金。但需扣减保险期间内所有剩余未缴期间的保险费。</em></p>
      <p class="p"><em>缴费延长期由投保人与保险人协商确定，并在保险单中载明。</em></p>
      <p class="p"><em>第十九条 </em>订立保险合同，保险人就被保险人的有关情况提出询问的，投保人应当如实告知。</p>
      <p class="p"><em>投保人故意或者因重大过失未履行前款规定的义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除本保险合同。</em></p>
      <p class="p"><em>投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，并不退还保险费。</em></p>
      <p class="p"><em>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，但应当退还保险费。</em></p>
      <p class="p"><em>第二十条 </em>投保人申请投保时，应按被保险人的周岁年龄和性别填写，如果发生错误按照下列方式办理：</p>
      <p class="p"><em>（一）投保人申报的被保险人年龄不真实，并且其真实年龄不符合本保险合同约定投保年龄限制的，保险人有权解除本保险合同，并向投保人退还未满期净保费（见释义32）。对于本保险合同解除前发生的保险事故，保险人不承担给付保险金的责任。</em>
      </p>
      <p class="p"><em>（二）投保人申报的被保险人年龄或者性别不真实，致使投保人实交保险费少于应交保险费的，保险人有权更正并要求投保人补交保险费。如果已经发生保险事故，在给付保险金时按实际缴纳保险费和应交保险费的比例给付。</em>
      </p>
      <p class="p"><em>（三）</em>投保人申报的被保险人年龄或者性别不真实，致使投保人实交保险费多于应交保险费的，保险人会将多收的保险费无息退还给投保人。</p>
      <p class="p"><em>第二十一条 </em>投保人住所或通讯地址变更时，应及时以书面形式通知保险人。<em>投保人未通知的，保险人按本保险合同所载的最后住所或通讯地址发送的有关通知，均视为已发送给投保人。</em>
      </p>
      <p class="p"><em>第二十二条
        被保险人变更其职业或工种时，投保人应于三十日内以书面形式通知保险人。若被保险人所变更的职业或者工种依照保险人职业分类在拒保范围内的，保险人对该被保险人所负保险责任自其职业或工种变更之日起终止，并退还未满期净保费。</em>
      </p>
      <p class="p"><em>被保险人未履行本条约定的通知义务而发生保险事故的，且被保险人所变更的职业或者工种依照保险人职业分类在拒保范围内的，保险人不承担给付保险金的责任</em>，并自其职业或工种变更之日起本保险合同终止，保险人退还未满期净保费，。
      </p>
      <p class="p"><em>第二十三条 </em>投保人、被保险人或者保险金受益人知道保险事故发生后，应当及时通知保险人。<em>故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金责任，</em>但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。
      </p>
      <p class="p">上述约定，不包括因<em>不可抗力（释义33）</em>释义而导致的迟延。</p>
      <h2 class="h2"><em>保险金申请与给付</em></h2>
      <p class="p"><em>第二十四条 保险金申请人（释义34）</em>请求赔偿时，应向保险人提供以下证明和资料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。<em>保险金申请人未能提供有关材料，导致保险人无法核实的，保险人对无法核实部分不承担给付保险金的责任。</em>
      </p>
      <p class="p">（一）保险金申请人填具的索赔申请书；</p>
      <p class="p">（二）保险单或其他保险凭证正本；</p>
      <p class="p">（三）保险金申请人的<em>有效身份证件（释义35）</em>；</p>
      <p class="p">（四）合同约定的医院或指定医疗机构出具的完整病历资料（包括门急诊病历、住院病历、出院小结以及相关的检查报告）；</p>
      <p class="p">（五）合同约定的医院或指定医疗机构出具的附有病历、病理检验、血液检验、影像学报告及其他科学方法检验报告的疾病诊断证明书；</p>
      <p class="p">
        （六）医疗费用原始收据、医疗费用明细清单及医疗费用分割单（若发生手术费用，还需提供手术费用的原始凭证，被保险人享有社会基本医疗保险或公费医疗保障的，需包含社会基本医疗保险或公费医疗有关规定取得医疗费用补偿证明）；</p>
      <p class="p">
        （七）被保险人罹患本保险合同约定的恶性肿瘤，还应提供由医院或指定医疗机构具有相应资质的医生出具的对被保险人的疾病诊断证明书以及由医院或指定医疗机构出具的与该疾病诊断证明书相关的病理显微镜检查、血液检验及其他科学方法检验报告；</p>
      <p class="p">（八）首次申请理赔时，应提供合同约定的医院或指定医疗机构出具的被保险人在本保险合同保险期间内历次的医疗诊断证明、病历及医疗、医药费原始单据、结算明细表与处方正本；</p>
      <p class="p">（九）恶性肿瘤异地转诊报销公共交通及救护车费用需提供转出医院出具的转院证明、交通费用支出的正式发票或收据原件。</p>
      <p class="p">（十）保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；</p>
      <p class="p">（十一）受益人为无民事行为能力人或者限制民事行为能力人的，由其监护人代为申领保险金，并需要提供监护人的身份证明等资料。</p>
      <p class="p"><em>第二十五条 </em>保险金申请人向保险人请求给付保险金的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</p>
