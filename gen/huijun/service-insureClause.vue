      <h2 class="h2"><em>保险责任</em></h2>
      <p class="p"><em>第六条 </em>本保险合同的保险责任包括“一般医疗保险金”、“恶性肿瘤医疗保险金”、“质子重离子医疗保险金” “恶性肿瘤住院津贴保险金” 和“恶性肿瘤异地转诊公共交通费用保险金”。</p>
      <p class="p"><em>（一）一般医疗保险金</em></p>
      <p class="p">
        在保险期间内，被保险人因遭受<em>意外伤害（见释义2）</em>事故或在<em>等待期（见释义3）</em>后因患疾病，在中华人民共和国境内（<em>不含香港特别行政区、澳门特别行政区和台湾地区</em>）经中华人民共和国卫生部门评审确定的二级或二级以上的公立<em>医院（释义4）</em>普通部或保险人认可的医疗机构接受治疗的，保险人对下述1-4类费用，按照本保险合同的约定承担给付一般医疗保险金责任：
      </p>
      <p class="p"><em>1.住院医疗费用</em></p>
      <p class="p">被保险人经医院诊断必须接受<em>住院（见释义5）</em>治疗的，对于住院期间发生的<em>必需且合理（见释义6）</em>的<em>住院医疗费用（见释义7）</em>，<em>保险人按照本保险合同的约定，在一般医疗保险金额内给付住院医疗保险金。</em>
      </p>
      <p class="p">
        到本保险合同满期日时，被保险人未结束本次住院治疗的，保险人继续承担因本次住院发生的、最高不超过本保险合同满期日后30日内的必需且合理的住院医疗费用，保险人按照本保险合同的约定，在保险单载明的一般医疗保险金额内给付住院医疗保险金。</p>
      <p class="p"><em>2.特殊门诊医疗费用</em></p>
      <p class="p">被保险人在<em>医院</em>接受特殊门诊（不含特需门诊）治疗期间发生的以下必需且合理的特殊门诊医疗费用，<em>保险人按照本保险合同的约定，在</em>保险单载明的<em>一般医疗保险金额内给付特殊门诊医疗保险金：</em>
      </p>
      <p class="p"><em>（1）门诊肾透析费；</em></p>
      <p class="p"><em>（2）门诊恶性肿瘤治疗费，包括化学疗法（见释义8）、放射疗法（见释义9）、肿瘤免疫疗法（见释义10）、肿瘤内分泌疗法（见释义11）、肿瘤靶向疗法（见释义12）的治疗费用；</em></p>
      <p class="p"><em>（3）器官移植后的门诊抗排异治疗费；</em></p>
      <p class="p"><em>3.门诊手术医疗费用</em></p>
      <p class="p">被保险人在医院接受门诊手术治疗期间发生的必需且合理的门诊手术费用，<em>保险人按照本保险合同的约定，在</em>保险单载明的<em>一般医疗保险金额内给付门诊手术医疗保险金。</em></p>
      <p class="p"><em>4.住院前后门诊急诊费用</em></p>
      <p class="p">被保险人在住院前7日（含住院当日）和出院后30日（含出院当日）内，因与该次住院相同原因而接受门急诊治疗发生的必需且合理的门诊急诊医疗费<em>（不包括特殊门诊医疗费用、门诊手术医疗费用），保险人按照本保险合同的约定，在</em>保险单载明的<em>一般医疗保险金额内给付住院前后门诊急诊保险金。</em>
      </p>
      <p class="p">
        <em>保险人对于以上4类费用的累计赔偿金额之和以保险单载明的一般医疗保险金的保险金额为限，一次或累计赔偿金额达到保险单载明的一般医疗保险金额时，保险人对于被保险人在一般医疗保险金项下的保险责任终止。</em></p>
      <p class="p"><em>（二）恶性肿瘤医疗保险金</em></p>
      <p class="p">
        在保险期间内，被保险人<em>等待期</em>后经医院<em>专科医生（见释义13）初次确诊（见释义14）</em>罹患本保险合同所定义的<em>恶性肿瘤（见释义15的“恶性肿瘤——重度”定义）</em>，在中华人民共和国境内（<em>不含香港特别行政区、澳门特别行政区和台湾地区</em>）经中华人民共和国卫生部门评审确定的二级或二级以上的公立医院普通部或保险人认可的医疗机构接受治疗的，保险人首先按照本条第（一）款的约定给付一般医疗保险金，当保险人累计给付金额达到一般医疗保险金的保险金额后，保险人将按照以下约定给付恶性肿瘤医疗保险金：
      </p>
      <p class="p"><em>1.恶性肿瘤住院医疗费用</em></p>
      <p class="p">
        被保险人因初次确诊罹患恶性肿瘤经合同约定的医疗机构诊断必须住院治疗的，对于住院期间发生的必需且合理的恶性肿瘤住院医疗费用，保险人按照本保险合同的约定，在恶性肿瘤医疗保险金额内给付恶性肿瘤住院医疗保险金。</p>
      <p class="p">
        到本保险合同满期日时，被保险人未结束本次住院治疗的，保险人继续承担因本次住院发生的、最高不超过本保险合同满期日后30日内的必需且合理的住院医疗费用，保险人按照本保险合同的约定，在保险单载明的恶性肿瘤医疗保险金额内给付恶性肿瘤住院医疗保险金。</p>
      <p class="p"><em>2.恶性肿瘤特殊门诊医疗费用</em></p>
      <p class="p">
        被保险人因初次确诊罹患恶性肿瘤在合同约定的医疗机构接受特殊门诊治疗期间发生的以下必需且合理的恶性肿瘤特殊门诊医疗费用，保险人按照本保险合同的约定，在保险单载明的恶性肿瘤医疗保险金额内给付恶性肿瘤特殊门诊医疗保险金。</p>
      <p class="p"><em>（1）门诊肾透析费；</em></p>
      <p class="p"><em>（2）门诊恶性肿瘤治疗费，包括化学疗法、放射疗法、肿瘤免疫疗法、肿瘤内分泌疗法、肿瘤靶向疗法的治疗费用；</em></p>
      <p class="p"><em>（3）器官移植后的门诊抗排异治疗费。</em></p>
      <p class="p"><em>3.恶性肿瘤门诊手术医疗费用</em></p>
      <p class="p">
        被保险人因初次确诊罹患恶性肿瘤在合同约定的医疗机构进行恶性肿瘤门诊手术治疗的，对发生的必需且合理的恶性肿瘤门诊手术医疗费用，保险人按照本保险合同的约定，在保险单载明的恶性肿瘤医疗保险金额内给付恶性肿瘤门诊手术医疗保险金。</p>
      <p class="p"><em>4.恶性肿瘤住院前后门诊急诊费用</em></p>
      <p class="p">
        被保险人经医院专科医生初次确诊罹患恶性肿瘤必须接受住院治疗，在住院前7日（含住院当日）和出院后30日（含出院当日）内，因与该次住院相同原因而接受恶性肿瘤门诊急诊治疗发生的必需且合理的门诊急诊医疗费用（不包括恶性肿瘤特殊门诊医疗费用、恶性肿瘤门诊手术医疗费用），保险人按照本保险合同的约定，在保险单载明的恶性肿瘤医疗保险金额内给付恶性肿瘤住院前后门诊急诊保险金。</p>
      <p class="p"><em>保险人对于本条以上4类费用的累计赔偿金额之和以保险单载明的恶性肿瘤医疗保险金的保险金额为限，一次或累计赔偿的金额达到本项恶性肿瘤医疗保险金额时，保险人对于被保险人在恶性肿瘤医疗保险金项下的保险责任终止。</em>
      </p>
      <p class="p"><em>（三）质子重离子医疗保险金</em></p>
      <p class="p"><em>在保险期间内，被保险人在等待期后经医院或指定医疗机构确诊初次罹患本保险合同所定义的恶性肿瘤，并在保险人认可的指定医疗机构接受质子重离子治疗，则保险人对于被保险人需个人支付的、</em>必需且合理的<em>质子重离子医疗费用（见释义16）</em>，依照约定的给付比例进行赔付。<em>保险人在本项下累计给付金额以保险单载明的质子重离子医疗保险金额为限，当保险人在本项下的累计给付金额达到本项保险金额时，保险人对被保险人在本项下的保险责任终止。</em>
      </p>
      <p class="p">本项责任下的指定医疗机构以在保险单中载明的名单为准。保险期间内保险人调整指定医疗机构的，以保险人在官方正式渠道（包括但不限于官网、官微）的通知为准。</p>
      <p class="p">
        质子重离子医疗保险金额包含于保险单载明的恶性肿瘤医疗保险金额之内，若保险人在恶性肿瘤医疗保险金责任及质子重离子医疗保险金责任下累计给付保险金的金额达到恶性肿瘤医疗保险金额，则恶性肿瘤医疗保险金责任和质子重离子医疗保险金责任同时终止，保险人对被保险人不再承担给付这两项保险金的责任。</p>
      <p class="p"><em>以上“（一）一般医疗保险金”、“（二）恶性肿瘤医疗保险金”和“（三）质子重离子医疗保险金”保险责任不包含本保险合同约定的医院或指定医疗机构内的医生开具的当前治疗必需的药品处方中所列无法在本保险合同约定的医院或指定医疗机构内获得的药品或者其他任何在本保险合同约定的医院或指定医疗机构内无法获得的医用材料或者任何其他医疗项目。</em>
      </p>
      <p class="p"><em>（四）恶性肿瘤住院津贴保险金</em></p>
      <p class="p">在保险期间内，被保险人<em>等待期</em>后经医院专科医生初次确诊罹患本保险合同所定义的恶性肿瘤，并在医院接受住院治疗，保险人按被保险人每次实际住院天数<em>扣除每次住院免赔天数后</em>乘以保险单载明的恶性肿瘤住院日津贴保险金额向被保险人给付恶性肿瘤住院津贴保险金。
      </p>
      <p class="p">除另有约定外，恶性肿瘤住院津贴责任须符合如下规定： </p>
      <p class="p">
        1、保险人对于被保险人每次住院的给付天数以本保险合同约定的天数为限；若被保险人多次住院的，保险人按本保险合同的约定分别给付保险金；对同一被保险人在同一保险期间内一次或多次住院的累计给付天数以180日为限。</p>
      <p class="p">2、到本保险合同满期日时，被保险人未结束本次住院治疗的，保险人继续承担因本次住院发生的、最高不超过本保险合同满期日后30日内的住院治疗，保险人承担给付恶性肿瘤住院津贴责任。</p>
      <p class="p">3、若被保险人本次住院治疗与前次住院原因相同，并且前次出院与本次入院间隔不超过30日的，则本次住院与前次住院视为同一次住院。</p>
      <p class="p"><em>（五）恶性肿瘤异地转诊公共交通费用保险金</em></p>
      <p class="p">在保险期间内，被保险人<em>等待期</em>后经医院专科医生初次确诊罹患本保险合同所定义的恶性肿瘤，因病情需要跨省或自治区或直辖市<em>（仅限中华人民共和国大陆境内，不包括中华人民共和国境外及香港特别行政区、澳门特别行政区、台湾地区）</em>住院治疗，经被保险人申请，由转出医院开具转院证明，保险人对被保险人发生的需被保险人个人支付的、必需且合理的因异地转诊产生的<em>客运公共交通（见释义17）</em>及救护车费用在保险单载明的恶性肿瘤异地转诊公共交通费用保险金额内给付恶性肿瘤异地转诊公共交通费用保险金。
      </p>
      <p class="p"><em>被保险人飞机舱位级别最高以经济舱（包含超级经济舱）为限，火车（含地铁、轻轨、动车、其他高速列车）以软卧或一等座为限。</em></p>
      <p class="p"><em>（六）免赔额</em></p>
      <p class="p"><em>免赔额的具体金额以及相关适用情况由投保人与保险人在订立本保险合同时协商确定，并在保险单中载明。</em></p>
      <p class="p"><em>本保险合同中所指免赔额均指年免赔额，指在本保险合同保险期间内，应由被保险人自行承担，本保险合同不予赔付的部分。被保险人从其他途径已获得的医疗费用补偿可用于抵扣免赔额，但通过基本医疗保险（见释义18）、公费医疗保险及政府救助获得的补偿，不可用于抵扣免赔额。</em>
      </p>
      <p class="p"><em>（七）补偿原则和赔付标准</em></p>
      <p class="p"><em>本保险合同适用医疗费用补偿原则。若被保险人已从其他途径（包括基本医疗保险、公费医疗、工作单位、保险人在内的任何商业保险机构等）获得医疗</em>费用补偿，<em>则保险人仅对被保险人实际发生的医疗费用扣除其所获医疗费用补偿后的余额按照本保险合同的约定进行赔付。基本医疗保险个人账户支出视为个人支付，不属于已获得的医疗费用补偿。</em>
      </p>
