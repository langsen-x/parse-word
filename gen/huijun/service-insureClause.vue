      <p class="p"><em>第四条 </em>除另有约定外，本保险合同的各项保险金受益人均为被保险人本人。</p>
      <p class="p"><em>第五条 </em>本保险合同约定的保险区域为中华人民共和国境内（<em>不含香港特别行政区、澳门特别行政区和台湾地区</em>）<em>,保险人仅对被保险人在约定保险区域内发生的保险事故承担保险责任。</em>
