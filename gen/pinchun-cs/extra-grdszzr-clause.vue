    <p class="p"><em>第八条 </em>未经保险人书面同意或者认可，被保险人不得主动建议、许诺支付或者承认对第三方负有任何责任。<em>否则，保险人不受其约束并有权重新核定，对不属于保险责任范围或超出应给付金额的，不承担给付责任。</em>
    </p>
    <p class="p"><em>被保险人接到法院通知、获悉相关机构已经开始调查或者接到责令赔偿的法律文书或者法令时，应当立即以书面形式通知保险人；当接到法院传票或者其他法律文书后，应当及时转交给保险人。未及时通知或者转交的，对由此扩大的赔偿责任，保险人不予承担。</em>
