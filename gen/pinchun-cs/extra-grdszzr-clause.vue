    <p class="p"><em>（四）被保险人所有或者在其监管、控制下的动物；</em></p>
    <p class="p"><em>（五）参加赛马、赛车或者使用枪支。</em></p>
    <p class="p"><em>第六条 对下列任何第三者责任，保险人不承担保险责任：</em></p>
    <p class="p"><em>（一）由被保险人、被保险人的家庭或者被保险人的雇员拥有、控制的财产丢失或者损坏所引起的责任；</em></p>
    <p class="p"><em>（二）与被保险人使用或者拥有的土地建筑物及该建筑物之附属物、建筑物上之悬挂物、搁置物有关的责任；</em></p>
    <p class="p"><em>（三）惩罚性损害赔偿、加重赔偿或者惩戒性赔偿金；</em></p>
    <p class="p"><em>（四）被保险人未经保险人同意自行承担的责任；</em></p>
    <p class="p"><em>（五）任何对被保险人的直系亲属，与被保险人有抚养、扶养或者赡养关系的人，或者被保险人的雇主、雇员或者商业伙伴造成的人身损害或者财产损失。</em></p>
    <h2 class="h2"><em>保险金额</em></h2>
    <p class="p"><em>第七条 </em>每一被保险人的保险金额由投保人和保险人约定，并于本附加合同中载明。</p>
