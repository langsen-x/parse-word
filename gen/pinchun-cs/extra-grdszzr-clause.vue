    <p class="p">（六）投保人、被保险人或者保险金申请人所能提供的其他与确认事故的原因、性质、损害事实或者损失程度等有关的证明和资料。</p>
    <p class="p"><em>第十条 </em>在理算保险金的数额时，需由外币转换为人民币的，所适用的汇率以中国银行公布的保险事故发生日期相应的外币与人民币汇率的中间价为准。</p>
    <p class="p"><em>第十一条 </em>若发生第三者责任事故被保险人可先从其他保险合同或者其他途径获得赔偿或者给付，被保险人应当首先向相应方请求赔偿或者给付，保险人给付的保险金以该被保险人依法应当向第三方承担的经济赔偿责任扣除已获得的赔偿后的余额为上限。
    </p>
