    <p class="p"><em>第四条 </em>个人第三者责任：</p>
    <p class="p">
      在保险期间内，被保险人在旅行期间因过失发生意外事故导致第三者人身伤亡或者财产损失的，对该被保险人依据事发地法律应当向第三方承担的经济赔偿责任，包括事先经保险人书面同意支付的其他必要的、合理的法律费用和开支，保险人根据本附加合同的约定负责赔偿，并以本附加合同项下的保险金额为上限。</p>
    <h2 class="h2"><em>责任免除</em></h2>
    <p class="p"><em>第五条 对由于下列任何原因导致的第三者责任，保险人不承担保险责任： </em></p>
    <p class="p"><em>（一）主险合同约定的责任免除事项；</em></p>
    <p class="p"><em>（二）使用、拥有、租用或者操作海、陆、空运输工具（无论有无营运执照）；</em></p>
